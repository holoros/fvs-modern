SUBROUTINE STASH (BUFFER,ILIMIT)
IMPLICIT NONE
!----------
! PG $Id$
!----------
!
!     STASH A BUFFER FULL OF DATA
!
!     BUFFER= BUFFER FULL OF DATA TO BE STASHED.
!     ILIMIT= LENGTH OF THE BUFFER.
!
!OMMONS
!
!
INCLUDE 'GLBLCNTL.f90'
!
!
!OMMONS
!
!
INTEGER ILIMIT
REAL BUFFER (ILIMIT)
WRITE (JSTASH) BUFFER
RETURN
END

