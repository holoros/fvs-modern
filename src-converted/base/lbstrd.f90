SUBROUTINE LBSTRD (IREAD,LENSET,SET,IRECNT,RECORD,KODE,WRK1,WRK2)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     READ RECORDS FORM IREAD AND FORM A SET IN STRING SET WITH
!     A TOTAL STRING LENGTH OF LENSET.  (FOR STAND AND ACTIVITY LABELS).
!
!     PART OF THE LABEL PROCESSING COMPONENT OF THE PROGNOSIS SYSTEM
!     N.L. CROOKSTON -- INTERMOUNTAIN RESEARCH STATION -- JAN 1987
!
!     IREAD = DATA SET REFERENCE NUMBER
!     LENSET= THE LENGTH OF THE STRING HOLDING THE SET
!     SET   = THE STRING HOLDING THE SET
!     IRECNT= NUMBER OF RECORDS READ ON IREAD.
!     RECROD= INPUT RECORD
!     KODE  = 0, OK,
!             1, INFORMATION OVERFILLS STRING HOLDING THE SET,
!             2, END-OF-DATA ON IREAD.
!
!     THE FOLLOWING RULES ARE FOLLOWED:
!
!     1.  LEADING BLANKS ARE DELETED, STRINGS OF OVER 1 BLANK ARE
!         CONVERTED TO ONE BLANK, BLANKS FOUND BETWEEN A NON-BLANK
!         CHAR. AND A COMMA ARE DELETED.
!     2.  ONE BLANK IS ADDED, IF NEEDED, FOLLOWING A COMMA.
!     3.  THE LAST MEMBER OF THE SET IS NOT FOLLOWED BY A COMMA.
!     4.  NULL-STRING AND ONE-BLANK SET MEMBERS ARE DELETED
!     5.  DUPLICATE SET MEMBERS ARE DELETED (A SINGLE MEMBER CAN
!         NOT OCCUR TWICE).
!     6.  PERIODS ARE DELETED.
!     7.  CONTINUATION RECORDS FOLLOW RECORDS ENDING WITH AN '&'.
!
CHARACTER*250 SET,WRK1,WRK2
CHARACTER*(*) RECORD
INTEGER KODE,IRECNT,LENSET,IREAD,IRPS,KGET,LNWRK1,LNWRK2,KINT
INTEGER I,ISTLNB
!
!     INITIALIZE RETURN CODE, LENSET, AND SET.
!
KODE=0
LENSET=0
SET=' '
IRPS=0
!
!     LOOP OVER ALL INPUT MEMBERS.
!
10 CONTINUE
!
!     GET ONE MEMBER FROM THE INPUT FILE.
!
CALL LBGET1 (IREAD,IRECNT,RECORD,IRPS,LNWRK2,WRK2,0,KGET)
KODE=KGET
IF (KODE.GT.0) GOTO 100
!
!     IF THE LENGTH OF THE MEMBER IS ZERO, THEN NO MEMBER WAS FOUND.
!
IF (LNWRK2.EQ.0) GOTO 100
!
!     FIND THE UNION OF THE MEMBER WITH THE CURRENT SET.
!
LNWRK1=LENSET
WRK1=SET
CALL LBUNIN (LNWRK1,WRK1,LNWRK2,WRK2,LENSET,SET,KINT)
IF (KINT.GT.0) THEN
!
!        IF THE LABEL SET IS FULL, DROP RECORDS AS NECESSARY
!        TO PASS BY THE END OF THE LABEL SET DEFINITION.
!
20    CONTINUE
   I=ISTLNB(RECORD)
   IF (I.GT.0) THEN
      IF (RECORD(I:I).EQ.'&') THEN
         READ (IREAD,'(A)',END=200) RECORD
         IRECNT=IRECNT+1
         GOTO 20
      ENDIF
   ENDIF
ENDIF
KODE=KINT
IF (KODE.GT.0) GOTO 100
!
!     LOOP TO GET ANOTHER MEMBER, UNLESS SIGNALED THAT NO MORE EXIST.
!
GOTO 10
!
100 CONTINUE
RETURN
200 CONTINUE
KODE=2
RETURN
END
