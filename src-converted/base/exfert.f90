SUBROUTINE EXFERT
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     EXTRA EXTERNAL REFERENCES FOR FERTILIZER CALLS
!----------
LOGICAL LNOTBK(12),LKECHO
REAL ARRAY(12)
INTEGER IRECNT,JOSTND
CHARACTER*8 KEYWRD
CHARACTER*10 KARD(12)
REAL RDANUW
INTEGER IDANUW
CHARACTER*1 CDANUW
LOGICAL LDANUW
!----------
! ENTRY FFIN
!----------
ENTRY FFIN (JOSTND,IRECNT,KEYWRD,ARRAY,LNOTBK,KARD,LKECHO)
  CALL ERRGRO (.TRUE.,11)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  IDANUW = JOSTND
  IDANUW = IRECNT
  CDANUW = KEYWRD(1:1)
  RDANUW = ARRAY(1)
  LDANUW = LNOTBK(1)
  CDANUW = KARD(1)(1:1)
  LDANUW = LKECHO
RETURN
!----------
! ENTRY FFIN
!----------
ENTRY FFERT
RETURN
!
END
