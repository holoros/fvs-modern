! COMMON $Id$
!----------
!
!     PARAMETERS FOR THE PPE:
!
INTEGER MXSTND,MXNBS,MXDAFL,NDAREC,MXLLSZ,IRECLN,IRESRV,MXHRVP
PARAMETER (MXSTND=10000)
PARAMETER (MXNBS=MXSTND*7)
PARAMETER (MXDAFL=5)
PARAMETER (NDAREC=MXSTND*80)
PARAMETER (MXLLSZ=MXSTND*4)
PARAMETER (IRECLN=1024)
PARAMETER (IRESRV=((NDAREC+5)/IRECLN)+2)
PARAMETER (MXHRVP=10)
!
!     MXSTND= THE MAX NUMBER OF STANDS THAT THE PPE CAN HOLD
!     MXNBS = THE MAX NUMBER OF NEIGHBORS DATA RECORDS THE PPE
!             CAN HOLD (SEE SPNBCM).
!     MCDAFL= THE MAX NUMBER OF ALL-DATA DA FILES.
!     NDAREC= TOTAL ALL-DATA DA RECORDS AVAILABLE OVER ALL FILES.
!     MXLLSZ= THE MAX NUMBER OF ENTRIES IN THE GLOBAL OPTION PROCESSOR
!             LINK LIST (SEE ROUTINES THAT START WITH LL AND LLCOM...
!             DIMENSION OF INEXR AND IELEM).
!     IRECLN= RECORD LENGTH IN 4-BYTE WORDS OF THE DA-ALL DATA FILE.
!     IRESRV= NUMBER OF RECORDS RESERVED TO WRITE THE RECORD POINTERS
!             IN PPAUSE.
!     MXHRVP= MAX NUMBER OF MULTISTAND POLICIES THAT CAN BE DEFINED
!
!-----END SEGMENT
