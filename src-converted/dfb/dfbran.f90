SUBROUTINE DFBRAN(SEL)
IMPLICIT NONE
!----------
! DFB $Id$
!----------
!
!  THIS RANDOM NUMBER GENERATED WAS MODIFIED FROM THE ALGORTHM
!  FOUND IN IN THE IMSL LIBRARY VOL 1 EDITION 4 TO 5,
!  DEC. 1, 1975, AND CAN BE FOUND IN THE FOLLOWING REFERENCES:
!
!  LEWIS, P.A.W., GOODMAN, A.S., AND MILLER, J.M. PSUEDO-RANDOM
!  NUMBER GENERATOR FOR THE SYSTEM/360, IBM SYSTEMS JOURNAL, NO. 2,
!  1969.
!
!  LEARMONTH, G.P. AND LEWIS, P.A.W., NAVAL POSTGRADUATE SCHOOL
!  RANDOM NUMBER GENERATOR PACKAGE LLRANDOM, NPS55LW73061A, NAVAL
!  POSTGRADUATE SCHOOL, MONTEREY, CALIFORNIA, JUNE, 1973.
!
!  LEARMONTH, G.P., AND LEWIS, P.A.W., STATISTICAL TESTS OF SOME
!  WIDELY USED AND RECENTLY PROPOSED UNIFORM RANDOM NUMBER
!  GENERATORS. NPS55LW73111A, NAVAL POSTGRADUATE SCHOOL, MONTEREY
!  CALIFORNIA, NOV. 1973.
!
!  THE CODE WAS WRITTEN BY N.L. CROOKSTON, FORESTRY SCIENCES LAB,
!  OCT 1982, FROM THE ALGORITHM PUBLISHED IN THE IMSL MANUAL AND
!  TESTED BY COMPAIRING THE FIRST 10000 DRAWS FORM THIS VERSION AND
!  AND THE GENERATOR 'GGUBS' IN THE 1981 EDITION OF IMSL.
!
!  CALLED BY :
!     DFBGO   [DFB]
!     DFBSCH  [DFB]
!
!  CALLS :
!     NONE
!
!  ENTRY POINTS :
!     DFBNSD
!     DFBGSD
!     DFBCSD
!
!  PARAMETERS :
!     SEL    - THE RANDOM NUMBER GENERATED.
!
!  LOCAL VARIABLES :
!     S0     - SEED VALUE FOR RANDOM NUMBER GENERATOR.
!     S1     - VARIABLE IN RANDOM NUMBER GENERATOR THAT BECOMES THE NEXT
!              SEED VALUE.
!     SS     - HOLDS THE ORIGINAL SEED
!

LOGICAL LSET
REAL   SEL, SEED
DOUBLE PRECISION SS0

INCLUDE 'PRGPRM.f90'

INCLUDE 'CONTRL.f90'

INCLUDE 'DFBCOM.f90'

S1 = DMOD(16807D0 * S0, 2147483647D0)
SEL = S1 / 2147483648D0
S0 = S1

RETURN


ENTRY DFBNSD (LSET,SEED)
!------------------------------
!     THIS ENTRY POINT IS USED TO START THE RANDOM NUMBER GENERATOR
!     OVER OR RESEED THE RANDOM NUMBER GENERATOR.
!
!     PARAMETERS :
!        LSET   - IF SET TO .TRUE. THEN THE RANDOM NUMBER GENERATOR IS
!                 RESEEDED WITH THE VALUE OF SEED.  IF SET TO .FALSE.
!                 THE RANDOM NUMBER GENERATOR WILL BE RESTARTED WITH THE
!                 ORIGINAL SEED.
!        SEED   - THE NEW SEED IF THE RANDOM NUMBER GENERATOR IS BEING
!                 RESEEDED.
!
IF (LSET) GOTO 10
SEED = SS
S0 = SEED
RETURN

10 CONTINUE
IF (AMOD(SEED,2.0) .EQ. 0.0) SEED = SEED + 1.0
SS = SEED
S0 = SEED
RETURN

ENTRY DFBGSD (SS0)
!------------------------------
!     THIS ENTRY POINT IS USED TO GET THE CURRENT SEED IN THE RANDOM
!     GENERATOR.
!
!     PARAMETERS :
!        SS0    - HOLDS THE CURRENT RANDOM NUMBER SEED (OUTPUT).
!
SS0 = S0

RETURN


ENTRY DFBCSD (SS0)
!------------------------------
!     THIS ENTRY POINT IS USED TO CHANGE THE SEED IN THE RANDOM NUMBER
!     GENERATOR.
!
!     PARAMETERS :
!        SS0    - HOLDS THE NEW RANDOM NUMBER SEED (INPUT).
!
S0 = SS0

RETURN
END
