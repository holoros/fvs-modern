CHARACTER*4 MDMGID(MXSTND)
CHARACTER*8 MDSTDS(MXSTND)
COMMON /PPMDCC/ MDSTDS,MDMGID
LOGICAL LMDDEB,LNEAR,LMDSPR,LMDVOL,LMDDBH
INTEGER JMPBPR,MDSTND,NDBHCL,MDHAB(MXSTND),MDSDIX(MXSTND), &
           IMDYR1,IMDYR2,IMDYRT,JMDVOL,JMDDBH,MRTYR1,MRTYR2, &
           MRTCNT,IDEDYR,MXZNAB
REAL    PRBMPB(MXSTND),VOLDED(MXSTND),VOLNHS(MXSTND), &
           GRNLP(MXSTND,8),SADLPP(MXSTND),ELEVMD(MXSTND), &
           PHLOEM(MXSTND,8),VOLLP(MXSTND,8),TACRES,ZNUDED(MXSTND,8), &
           PRNOIN(7),ZINMOR(7),ZMXMOR(7),RNGSHT,RNGLNG, &
           CUMDED(MXSTND,8),DEDLP(MXSTND,8),PLONG
COMMON /PPMDCM/ JMPBPR,LMDDEB,MDSTND,NDBHCL,PRBMPB,VOLDED,VOLNHS, &
                   GRNLP,SADLPP,ELEVMD,MDHAB,MDSDIX,PHLOEM,VOLLP, &
                   TACRES,ZNUDED,IMDYR1,IMDYR2,IMDYRT,PRNOIN,ZINMOR, &
                   ZMXMOR,JMDVOL,JMDDBH,LMDVOL,LMDDBH,LMDSPR,MXZNAB, &
                   RNGSHT,RNGLNG,MRTYR1,MRTYR2,MRTCNT,CUMDED,DEDLP, &
                   IDEDYR,LNEAR,PLONG
!
!     LMDDEB= DEBUG SWITCH
!     LNEAR = TRUE IF NEIGHBORS DATA ARE PRESENT AND USEFUL
!     LMDSPR= TRUE IF LOCATION/AREA DATA ARE PRESENT AND USEFUL
!     LMDVOL= TRUE IF VOL LIVE AND DEAD STATS ARE OUTPUT
!     JMDVOL= OUTPUT DATA SET REF NUMBER FOR VOL LIVE AND DEAD STATS
!     LMDDBH= TRUE IF TREE PER ACRE BY DBH CLASS DATA IS OUTPUT
!     JMDDBH= OUTPUT DATA SET REF NUMBER FOR DBH CLASS DATA.
!     IDEDYR= YEAR THAT THE STATS IN DEDLP, ZNUDED, AND CUMDED APPLY
!             TO (USED TO CARRY OVER AN OUTBREAK IN PROGRESS). -999 IF
!             IDEDYR IS NOT SET.
!     DEDLP = ARRAY BY STAND AND DBH CLASS OF CUM DEAD LPP (INCLUDING
!             DEAD TREES KILLED IN DIFFERENT PPE MASTER CYCLES).
!     ZNUDED= ARRAY BY STAND AND DBH CLASS OF NEWLY INFESTED LPP
!     MDSDIX= LIST OF PPE STANDS THAT ARE IN A CURRENT OUTBREAK. DURING
!             THE INITIAL PROGNOSIS CYCLE, THIS ARRAY HOLDS THE
!             INVENTORY YEAR FOR EACH STAND IN THE RUN, INDEXED BY
!             PROGNOSIS STAND NUMBER.
!     MDSTND= NUMBER OF STANDS IN A MPB OUTBREAK.
!     IMDYR1= FIRST YEAR OF CURRENT OUTBREAK, ZERO OTHERWISE.
!     IMDYR2= LAST YEAR OF CURRENT OUTBREAK.
!     IMDYRT= NUMBER OF YEARS REQUESTED FOR CURRENT OUTBREAK.
!     GRNLP = ARRAY BY STAND AND DBH CLASS OF REMAINING LIVE LPP
!     NDBHCL= NUMBER OF DIAMETER CLASSES
!     MDSTND= NUMBER OF STANDS BEING PROJECTED
!     GRNLP = ARRAY BY STAND AND DBH CLASS OF THE REMAINING UNINFESTED
!             LPP TREES (MEASURED IN TREES PER ACRE)
!     VOLLP = ARRAY BY STAND AND DBH CLASS OF THE AVERAGE VOLUME PER
!             TREE OF LODGEPOLE PINE.  DURING THE INVENTORY YEAR, THIS
!             ARRAY HOLDS THE NUMBER OF DEAD TREES PER ACRE BY DBH
!             CLASS THAT ARE IN THE INVENTORY DATA.
!     PHLOEM= ARRAY BY STAND AND DBH CLASS OF THE AVERAGE VOLUME OF
!             A LIVE LPP TREE.
!     CUMDED= CUMMULITIVE DEAD OVER WITHIN AN OUTBREAK PERIOD.
!             WHEN MPBHIST KEYWORD IS USED, CUMDED CARRIES THE DEAD
!             TREES PER ACRE CORRESPOINDING TO THE MASTER STARTING
!             YEAR AND EACH OF UP TO 7 YEARS BEFORE.
!     MRTYR1= *** NOT USED, CURRENTLY ***
!     MRTYR2= *** NOT USED, CURRENTLY ***
!     MRTCNT= THE NUMBER OF RECORDS...THE IDS ARE STORED IN MDSTDS.
!     PRBMPB= PROBABILITY OF MPB OUTBREAK BY STAND.
!     PORTN = THE PROPORTION OF THE BASE TO BE DISPERSED
!     RNGLNG= LONG RANGE DISPERSAL CRITERIA LIMIT
!     RNGSHT= SHORT RANGE DISPERSAL CRITERIA LIMIT
!     RELMOR= ARRAY BY STAND AND DBH CLASS OF THE RELATIVE MORTALITY
!             THAT OCCURRED DURING THE PREVIOUS YEAR
!     SADLPP= NUMBER OF SAD TREES PER ACRE.  THESE ARE TREES THAT
!             ARE IN THE LOWER 20%TILE OF THE DBH DISTRIBUTION,
!             BETWEEN 4.5 AND 6.5 INCHES DBH, HAVE LESS THAN 30% CROWN
!             AND HAVE SOME KIND OF DAMAGE *** CURRENTLY, DAMAGE CODES
!             ARE NOT USED ****
!     VOLDED= ARRAY BY STAND OF THE VOLUME OF DEAD HOST
!     VOLNHS= ARRAY BY STAND OF THE VOLUME OF LIVE NON-HOST SPECIES
!     ZINMOR= ARRAY BY DBH CLASS OF THE INITITAL MORTALITY
!             PROBABILITIES
!     ZMXMOR= ARRAY BY DBH CLASS OF THE MAXIMUM ANNUAL MORTALITY PROBS
!
!-----END SEGMENT
