SUBROUTINE PTSYM (IPLTNO,L1,L2,L3,L4,L5,L6,L7,L8,L9,L10)
IMPLICIT NONE
!----------
! LPMPB $Id$
!----------
!
!      ROUTINE TO ESTABLISH PLOTTING SYMBOLS AND INITIALIZE
!      PLOTTING SYSTEM.
!
!     NOTE- CALLING ROUTINE MUST SPECIFY THE SYMBOLS TO BE
!     USED AS HOLLERITH CONSTANTS. SYMBOL PARAMETERS NOT USED
!     MUST BE PRESENT BUT NEED NOT BE HOLLERITH.
!     EG.  CALL PTSYM(1,1H1,1H2,1HX,1H+,0,0,0,0,0,0)
!
! Revision History
!   02/08/88 Last noted revision date.
!   07/02/10 Lance R. David (FMSC)
!     Added IMPLICIT NONE.
!----------
!
!OMMONS
!
!
INCLUDE 'PT.f90'
!
!
!OMMONS
!
CHARACTER*1 L1,L2,L3,L4,L5,L6,L7,L8,L9,L10
INTEGER IPLTNO, J
!
DO 1 J=1,10
PTMAX(IPLTNO,J)=-1E35
PTMIN(IPLTNO,J)=+1E35
PTU(IPLTNO,J)=0.
PTL(IPLTNO,J)=0.
IPTCOD(IPLTNO,J)=100
1 CONTINUE
!
IPTSPL(IPLTNO)=0
IPTVAR(IPLTNO)=0
!
IPTLET(IPLTNO,1 )=L1
IPTLET(IPLTNO,2 )=L2
IPTLET(IPLTNO,3 )=L3
IPTLET(IPLTNO,4 )=L4
IPTLET(IPLTNO,5 )=L5
IPTLET(IPLTNO,6 )=L6
IPTLET(IPLTNO,7 )=L7
IPTLET(IPLTNO,8 )=L8
IPTLET(IPLTNO,9 )=L9
IPTLET(IPLTNO,10)=L10
!
RETURN
END
