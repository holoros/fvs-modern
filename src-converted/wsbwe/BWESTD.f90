!     AVYRMX=
!     BWMXCD= MAX OF CDEF
!     CDEF=   CUMULATIVE (5-YR MAX) DEFOLIATION TO DATE BY HOST AND SIZE CLASS.
!     CUMDEF= CUMULATIVE DEFOLIATION OF HOST IHOST BY TREE SIZE
!             ITREE AND YEAR IYEAR (5 YEARS MAXIMUM).
!     FOLADJ= AMOUNT OF ADJUSTED POTENTIAL BIOMASS IN GRAMS,
!             BROKEN DOWN BY HOST, CROWN, AND AGE STEP.
!     FOLPOT= POTENTIAL FOLIAGE POSSIBLE AT BEGINNING OF MODEL
!             RUN FOR CROWN LEVEL ICROWN OF HOST IHOST DURING 20 DEGREE
!             DAY STEP IAGE.
!     PEDDS=  PROPORTION OF EXPECTED DELTA DIAMETER SQUARED FOR TREE
!             SIZE ISIZ ON HOST IHOST.
!     PEHTG=  PROPORTIONAL EXPECTED HEIGHT GROWTH OF TREE SIZE ISIZ
!             AND HOST IHOST
!     POFPOT=
!     PRBIO=  PROPORTION OF RETAINED BIOMASS OF FOLIAGE AGE IAGE IN
!             CROWN LEVEL ICROWN OF HOST IHOST.
!     PRCRN3=
!     RDDSM1=
!     RELFX=
!     RELFY=
!     RHTGM1=
!     THEOFL=
!     IFHOST= HOST PRESENCE INDICATOR. 1=HOST IS PRESENT
!     FNEW =  GRAMS OF NEW BUDS BY CROWN AND HOST
!     FOLD1=  GRAMS OF 1-YEAR-OLD FOLIAGE BY CROWN AND HOST
!     FOLD2=  GRAMS OF 2-YEAR-OLD FOLIAGE BY CROWN AND HOST
!     FREM=   GRAMS OF FOLIAGE OLDER THAN 2 YEARS, BY CROWN AND HOST
!     FOLNH=  GRAMS OF NON-HOST FOLIAGE BY CROWN LEVEL
!     BWTPHA= NUMBER OF BUDWORM HOST TREES PER HECTARE BY HOST AND TREE SIZE
