SUBROUTINE CCFCAL(ISPC,D,H,JCR,P,LTHIN,CCFT,CRWDTH,MODE)
IMPLICIT NONE
!----------
! SO $Id$
!----------
!  THIS ROUTINE COMPUTES CROWN WIDTH AND CCF FOR INDIVIDUAL TREES.
!  CALLED FROM DENSE, REGENT (TT VARIANT), PRTRLS, AND SSTAGE.
!
!  ARGUMENT DEFINITIONS:
!    ISPC = NUMERIC SPECIES CODE
!       D = DIAMETER AT BREAST HEIGHT
!       H = TOTAL TREE HEIGHT
!     JCR = CROWN RATIO IN PERCENT (0-100)
!       P = TREES PER ACRE
!   LTHIN = .TRUE. IF THINNING HAS JUST OCCURRED
!         = .FALSE. OTHERWISE
!    CCFT = CCF REPRESENTED BY THIS TREE
!  CRWDTH = CROWN WIDTH OF THIS TREE
!    MODE = 1 IF ONLY NEED CCF RETURNED
!           2 IF ONLY NEED CRWDTH RETURNED
!
!  DIMENSION AND DATA STATEMENTS FOR INTERNAL VARIABLES.
!
!     CCF COEFFICIENTS FOR TREES THAT ARE GREATER THAN 10.0 IN. DBH:
!      RD1 -- CONSTANT TERM IN CROWN COMPETITION FACTOR EQUATION,
!             SUBSCRIPTED BY SPECIES
!      RD2 -- COEFFICIENT FOR SUM OF DIAMETERS TERM IN CROWN
!             COMPETITION FACTOR EQUATION,SUBSCRIPTED BY SPECIES
!      RD3 -- COEFFICIENT FOR SUM OF DIAMETER SQUARED TERM IN
!             CROWN COMPETITION EQUATION, SUBSCRIPTED BY SPECIES
!
!     CCF COEFFICIENTS FOR TREES THAT ARE LESS THAN 10.0 IN. DBH:
!      RDA -- MULTIPLIER.
!      RDB -- EXPONENT.  CCF(I) = RDA*DBH**RDB
!
!  SPECIES ORDER:
!  1=WP,  2=SP,  3=DF,  4=WF,  5=MH,  6=IC,  7=LP,  8=ES,  9=SH,  10=PP,
! 11=JU, 12=GF, 13=AF, 14=SF, 15=NF, 16=WB, 17=WL, 18=RC, 19=WH,  20=PY,
! 21=WA, 22=RA, 23=BM, 24=AS, 25=CW, 26=CH, 27=WO, 28=WI, 29=GC,  30=MC,
! 31=MB, 32=OS, 33=OH
!
!  SOURCE OF CCF COEFFICIENTS:
!     1 = PAINE AND HANN TABLE 2: WESTERN WHITE PINE
!     2 = PAINE AND HANN TABLE 2: SUGAR PINE
!     3 = PAINE AND HANN TABLE 2: DOUGLAS-FIR
!     4 = PAINE AND HANN TABLE 2: WHITE/GRAND FIR
!     5 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: PONDEROSA PINE
!     6 = PAINE AND HANN TABLE 2: INCENSE CEDAR
!     7 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!     8 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: ENGELMANN SPRUCE
!     9 = R5 FIAS USER'S GUIDE (SEE R5CRWD): RED FIR
!    10 = PAINE AND HANN TABLE 2: PONDEROSA PINE
!    11 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    12 = PAINE AND HANN TABLE 2: WHITE/GRAND FIR
!    13 = PAINE AND HANN TABLE 2: RED FIR
!    14 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: GRAND FIR
!    15 = PAINE AND HANN TABLE 2: NOBLE FIR
!    16 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    17 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN LARCH
!    18 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN RED CEDAR
!    19 = PAINE AND HANN TABLE 2: WESTERN HEMLOCK
!    20 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    21 = PAINE AND HANN TABLE 2: TANOAK
!    22 = PAINE AND HANN TABLE 2: TANOAK
!    23 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    24 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN RED CEDAR
!    25 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    26 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    27 = R5 FIAS USER'S GUIDE (SEE R5CRWD): OREGON WHITE OAK
!    28 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    29 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    30 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    31 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    32 = PAINE AND HANN TABLE 2: CALIFORNIA DOUGLAS-FIR
!    33 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!
!      PAINE AND HANN, 1982. MAXIMUM CROWN WIDTH EQUATIONS FOR
!        SOUTHWESTERN OREGON TREE SPECIES. RES PAP 46, FOR RES LAB
!        SCH FOR, OSU, CORVALLIS. 20PP.
!
!      WYKOFF, CROOKSTON, STAGE, 1982. USER'S GUIDE TO THE STAND
!        PROGNOSIS MODEL. GEN TECH REP INT-133. OGDEN, UT:
!        INTERMOUNTAIN FOREST AND RANGE EXP STN. 112P.
!----------
!
!  CROWN WIDTH EQUATIONS FOR REGION 5:
!  FROM WARBINGTON/LEVITAN & DIXON --- SEE DOCUMENTATION IN
!                                      SUBROUTINE **R5CRWD**
!
!  CROWN WIDTH EQUATIONS FOR REGION 6:
!  FROM DONNELLY --- SEE DOCUMENTATION IN SUBROUTINE **R6CRWD**
!----------
!OMMONS
!
!
INCLUDE  'PRGPRM.f90'
!
!
INCLUDE  'PLOT.f90'
!
!
!OMMONS
!----------
!----------
LOGICAL LTHIN
REAL RD1(MAXSP),RD2(MAXSP),RD3(MAXSP),RDA(MAXSP),RDB(MAXSP)
REAL CRWDTH,CCFT,P,H,D
INTEGER MODE,JCR,ISPC
INTEGER IDANUW
LOGICAL LDANUW
!
DATA RD1/ &
        .0186,     .0392,     .0388,     .0690,       .03, &
        .0194,    .01925,       .03,        .0,     .0219, &
       .01925,     .0690,     .0172,      0.04,   0.02453, &
       .01925,      0.02,      0.03,   0.03758,    0.0204, &
      0.03561,   0.03561,    0.0204,       .03,    0.0204, &
       0.0204,        .0,    0.0204,    0.0204,     .0204, &
        .0204,     .0388,     .0204/
DATA RD2/ &
        .0146,     .0180,     .0269,     .0225,      .018, &
        .0142,    .01676,     .0173,        .0,     .0169, &
       .01676,     .0225,    .00877,    0.0270,   0.01147, &
       .01676,    0.0148,    0.0238,   0.02329,    0.0246, &
       0.0273,    0.0273,    0.0246,     .0238,    0.0246, &
       0.0246,        .0,     .0246,     .0246,     .0246, &
        .0246,     .0269,     .0246/
DATA RD3/ &
       .00288,    .00207,    .00466,    .00183,    .00281, &
       .00261,    .00365,    .00259,        .0,    .00325, &
       .00365,    .00183,    .00112,   0.00405,   0.00134, &
       .00365,   0.00338,   0.00490,   0.00361,    0.0074, &
      0.00524,   0.00524,    0.0074,    .00490,    0.0074, &
       0.0074,        .0,     .0074,     .0074,     .0074, &
        .0074,    .00466,     .0074/
DATA RDA/ &
     0.009884,  0.007244,  0.017299,  0.015248,   0.011109, &
     0.008915,  0.009187,  0.007875,        .0,   0.007813, &
     0.009187,  0.015248,  0.011402,  0.015248,         .0, &
     0.009187,  0.007244,  0.008915,        .0,         .0, &
           .0,        .0,        .0,  0.008915,         .0, &
           .0,        .0,        .0,        .0,         .0, &
           .0,  0.017299,        .0/
DATA RDB/ &
       1.6667,    1.8182,    1.5571,    1.7333,    1.7250, &
       1.7800,    1.7600,    1.7360,        .0,    1.7780, &
       1.7600,    1.7333,    1.7560,    1.7333,        .0, &
       1.7600,    1.8182,    1.7800,        .0,        .0, &
           .0,        .0,        .0,    1.7800,        .0, &
           .0,        .0,        .0,        .0,        .0, &
           .0,    1.5571,        .0/
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
IDANUW = JCR
LDANUW = LTHIN
!
!----------
!  INITIALIZE RETURN VARIABLES.
!----------
CCFT = 0.
CRWDTH = 0.
IF(P.LE.0.)GOTO 900
!----------
!  COMPUTE CROWN WIDTH, MODE=2; ALSO NEED CW TO COMPUTE CCF FOR
!  SPECIES 9=SH AND 27=WO
!----------
IF(MODE.EQ.2 .OR. ISPC.EQ.9 .OR. ISPC.EQ.27)THEN
!
  SELECT CASE (IFOR)
  CASE(4,5,6,7,8,9)
    CALL R5CRWD (ISPC,D,H,CRWDTH)
!----------
!  FOR REGION 6 FORESTS, COMPUTE CROWN WIDTH WITH DONNELLY EQUATIONS
!----------
  CASE DEFAULT
    CALL R6CRWD (ISPC,D,H,CRWDTH)
  END SELECT
!----------
!  LIMIT CROWN WIDTH FOR PRINTING ON TREELIST.
!----------
  IF(CRWDTH .GT. 99.9) CRWDTH=99.9
ENDIF
!----------
!  COMPUTE CCF
!----------
IF(MODE.EQ.1 .OR. &
      ISPC.EQ.11.OR.ISPC.EQ.16.OR.ISPC.EQ.24) THEN
!
  SELECT CASE (ISPC)
!----------
!  SPECIES FROM WC
!----------
  CASE(15,19:23,25,26,28:31,33)
    IF (D .LT. 1.0) THEN
      CCFT = D * (RD1(ISPC)+RD2(ISPC)+RD3(ISPC))
    ELSE
      CCFT = RD1(ISPC) + RD2(ISPC)*D + RD3(ISPC)*D**2.0
    ENDIF
    CCFT = CCFT * P
  CASE(9,27)
!----------
!  SPECIES FROM CA
!----------
    CCFT= CRWDTH  * CRWDTH  * 0.001803
    CCFT = CCFT * P
!----------
!  ALL OTHER SPECIES
!----------
  CASE DEFAULT
    IF (D .GE. 1.0) THEN
      CCFT = RD1(ISPC) + D*RD2(ISPC) + D*D*RD3(ISPC)
    ELSE IF(D.GT.0.1) THEN
      CCFT = RDA(ISPC) * (D**RDB(ISPC))
    ELSE
      CCFT=0.001
    ENDIF
!
    CCFT = CCFT * P
  END SELECT
ENDIF
!
IF((MODE.EQ.2).AND. &
     (ISPC.EQ.11.OR.ISPC.EQ.16.OR.ISPC.EQ.24)) THEN
!----------
!  COMPUTE CROWN WIDTH WHEN CRWDTH=F(CCF) - UT AND TT SPECIES
!----------
  CRWDTH = SQRT(CCFT/0.001803)
  IF(CRWDTH .GT. 99.9) CRWDTH=99.9
ENDIF
!
900 CONTINUE
RETURN
END
