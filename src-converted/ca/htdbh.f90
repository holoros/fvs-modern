!   41 = JU__     WALNUT                    JUGLANS sp.
!   42 = LIDE3    TANOAK                    LITHOCARPUS DENSIFLORUS
!   43 = PLRA     CALIFORNIA SYCAMORE       PLATANUS RACEMOSA
!   44 = POTR5    QUAKING ASPEN             POPULUS TREMULOIDES
!   45 = POBAT    BLACK COTTONWOOD          POPULUS TRICHOCARPA
!   46 = SA__     WILLOW                    SALIX sp.
!   47 = TOCA     CALIFORNIA NUTMEG         TORREYA CALIFORNICA
!   48 = UMCA     CALIFORNIA LAUREL         UMBELLULARIA CALIFORNICA
!
!   49 =          OTHER HARDWOODS
!   50 =          COAST REDWOOD             SEQUIOA SEMPERVIRENS
!----------
! SPECIES WITH CURTIS/ARNEY EQNS FIT FROM DATA FOR THIS VARIANT ---
!       DF, IC, KP, GP, LP, MA, BO, WO, PP, JP, RF/SF, SP, WF, WP
! COEFFICIENTS FOR OTHER SPECIES ARE FROM OTHER R6 VARIANTS
!----------
