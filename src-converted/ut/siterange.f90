SUBROUTINE SITERANGE(IWHO,IS,SILOW,SIHIGH)
IMPLICIT NONE
!----------
! UT $Id$
!----------
!
! SUBROUTINE TO RETURN THE SITE INDEX RANGE FOR A SPECIES.
!
! THIS SUBROUTINE IS CALLED FROM **SITSET** AND **REGENT**.
!----------
!  SPECIES ORDER:
!  1=WB,  2=LM,  3=DF,  4=WF,  5=BS,  6=AS,  7=LP,  8=ES,  9=AF, 10=PP,
! 11=PI, 12=WJ, 13=GO, 14=PM, 15=RM, 16=UJ, 17=GB, 18=NC, 19=FC, 20=MC,
! 21=BI, 22=BE, 23=OS, 24=OH
!
! VARIANT EXPANSION:
! GO AND OH USE OA (OAK SP.) EQUATIONS FROM UT
! PM USES PI (COMMON PINYON) EQUATIONS FROM UT
! RM AND UJ USE WJ (WESTERN JUNIPER) EQUATIONS FROM UT
! GB USES BC (BRISTLECONE PINE) EQUATIONS FROM CR
! NC, FC, AND BE USE NC (NARROWLEAF COTTONWOOD) EQUATIONS FROM CR
! MC USES MC (CURL-LEAF MTN-MAHOGANY) EQUATIONS FROM SO
! BI USES BM (BIGLEAF MAPLE) EQUATIONS FROM SO
! OS USES OT (OTHER SP.) EQUATIONS FROM UT
!
!----------
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
!OMMONS
!----------
REAL SITELO(MAXSP),SITEHI(MAXSP),SLO(MAXSP),SHI(MAXSP)
REAL SILOW, SIHIGH
INTEGER IS,IWHO
!----------
!  DEFINITION OF VARIABLES:
!----------
!      IS -- FVS SPECIES INDEX NUMBER
!    IWHO -- 1 IF CALLED FROM SUBROUTINE **SITSET**
!            2 IF CALLED FROM SUBROUTINE **REGENT**
!  SITEHI -- UPPER SITE INDEX VALUE FOR SUBROUTINE **SITSET**
!  SITELO -- LOWER SITE INDEX VALUE FOR SUBROUTINE **SITEST**
!     SHI -- UPPER SITE INDEX VALUE FOR SUBROUTINE **REGENT**
!     SLO -- LOWER SITE INDEX VALUE FOR SUBROUTINE **REGENT**
!----------
!  DATA STATEMENTS:
!----------
!  SITE INDEX RANGE VALUES USED IN SUBROUTINE **SITSET**.
!  THESE VALUES SHOULD BE BASED ON THE BASE-AGE OF THE SITE CURVE
!  BEING USED FOR THAT SPECIES.
!
!  WHEN CHANGING THESE VALUES, ALSO CHANGE THE CORRESPONDING VALUES IN
!  ARRAYS SLO AND SHI, ADJUSTING VALUES FOR WB, LM, WF, BS, LP, ES, AF,
!  PP, AND OS AS DIRECTED.
!----------
DATA SITELO/ &
     20.,  20.,  30.,  40.,  40.,  30.,  40.,  40.,  50.,  40., &
      5.,   5.,   5.,   5.,   5.,   5.,  20.,  30.,  30.,   5., &
      5.,  15.,  20.,   5./
!
DATA SITEHI/ &
     50.,  50.,  70.,  80.,  90.,  70.,  85., 100.,  90.,  80., &
     20.,  15.,  20.,  20.,  15.,  15.,  60., 120.,  90.,  15., &
     30.,  40.,  50.,  20./
!----------
!  SITE INDEX RANGE VALUES USED IN SUBROUTINE **REGENT**.
!  VALUES HERE FOR WB, LM, WF, BS, LP, ES, AF, PP, AND OS ARE ADJUSTED
!  TO A 50-YEAR AGE BASIS EQUIVALENT TO THE BASE-AGE BASIS VALUES THAT
!  APPEAR IN ARRAYS SITELO AND SITEHI.
!----------
DATA SLO/ &
     15.,  15.,  30.,  20.,  20.,  30.,  27.,  20.,  27.,  23., &
      5.,   5.,   5.,   5.,   5.,   5.,  20.,  30.,  30.,   5., &
      5.,  15.,  15.,   5./
!
DATA SHI/ &
     32.,  32.,  70.,  50.,  58.,  70.,  53.,  65.,  58.,  50., &
     20.,  15.,  20.,  20.,  15.,  15.,  60., 120.,  90.,  15., &
     30.,  40.,  32.,  20./
!----------
SELECT CASE (IWHO)
!
!  CALLED FROM SUBROUTINE **SITSET**
!
  CASE (1)
    SILOW = SITELO(IS)
    SIHIGH = SITEHI(IS)
!
!  CALLED FROM SUBROUTINE **REGENT**
!
  CASE (2)
    SILOW = SLO(IS)
    SIHIGH = SHI(IS)
!
!  SPACE FOR ADDITIONAL CALLS
!
  CASE DEFAULT
    SILOW = 0.
    SIHIGH = 999.
!
END SELECT
!
RETURN
END
