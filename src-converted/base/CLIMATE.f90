!ODE SEGMENT CLIMATE
!----------
! COMMON $Id$
!----------
!
LOGICAL LAESTB,LCLIMATE
!
CHARACTER*8  ATTR_LABELS
CHARACTER*20 CLIMATE_NAME
!
INTEGER    MXCLATTRS,MXCLYEARS
PARAMETER (MXCLATTRS=130)
PARAMETER (MXCLYEARS=4)
!
INTEGER CLHABINDX,INDXSPECIES,IXD100,IXDD0,IXDD5,IDEdd0,IDEdd5, &
           IDEmapdd5,IDEmtcm,IDEmtwm,IDEsdi,IXGSDD5,IXGSP,IXMAP, &
           IXMAT,IXMMIN,IXMTCM,IXMTWM,IXPSITE,JCLREF, &
           NATTRS,NESPECIES,NYEARS,YEARS
!
REAL    AESNTREES,AESTOCK,ATTRS,CLGROWMULT,CLMRTMLT1,CLMRTMLT2, &
           CLMXDENMULT,MXDENMLT,POTESTAB,SPCALIB,SPGMULT,SPMORT1, &
           SPMORT2,SPSITGM,SPVIAB
!
COMMON /CLIMCHR/ ATTR_LABELS(MXCLATTRS),CLIMATE_NAME
!
COMMON /CLIMATE/ AESNTREES,AESTOCK,ATTRS(MXCLYEARS,MXCLATTRS), &
          CLGROWMULT(MAXSP),CLHABINDX,CLMRTMLT1(MAXSP), &
          CLMRTMLT2(MAXSP),CLMXDENMULT,IDEdd0,IDEdd5,IDEmapdd5, &
          IDEmtcm,IDEmtwm,IDEsdi,INDXSPECIES(MAXSP),IXD100,IXDD0, &
          IXDD5,IXGSDD5,IXGSP,IXMAP,IXMAT,IXMMIN,IXMTCM,IXMTWM, &
          IXPSITE,JCLREF,LAESTB,LCLIMATE,MXDENMLT,NATTRS,NESPECIES, &
          NYEARS,POTESTAB(MAXSP),SPCALIB(MAXSP),SPGMULT(MAXSP), &
          SPMORT1(MAXSP),SPMORT2(MAXSP),SPSITGM(MAXSP), &
          SPVIAB(MAXSP),YEARS(MXCLYEARS)
!
!----------
!  DEFINITIONS OF VARIABLES IN 'CLIMATE' COMMON BLOCK:
!----------
!    AESNTREES    = NUMBER OF TREES TO ADD TO IF CLIMATE ESTAB IS ON
!    AESTOCK      = PERCENT OF MAX STOCKING THE TRIGGERS ESTAB
!    ATTRS        = THE ATTRIBUES, ROWS=YEARS, COLS=ATTR_LABELS
!    ATTR_LABELS  = THE COLUMN NAMES FROM THE INPUT DATA
!    CLGROWMULT   = GROWTH ADJUSTMENT WEIGHTING FACTOR (0-1).
!    CLHABINDX    = INDEX TO HABITAT ZONE FOR DEFAULT MEAN ANNUAL TEMP
!    CLMRTMLT1    = SPECIES-SPECIFIC MORTALITY MULTIPLIERS
!    CLMRTMLT2    =
!    CLMXDENMULT  = MODIFIES THE MAGNITUDE OF THE MAX DENSITY EFFECTS.
!    CLIMATE_NAME = THE GSM/SCENARIO NAME
!    IDEmtwm,IDEmtcm,IDEdd5,IDEsdi,IDEdd0,IDEmapdd5=POINTS TO THE COLUMN
!                   WHERE THE CHANGE OVER 300 M RANGE IN THESE VARIABLES
!                   IS STORED.
!    INDXSPECIES  = FVS TO ATTRIBUES SPECIES POINTER
!    IXD100       = POINTS TO THE COLUMN WHERE d100 IS STORED
!    IXDD0        = POINTS TO THE COLUMN WHERE dd0 IS STORED
!    IXDD5        = POINTS TO THE COLUMN WHERE dd5 IS STORED
!    IXGSDD5      = POINTS TO THE COLUMN WHERE gsdd5 IS STORED
!    IXGSP        = POINTS TO THE COLUMN WHERE gsp IS STORED
!    IXMAP        = POINTS TO THE COLUMN WHERE map IS STORED
!    IXMAT        = POINTS TO THE COLUMN WHERE mat IS STORED
!    IXMMIN       = POINTS TO THE COLUMN WHERE mmin IS STORED
!    IXMTCM       = POINTS TO THE COLUMN WHERE mtcm IS STORED
!    IXMTWM       = POINTS TO THE COLUMN WHERE mtwm IS STORED
!    IXPSITE      = POINTS TO THE COLUMN WHERE site adjustment IS STORED
!    IDEV         = POINTS TO THE COLUMN WHERE THE CHANGE IN ELEVATION IS STORED
!    JCLREF       = THE REPORT OUTPUT ID FOR THE REPORT SPOOLER, IF -1
!                   THE REPORT WILL NOT BE OUTPUT, IF 0, IT NEEDS TO BE
!                   ASSIGNED.
!    LAESTB       = TRUE IF CLIMATE-ESTAB IS ON
!    LCLIMATE     = TRUE IF EXTENSION IS BEING USED.
!    MXCLATTRS    = MAX NUMBER OF ATTRIBUES (COLUMNS OF ATTR)
!    MXCLYEARS    = MAX NUMBER OF YEARS (ROWS OF ATTR)
!    MXDENMLT     = MAXIMUM DENSITY ADJUSTMENT MULTIPLIER, USED IN OUTPUT.
!    NATTRS       = THE NUMBER OF COLUMNS FOUND IN THE INPUT DATA
!    NESPECIES    = MAXIMUM NUMBER OF SPECEIES TO CONSIDER IN ESTABLISHMENT
!    NYEARS       = THE NUMBER OF YEARS FOUND IN THE INPUT DATA
!    POTESTAB     = THE POTENTIAL NUMBER OF TREES/ACRE TO ESTABLISH
!    SPCALIB      = SPECIES CALIBRATION, SEE CLMORTS.
!    SPGMULT      = AVERAGE SPECIES SPECIFIC GROWTH MODIFIER
!    SPMORT1      = SPECIES SPECIFIC MORTALITY RATE BASED ON VIABILITY SCORES,
!                   USED FOR REPORTING ONLY.
!    SPMORT2      = SPECIES SPECIFIC MORTALITY RATE BASED ON CLIMATE CHANGE,
!                   USED FOR REPORTING ONLY (SPECIES AVERAGE OVER TREES).
!    SPSITGM      = GROWTH MULTIPLIER RELATED TO CHANGING SITE INDEX.
!    SPVIAB       = SPECIES VIABILITY SCORES AT THE CURRENT YEAR
!    YEARS        = THE YEARS IN THE ATTRIBUTES FILE
!
!-----END SEGMENT
