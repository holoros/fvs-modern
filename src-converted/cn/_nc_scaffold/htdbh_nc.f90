SUBROUTINE HTDBH (IFOR,ISPC,D,H,MODE)
IMPLICIT NONE
!----------
! NC $Id$
!----------
!  THIS SUBROUTINE CONTAINS THE DEFAULT HEIGHT-DIAMETER RELATIONSHIPS
!  FROM THE INVENTORY DATA.  IT IS CALLED FROM CRATET TO DUB MISSING
!  HEIGHTS, AND FROM REGENT TO ESTIMATE DIAMETERS (PROVIDED IN BOTH
!  CASES THAT LHTDRG IS SET TO .TRUE.).
!
!  DEFINITION OF VARIABLES:
!         D = DIAMETER AT BREAST HEIGHT
!         H = TOTAL TREE HEIGHT (STUMP TO TIP)
!      IFOR = FOREST CODE
!             1 IS KLAMATH (505)
!             2 IS SIX RIVERS (510)
!             3 IS TRINITY (514)
!             4 IS SISKIYOU (611)
!             5 IS HOOPA INDIAN RESERVATION (705)
!             6 IS SIMPSON TIMBER (800)
!             7 IS BLM COOS BAY ADMIN UNIT (712)
!      MODE = MODE OF OPERATING THIS SUBROUTINE
!             0 IF DIAMETER IS PROVIDED AND HEIGHT IS DESIRED
!             1 IF HEIGHT IS PROVIDED AND DIAMETER IS DESIRED
!----------
!
REAL SISKIY(12,3),D,H,P2,P3,P4,HAT3
INTEGER MODE,ISPC,IFOR
INTEGER IDANUW
!
! SPECIES ORDER IN NC VARIANT:
!    1 =          OTHER CONIFERS            USE DOUGLAS-FIR
!    2 = PILA     SUGAR PINE                PINUS LAMBERTIANA
!    3 = PSME     DOUGLAS-FIR               PSEUDOTSUGA MENZIESII
!    4 = ABCO     WHITE FIR                 ABIES CONCOLOR
!    5 = ARME     MADRONE                   ARBUTUS MENZIESII
!    6 = LIDE     INCENSE CEDAR             LIBOCEDRUS DECURRENS
!    7 = QUKE     BLACK OAK                 QUERQUS KELOGGII
!    8 = LIDE3    TANOAK                    LITHOCARPUS DENSIFLORUS
!    9 = ABMA     RED FIR                   ABIES MAGNIFICA
!   10 = PIPO     PONDEROSA PINE            PINUS PONDEROSA
!   11 =          OTHER HARDWOODS           USE TANOAK
!   12 = SESE3    COAST REDWOOD             SEQUIOA SEMPERVIRENS
!----------
!
!  SISKIYOU
!
DATA SISKIY / &
     523.0987,  819.8690,  523.0987,  604.8450,  160.6821, &
    1530.3300,   48.6795,  679.1972,  202.8860, 1348.0419, &
     679.1972,  595.1068, &
!
       5.7243,    6.4531,    5.7243,    5.9835,    4.1677, &
       7.0811,    8.9420,    5.5698,    8.7469,    7.0463, &
       5.5698,    5.8103, &
!
      -0.4109,   -0.3434,   -0.4109,   -0.3789,   -0.4954, &
      -0.2544,   -1.4832,   -0.3074,   -0.8317,   -0.3076, &
      -0.3074,   -0.3821 /
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
IDANUW = IFOR
!
!----------
!  SET EQUATION PARAMETERS ACCORDING TO FOREST AND SPECIES.
!  SISKIYOU EQNS ARE USED FOR ALL FOREST CODES.
!----------
P2 = SISKIY(ISPC,1)
P3 = SISKIY(ISPC,2)
P4 = SISKIY(ISPC,3)
IF(MODE .EQ. 0) H=0.
IF(MODE .EQ. 1) D=0.
!----------
!  PROCESS ACCORDING TO MODE
!----------
IF(MODE .EQ. 0) THEN
  IF(D .GE. 3.) THEN
    H = 4.5 + P2 * EXP(-1.*P3*D**P4)
  ELSE
    H = ((4.5+P2*EXP(-1.*P3*(3.**P4))-4.51)*(D-0.3)/2.7)+4.51
  ENDIF
ELSE
  HAT3 = 4.5 + P2 * EXP(-1.*P3*3.0**P4)
  IF(H .GE. HAT3) THEN
    D = EXP( ALOG((ALOG(H-4.5)-ALOG(P2))/(-1.*P3)) * 1./P4)
  ELSE
    D = (((H-4.51)*2.7)/(4.5+P2*EXP(-1.*P3*(3.**P4))-4.51))+0.3
  ENDIF
ENDIF
!
RETURN
END
