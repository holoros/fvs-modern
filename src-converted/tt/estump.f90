SUBROUTINE ESTUMP (JSSP,DBH,PREM,JPLOT,ISHAG)
IMPLICIT NONE
!----------
! TT $Id$
!----------
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'ESPARM.f90'
!
!
INCLUDE 'ESHOOT.f90'
!
!
INCLUDE 'ESHAP.f90'
!
!
INCLUDE 'ESCOMN.f90'
!
!
!OMMONS
!----------
!     STORE INFORMATION FOR STUMP SPROUT SUBROUTINE.
!----------
INTEGER*4 MDBH,IPLT,MSP
INTEGER ISHAG,JPLOT,JSSP,I,ISSP,IDBH
REAL PREM,DBH,DBHEND
!----------
! SPECIES ORDER FOR TETONS VARIANT:
!
!  1=WB,  2=LM,  3=DF,  4=PM,  5=BS,  6=AS,  7=LP,  8=ES,  9=AF, 10=PP,
! 11=UJ, 12=RM, 13=BI, 14=MM, 15=NC, 16=MC, 17=OS, 18=OH
!
! VARIANT EXPANSION:
! BS USES ES EQUATIONS FROM TT
! PM USES PI (COMMON PINYON) EQUATIONS FROM UT
! PP USES PP EQUATIONS FROM CI
! UJ AND RM USE WJ (WESTERN JUNIPER) EQUATIONS FROM UT
! BI USES BM (BIGLEAF MAPLE) EQUATIONS FROM SO
! MM USES MM EQUATIONS FROM IE
! NC AND OH USE NC (NARROWLEAF COTTONWOOD) EQUATIONS FROM CR
! MC USES MC (CURL-LEAF MTN-MAHOGANY) EQUATIONS FROM SO
! OS USES OT (OTHER SP.) EQUATIONS FROM TT
!----------
DATA MDBH/10000000/,MSP/10000/
!
DO 10 I=1,NSPSPE
IF(JSSP.EQ.ISPSPE(I)) GO TO 11
10 CONTINUE
GO TO 900
11 CONTINUE
ISSP=I
!
!     DETERMINE DIAMETER CLASS OF THE TREE BEING CUT.
!
DO 100 I=1,NDBHCL-1
DBHEND=(DBHMID(I)+DBHMID(I+1))/2.0
IF(DBH.GT.DBHEND) GO TO 100
IDBH=I
GO TO 110
100 CONTINUE
IDBH=NDBHCL
110 CONTINUE
IPLT=JPLOT
IF(IPLT.GT.9999) IPLT=9999
ITRNRM=ITRNRM+1
!----------
! ISHOOT() IS A NUMBER COMPOSED OF DDDSSSPPPP, WHERE DDD = DBH CLASS,
! SSS = SPECIES, AND PPPP = PLOT.  DECODED IN **ESUCKR**.
! PRBREM() CONTAINS AMOUNT OF PROB REMOVED IN CUTTING TREE RECORD.
!----------
DSTUMP(ITRNRM) = DBH
ISHOOT(ITRNRM) = IDBH*MDBH + ISSP*MSP + IPLT
PRBREM(ITRNRM) = PREM
JSHAGE(ITRNRM) = ISHAG
IF(JSSP.EQ.6)THEN
  ASTPAR = ASTPAR + PREM
  ASBAR = ASBAR + 0.0054542*PREM*DBH**2.
ENDIF
900 CONTINUE
RETURN
END
