SUBROUTINE CCFCAL(ISPC,D,H,JCR,P,LTHIN,CCFT,CRWDTH,MODE)
IMPLICIT NONE
!----------
! BM $Id$
!----------
!  THIS ROUTINE COMPUTES CROWN WIDTH AND CCF FOR INDIVIDUAL TREES.
!  CALLED FROM DENSE, PRTRLS, SSTAGE, AND CVCW.
!
!  ARGUMENT DEFINITIONS:
!    ISPC = NUMERIC SPECIES CODE
!       D = DIAMETER AT BREAST HEIGHT
!       H = TOTAL TREE HEIGHT
!     JCR = CROWN RATIO IN PERCENT (0-100)
!       P = TREES PER ACRE
!   LTHIN = .TRUE. IF THINNING HAS JUST OCCURRED
!         = .FALSE. OTHERWISE
!    CCFT = CCF REPRESENTED BY THIS TREE
!  CRWDTH = CROWN WIDTH OF THIS TREE
!    MODE = 1 IF ONLY NEED CCF RETURNED
!           2 IF ONLY NEED CRWDTH RETURNED
!             NOT USED ANY MORE; CROWN WIDTHS COMPUTED WITH SUBROUTINES
!             CWIDTH AND CWCALC
!----------
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
!OMMONS
!
!----------
!  VARIABLE DEFINITIONS:
!
!     CCF COEFFICIENTS FOR TREES THAT ARE GREATER THAN 10.0 IN. DBH:
!      RD1 -- CONSTANT TERM IN CROWN COMPETITION FACTOR EQUATION,
!             SUBSCRIPTED BY SPECIES
!      RD2 -- COEFFICIENT FOR SUM OF DIAMETERS TERM IN CROWN
!             COMPETITION FACTOR EQUATION,SUBSCRIPTED BY SPECIES
!      RD3 -- COEFFICIENT FOR SUM OF DIAMETER SQUARED TERM IN
!             CROWN COMPETITION EQUATION, SUBSCRIPTED BY SPECIES
!
!     CCF COEFFICIENTS FOR TREES THAT ARE LESS THAN 10.0 IN. DBH:
!      RDA -- MULTIPLIER.
!      RDB -- EXPONENT.  CCF(I) = RDA*DBH**RDB
!
!  CCF EQUATIONS DERIVED FROM PAINE AND HANN ORE STATE UNIV RP46
!
!----------
!  SPECIES ORDER:
!   1=WP,  2=WL,  3=DF,  4=GF,  5=MH,  6=WJ,  7=LP,  8=ES,
!   9=AF, 10=PP, 11=WB, 12=LM, 13=PY, 14=YC, 15=AS, 16=CW,
!  17=OS, 18=OH
!
!  SPECIES EXPANSION:
!  WJ USES SO JU (ORIGINALLY FROM UT VARIANT; REALLY PP FROM CR VARIANT)
!  WB USES SO WB (ORIGINALLY FROM TT VARIANT)
!  LM USES UT LM
!  PY USES SO PY (ORIGINALLY FROM WC VARIANT)
!  YC USES WC YC
!  AS USES SO AS (ORIGINALLY FROM UT VARIANT)
!  CW USES SO CW (ORIGINALLY FROM WC VARIANT)
!  OS USES BM PP BARK COEFFICIENT
!  OH USES SO OH (ORIGINALLY FROM WC VARIANT)
!----------
!
!  SOURCES OF COEFFICIENTS:
!     1 = PAINE AND HANN TABLE 2: WESTERN WHITE PINE
!     2 = PAINE AND HANN TABLE 2: SUGAR PINE
!     3 = PAINE AND HANN TABLE 2: DOUGLAS-FIR
!     4 = PAINE AND HANN TABLE 2: WHITE/GRAND FIR
!     5 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: PONDEROSA PINE
!     6 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!     7 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!     8 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: ENGELMANN SPRUCE
!     9 = PAINE AND HANN TABLE 2: RED FIR
!    10 = PAINE AND HANN TABLE 2: PONDEROSA PINE
!    11 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    12 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    13 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    14 = PAINE AND HANN TABLE 2: INCENSE CEDAR
!    15 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN RED CEDAR
!    16 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    17 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    18 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!
!      PAINE AND HANN, 1982. MAXIMUM CROWN WIDTH EQUATIONS FOR
!        SOUTHWESTERN OREGON TREE SPECIES. RES PAP 46, FOR RES LAB
!        SCH FOR, OSU, CORVALLIS. 20PP.
!
!      WYKOFF, CROOKSTON, STAGE, 1982. USER'S GUIDE TO THE STAND
!        PROGNOSIS MODEL. GEN TECH REP INT-133. OGDEN, UT:
!        INTERMOUNTAIN FOREST AND RANGE EXP STN. 112P.
!----------
!  VARIABLE DECLARATIONS:
!----------
!
LOGICAL LDANUW,LTHIN
!
INTEGER IDANUW,ISPC,JCR,MODE
!
REAL CCFT,CRWDTH,D,H,P,RDANUW
REAL RD1(MAXSP),RD2(MAXSP),RD3(MAXSP),RDA(MAXSP),RDB(MAXSP)
!
!----------
!  DATA STATEMENTS:
!----------
DATA RD1/ &
     .0186,  .0392,  .0388,  .0690,    .03, &
    .01925, .01925,    .03,  .0172,  .0219, &
    .01925, .01925,  .0204,  .0194,    .03, &
     .0204,  .0219,  .0204/
!
DATA RD2/ &
     .0146,  .0180,  .0269,  .0225,   .018, &
    .01676, .01676,  .0173, .00877,  .0169, &
    .01676, .01676,  .0246,  .0142,  .0238, &
     .0246,  .0169,  .0246/
!
DATA RD3/ &
    .00288, .00207, .00466, .00183, .00281, &
    .00365, .00365, .00259, .00112, .00325, &
    .00365, .00365,  .0074, .00261, .00490, &
     .0074, .00325,  .0074/
!
DATA RDA/ &
    0.009884, 0.007244, 0.017299, 0.015248, 0.011109, &
    0.009187, 0.009187, 0.007875, 0.011402, 0.007813, &
    0.009187, 0.009187,      0.0,      0.0, 0.008915, &
         0.0, 0.007813,      0.0/
!
DATA RDB/ &
      1.6667,  1.8182,  1.5571,  1.7333,  1.7250, &
      1.7600,  1.7600,  1.7360,  1.7560,  1.7780, &
      1.7600,  1.7600,     0.0,     0.0,  1.7800, &
         0.0,  1.7780,     0.0/
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
RDANUW = H
IDANUW = JCR
LDANUW = LTHIN
!----------
!  INITIALIZE RETURN VARIABLES.
!----------
CCFT = 0.
CRWDTH = 0.
!----------
!  COMPUTE CCF
!----------
IF(MODE.EQ.1 .OR. ISPC.EQ.6 .OR. ISPC.EQ.12 .OR. ISPC.EQ.15) THEN
!
  SELECT CASE (ISPC)
!----------
!  ORIGINAL BM VARIANT SPECIES
!  UT VARIANT SPECIES
!  TT VARIANT SPECIES
!----------
  CASE(1:12,15,17)
    IF (D .GE. 1.0) THEN
      CCFT = RD1(ISPC) + D*RD2(ISPC) + D*D*RD3(ISPC)
    ELSE IF(D.GT.0.1) THEN
      CCFT = RDA(ISPC) * (D**RDB(ISPC))
    ELSE
      CCFT=0.001
    ENDIF
    CCFT = P * CCFT
!----------
!  WC VARIANT SPECIES
!----------
  CASE(13,14,16,18)
    IF (D .LT. 1.0) THEN
      CCFT = D * (RD1(ISPC)+RD2(ISPC)+RD3(ISPC))
    ELSE
      CCFT = RD1(ISPC) + RD2(ISPC)*D + RD3(ISPC)*D**2.0
    ENDIF
    CCFT = CCFT * P
  END SELECT
ENDIF
!----------
!  COMPUTE CROWN WIDTH. (NOT USED ANY MORE; REPLACED BY CWCALC)
!----------
IF(MODE.EQ.2) THEN
  CRWDTH = 0.
!
!        SELECT CASE (ISPC)
!        CASE(1:5,7:11,13:15,17:18)
!          CALL R6CRWD (ISPC,D,H,CRWDTH)
!        CASE(6,12,16)
!          CRWDTH = SQRT(CCFT/0.001803)
!        END SELECT
!        IF(CRWDTH .GT. 99.9) CRWDTH=99.9
!
ENDIF
!
RETURN
END
