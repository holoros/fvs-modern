!
DATA SPLINE / &
    3., 6., 3., 3., 3., 3., 3., 3., 3., 3., &
    2., 5., 3., 3., 5., 5., 3., 2., 3., 2., &
    3., 3., 3., 3., 3., 3., 3., 3., 3., 3., &
    3., 3., 3., 3., 3., 3., 3., 3., 3., 3., &
    3., 3., 3., 3., 3., 3., 3., 3., 3., 3./
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
IDANUW = IFOR
!----------
!  SET EQUATION PARAMETERS ACCORDING TO SPECIES.
!----------
P2 = CURARN(ISPC,1)
P3 = CURARN(ISPC,2)
P4 = CURARN(ISPC,3)
Z = SPLINE(ISPC)
IF(MODE .EQ. 0) H=0.
IF(MODE .EQ. 1) D=0.
!----------
!  PROCESS ACCORDING TO MODE
!----------
IF(MODE .EQ. 0) THEN
  IF(D .GE. Z) THEN
    H = 4.5 + P2 * EXP(-1.*P3*D**P4)
  ELSE
    H = ((4.5+P2*EXP(-1.*P3*(Z**P4))-4.51)*(D-0.3)/(Z-0.3))+4.51
  ENDIF
ELSE
  HATZ = 4.5 + P2 * EXP(-1.*P3*Z**P4)
  IF(H .GE. HATZ) THEN
    D = EXP( ALOG((ALOG(H-4.5)-ALOG(P2))/(-1.*P3)) * 1./P4)
  ELSE
    D=(((H-4.51)*(Z-0.3))/(4.5+P2*EXP(-1.*P3*(Z**P4))-4.51))+0.3
  ENDIF
ENDIF
!
RETURN
END
