SUBROUTINE RCDSET (IC,LRETRN)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     SETS THE RETURN CODE FOR THE PROGNOSIS MODEL.
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
!OMMONS
!
INTEGER IC
LOGICAL LRETRN
!
IF (IC.GT.ICCODE) ICCODE=IC
IF (.NOT.LRETRN) CALL fvsSetRtnCode (1)
RETURN
END
