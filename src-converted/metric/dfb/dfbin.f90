SUBROUTINE DFBIN (PASKEY,ARRAY,LNOTBK,LKECHO)
!----------
! METRIC-DFB $Id$
!----------
!
!  KEYWORD PROCESSOR FOR DOUGLAS-FIR BEETLE MODEL.
!
!  CALLED BY :
!     INITRE  [PROGNOSIS]
!
!  CALLS :
!     ERRGRO  (SUBROUTINE)   [PROGNOSIS]
!     FNDKEY  (SUBROUTINE)   [PROGNOSIS]
!     KEYRDR  (SUBROUTINE)   [PROGNOSIS]
!     DFBNSD  (ENTRY POINT)  [DFB (DFBRAN)]
!     OPNEW   (SUBROUTINE)   [PROGNOSIS]
!     TXNOTE
!
!  ENTRY POINTS :
!     DFBKEY
!
!  PARAMETERS :
!     ARRAY  - ARRAY THAT HOLDS THE FIELDS READ IN WITH THE KEYWORD.
!     LNOTBK - LOGICAL ARRAY THAT HOLDS THE 'NOT BLANK' FLAGS FOR THE
!              FIELDS READ IN WITH THE KEYWORD.
!     PASKEY - THE KEYWORD READ IN BEFORE CALL TO THIS SUBROUTINE.
!
!  LOCAL VARIABLES :
!     I      - COUNTER INDEX.
!     IDT    - DATE THAT REGIONAL OUTBREAK IS SCHEDULED.
!     ISIZE  - THE SIZE OF THE KEYWORD TABLE.
!     KARD   - ARRAY THAT HOLDS THE CHARACTER REPRESENTATION OF THE
!              FIELDS READ IN WITH THE KEYWORD.
!     KEYWRD - KEYWORD READ IN.
!     KODE   - ERROR KODE RETURNED FROM KEYRDR, FNDKEY, AND OPNEW.
!     NUMBER - HOLDS THE POSITION OF THE KEYWORD IN THE TABLE.
!     PNAME  - HOLDS THE NAME OF THE POST PROCESSOR OUTPUT FILE.
!     PRMS   - HOLDS PARAMETERS TO BE SENT TO THE EVENT MONITOR.
!     TABLE  - ARRAY THAT HOLDS THE TABLE OF DFB KEYWORDS.
!
!  COMMON BLOCK VARIABLES USED :
!     DBEVNT - (DFBCOM)  OUTPUT
!     DEBUIN - (DFBCOM)  OUTPUT
!     EPIPRB - (DFBCOM)  OUTPUT
!     EXPCTD - (DFBCOM)  OUTPUT
!     EXSTDV - (DFBCOM)  OUTPUT
!     IDBSCH - (DFBCOM)  OUTPUT
!     IDFBYR - (DFBCOM)  OUTPUT
!     ILENTH - (DFBCOM)  OUTPUT
!     IPAST  - (DFBCOM)  OUTPUT
!     IREAD  - (CONTRL)  INPUT
!     IRECNT - (CONTRL)  OUTPUT
!     ISMETH - (DFBCOM)  OUTPUT
!     IWAIT  - (DFBCOM)  OUTPUT
!     IYOUT  - (DFBCOM)  OUTPUT
!     JODFB  - (DFBCOM)  OUTPUT
!     JODFBX - (DFBCOM)  OUTPUT
!     JOSTND - (CONTRL)  INPUT
!     LBAMOD - (DFBCOM)  OUTPUT
!     LDFBON - (DFBCOM)  OUTPUT
!     LEPI   - (DFBCOM)  OUTPUT
!     LINPRG - (DFBCOM)  OUTPUT
!     LINV   - (DFBCOM)  OUTPUT
!     MINDEN - (DFBCOM)  OUTPUT
!     ORSEED - (DFBCOM)  OUTPUT
!     PREKLL - (DFBCOM)  OUTPUT
!     PRPWIN - (DFBCOM)  OUTPUT
!
! Revision History:
!   26-SEP-2002 Lance R. David (FHTET)
!     Date of previous revision was 02/28/94.
!     Corrected error in WINDTHR keyword processing. The LNOTBK, PRMS and ARRAYS
!     index numbers were misaligned for fields 2 and 3.
!   10-NOV-2003 - Lance R. David (FHTET)
!     Added LFLAG to KEYRDR call statement argument list.
!
!---------------------------------
!
!OMMONS
!
INCLUDE 'PRGPRM.f90'
INCLUDE 'CONTRL.f90'
INCLUDE 'DFBCOM.f90'
INCLUDE 'METRIC.f90'
!
!OMMONS
!

INTEGER IDT, KODE, ISIZE, I, NUMBER

CHARACTER*8 PASKEY, TABLE(20), KEYWRD
CHARACTER*10 KARD(7)
CHARACTER*80 PNAME

LOGICAL LNOTBK(7),LKECHO

REAL ARRAY(7), PRMS(5)

DATA ISIZE /20/

DATA TABLE / &
        'END','CUROUTBK','DEBUG','DFBECHO','MANSTART','RANSTART', &
        'RANNSEED','RANSCHED','MANSCHED','WINDTHR','STOPROB', &
        'NODEBUG','MORTDIS',' ','OLENGTH','EXYRMORT',4*' ' /

KEYWRD = PASKEY
!
!     Set flag for writing warnig note RE: use of ??X.exe models
!
CALL TXNOTE (LXNOTE)
!
!
10 CONTINUE
!
!     GET THE NEXT KEYWORD.
!
CALL KEYRDR (IREAD,JOSTND,DEBUIN,KEYWRD,LNOTBK, &
                ARRAY,IRECNT,KODE,KARD,LFLAG,LKECHO)

!
!     RETURN KODES: 0=NO ERROR, 1=COLUMN 1 BLANK OR ANOTHER ERROR,
!                   2=EOF, LESS THAN ZERO...USE OF PARMS STATEMENT IS
!                   PRESENT.
!
IF (KODE .NE. 0) THEN
   IF (KODE .EQ. 2) CALL ERRGRO(.FALSE.,2)
   CALL ERRGRO (.TRUE.,6)
   GOTO 10
ENDIF

!
!     FIND THE KEYWORD IN THE KEYWORD TABLE.
!
CALL FNDKEY (NUMBER,KEYWRD,TABLE,ISIZE,KODE,DEBUIN,JOSTND)
!
!     RETURN KODES 0=NO ERROR,1=KEYWORD NOT FOUND,2=MISSPELLING.
!
IF (KODE .NE. 0) THEN
   IF (KODE .EQ. 1) CALL ERRGRO (.FALSE.,1)
   CALL ERRGRO (.TRUE.,5)
ENDIF

!
!     SET FLAG. "DFB MODEL IS BEING USED"
!
LDFBON  = .TRUE.

!
!     PROCESS OPTIONS
!
GOTO  (1000,1200,1300,1400,1500,1600,1700,1800,1900,2000 &
         ,2100,2200,2300,2400,2500,2600),  NUMBER

1000 CONTINUE
!
!  ====================  OPTION NUMBER 1  -- END  ===================
!
IF(LKECHO)WRITE(JOSTND,1010) KEYWRD
1010 FORMAT (/1X,A8,'   END OF DOUGLAS-FIR BEETLE OPTIONS.')

RETURN

1200 CONTINUE
!
!  ====================  OPTION NUMBER 2  -- CUROUTBK  ==============
!
IF (LNOTBK(1)) THEN
!
!        OUTBREAK IN PROGRESS.
!
   IYOUT  = IFIX(ARRAY(1))
   LINPRG = .TRUE.

   IF (LNOTBK(2)) THEN
!
!           USER ENTERED THE MORTALITY FROM DFB.
!
      PREKLL = ARRAY(2)
      IF(LKECHO)WRITE(JOSTND,1210) KEYWRD, IYOUT,IFIX(PREKLL)
1210       FORMAT (/1X,A8,'   YEAR IN OUTBREAK IS ',I2,'.',2X,I3, &
                ' TREES/HA HAVE BEEN KILLED.')
      PREKLL = ARRAY(2)/HAtoACR ! now /ac
   ELSE
!
!           THE MORTALITY FROM DFB WILL COME FROM THE TREE LIST.
!
      LINV = .TRUE.
      IF(LKECHO)WRITE(JOSTND,1220) KEYWRD, IYOUT
1220       FORMAT (/1X,A8,'   YEAR IN OUTBREAK IS ',I2,'.  NUMBER ', &
                 'OF TREES/HA KILLED IS READ FROM TREE LIST.')
   ENDIF

ENDIF

GOTO 10

1300 CONTINUE
!
!  ====================  OPTION NUMBER 3  -- DEBUG  =================
!
IF(LKECHO)WRITE(JOSTND,1310) KEYWRD
1310 FORMAT (/1X,A8,'   DOUGLAS-FIR BEETLE DEBUG ACTIVATED.')
JODFB = JOSTND
DEBUIN = .TRUE.
GOTO 10

1400 CONTINUE
!
!  ====================  OPTION NUMBER 4 -- DFBECHO  ===============
!
!     SET JODFBX TO THE UNIT NUMBER FOR THE POST PROCESSOR OUTPUT.
!
JODFBX = 21
!
!     READ IN THE FILE NAME FROM THE SUPPLEMENTAL RECORD.
!
READ (IREAD,1410) PNAME
1410 FORMAT (A)
IF (PNAME .EQ. ' ') PNAME = 'DFBOUT'
!
!     TRY TO OPEN THE FILE.
!
CALL MYOPEN (JODFBX, PNAME, 1, 133, 0, 1, 1, 0, KODE)
!
!     PRINT OUT KEYWORD MESSAGE BASED ON IF THE FILE OPENS OR NOT.
!
IF (KODE .EQ. 1) THEN
   WRITE(JOSTND,1420) KEYWRD, PNAME
1420    FORMAT (/1X,A8,'   POST PROCESSOR OUTPUT FILE ',A80,/, &
              12X,'WAS NOT OPENED DUE TO AN ERROR!!!!')
   JODFBX = 0
ELSE
   IF(LKECHO)WRITE(JOSTND,1430) KEYWRD, PNAME
1430    FORMAT (/1X,A8,'   POST PROCESSOR OUTPUT FILE ',A80,/, &
              12X,'WAS OPENED.')
ENDIF
GOTO 10

1500 CONTINUE
!
!  ====================  OPTION NUMBER 5  -- MANSTART  ==============
!
ISMETH = 1
IF(LKECHO)WRITE(JOSTND,1510) KEYWRD
1510 FORMAT (/1X,A8,'   STAND IS INCLUDED IN ALL REGIONAL OUTBREAKS.')
GOTO 10

1600 CONTINUE
!
!  ====================  OPTION NUMBER 6  -- RANSTART  ==============
!
ISMETH = 2
IF(LKECHO)WRITE(JOSTND,1610) KEYWRD
1610 FORMAT (/1X,A8,'   STAND INCLUSION IN REGIONAL OUTBREAKS IS ', &
      'STOCHASTICALLY DETERMINED.')
GOTO 10

1700 CONTINUE
!
!  ====================  OPTION NUMBER 7 -- RANNSEED  ===============
!

ORSEED = ARRAY(1)
CALL DFBNSD (LNOTBK(1), ORSEED)
IF(LKECHO)WRITE(JOSTND,1710) KEYWRD, ORSEED
1710 FORMAT (/1X,A8,'   RANDOM SEED IS:',F10.0)
GOTO 10

1800 CONTINUE
!
!  ====================  OPTION NUMBER 8 -- RANSCHED  ===============
!
IDBSCH = 2
IF (LNOTBK(1)) IWAIT  = IFIX(ARRAY(1))
IF (LNOTBK(2)) DBEVNT = ARRAY(2)
IF (LNOTBK(3)) IPAST  = IFIX(ARRAY(3))

IF(LKECHO)WRITE(JOSTND,1810) KEYWRD, IWAIT, DBEVNT, IPAST
1810 FORMAT (/1X,A8,'   REGIONAL OUTBREAKS AUTOMATICALLY ', &
      'SCHEDULED.',/T13,'MINIMUM WAITING PERIOD IS ',I5, &
      ' YEARS; EVENT PROBABILITY IS ',F6.3,/T13,'LAST RECORDED ', &
      'DOUGLAS-FIR BEETLE OUTBREAK WAS IN YEAR: ',I5)
GOTO 10

1900 CONTINUE
!
!  ====================  OPTION NUMBER 9 -- MANSCHED  ===============
!
IDT = 1
IF (LNOTBK(1)) IDT = IFIX(ARRAY(1))
IDBSCH = 1

!
!     CALL OPTION PROCESSOR AND SCHEDULE REGIONAL OUTBREAK.
!
CALL OPNEW (KODE,IDT,2209,0,PRMS)
IF (KODE .GT. 0) GOTO 10

IF(LKECHO)WRITE(JOSTND,1910) KEYWRD, IDT
1910 FORMAT (/1X,A8,'   DATE/CYCLE= ',I5,'; REGIONAL OUTBREAKS ', &
      'SCHEDULED BY USER.')

!
!     KEEP TRACK OF OUTBREAKS SCHEDULED BY DATE.
!
IF (IDT .GT. 40) THEN
   IDFBYR(41) = IDFBYR(41) + 1
   I = IDFBYR(41)
   IDFBYR(I) = IDT
ENDIF

GOTO 10

2000 CONTINUE
!
!  ====================  OPTION NUMBER 10 -- WINDTHR  ===============
!

IDT = 1
IF (LNOTBK(1)) IDT = INT(ARRAY(1))

PRMS(1) = PRPWIN
PRMS(2) = MINDEN
IF (LNOTBK(2)) PRMS(1) = ARRAY(2)
IF (LNOTBK(3)) PRMS(2) = ARRAY(3)
PRMS(2) = PRMS(2)/HAtoACR
!
!     CALL OPTION PROCESSOR AND SCHEDULE WINDTHROW.
!
CALL OPNEW (KODE,IDT,2210,2,PRMS)
IF (KODE .GT. 0) GOTO 10

IF(LKECHO)WRITE(JOSTND,2010) KEYWRD, IDT, PRMS(1), &
     PRMS(2)*HAtoACR
2010 FORMAT(/1X,A8,'   WINDTHROW EVENT TO BE ATTEMPTED IN DATE/CYCLE=', &
               I4,';   PROPORTION TO WINDTHROW =',F4.2, &
             /T13,'MINIMUM ELIGIBLE STEMS FOR EVENT =',F7.0)

GOTO 10

2100 CONTINUE
!
!  ====================  OPTION NUMBER 11 -- STOPROB  ===============
!
IF (ARRAY(1) .LT. 0.0 .OR. ARRAY(1) .GT. 1.0) GOTO 10

IF (LNOTBK(1)) THEN
!
!        USER SET THE PROBABILITY OF THIS STAND BEING INCLUDED IN A
!        REGIONAL OUTBREAK.
!
   LEPI = .TRUE.
   EPIPRB = ARRAY(1)
   IF(LKECHO)WRITE(JOSTND,2110) KEYWRD, EPIPRB
2110    FORMAT (/1X,A8,'   SPECIFIED OUTBREAK PROBABILITY = ',F11.3)
ELSE
!
!        MODEL WILL CALCULATE PROBABILITY OF THIS STAND BEING INCLUDED
!        IN A REGIONAL OUTBREAK.
!
   WRITE(JOSTND,2120) KEYWRD
2120    FORMAT (/1X,A8,'   KEYWORD IGNORED!!!  PROBABILITY WILL BE ' &
              'CALCULATED.')
ENDIF
GOTO 10

2200 CONTINUE
!
!  ====================  OPTION NUMBER 12 -- NODEBUG  ===============
!
IF(LKECHO)WRITE(JOSTND,2210) KEYWRD
2210 FORMAT (/1X,A8,'   TURN OFF DEBUG OUTPUT.')
DEBUIN = .FALSE.
GOTO 10

2300 CONTINUE
!
!  ====================  OPTION NUMBER 13 -- MORTDIS  ===============
!
LBAMOD = .TRUE.

IF(LKECHO)WRITE(JOSTND,2310) KEYWRD
2310 FORMAT (/1X,A8,'   THE BASAL AREA METHOD IS USED FOR THE ', &
           'MORTALITY DISTRIBUTION.')

GOTO 10

2400 CONTINUE
!
!  ====================  OPTION NUMBER 14 --          ===============
!
GOTO 10

2500 CONTINUE
!
!  ====================  OPTION NUMBER 15 -- OLENGTH  ===============
!
IF (ARRAY(1) .GE. 1.0 .AND. ARRAY(1) .LE. 10.0) &
      ILENTH = IFIX (ARRAY(1))
IF(LKECHO)WRITE(JOSTND,2510) KEYWRD, ILENTH
2510 FORMAT (/1X,A8,'   LENGTH OF DFB OUTBREAK  = ',I4)
GOTO 10

2600 CONTINUE
!
!  ====================  OPTION NUMBER 16 -- EXYRMORT  ==============
!
IF (LNOTBK(1)) EXPCTD = ARRAY(1)
IF (LNOTBK(2)) EXSTDV = ARRAY(2)

IF(LKECHO)WRITE(JOSTND,2610) KEYWRD, EXPCTD, EXSTDV
2610 FORMAT (/1X,A8,'   AVERAGE NUMBER OF DOUGLAS-FIR TREES EXPECTED ', &
          'TO BE KILLED BY THE DFB EACH YEAR OF AN OUTBREAK = ', &
          F5.1,/,9X,'   STANDARD DEVIATION USED IN RANDOM ', &
          'CALCULATION OF NUMBER THAT WILL BE KILLED = ',F5.1)
GOTO 10



ENTRY DFBKEY (KEY,PASKEY)
!
!     ENTRY POINT
!
!     PARAMETERS :
!        KEY    - POSITION OF KEYWORD IN TABLE (INPUT).
!        PASKEY - KEYWORD TO BE RETURNED (OUTPUT).
!
PASKEY=TABLE(KEY)

RETURN
END
