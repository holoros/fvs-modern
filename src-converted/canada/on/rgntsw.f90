      P=PROB(I)
      H1=WK3(I)
      BAL=BAJ*(100.0-PCT(I))*0.01
      CALL CCFCAL(ISPC,D,H1,ICR(I),P,.FALSE.,C1,CW,1)
      B1=BACON*D*D

