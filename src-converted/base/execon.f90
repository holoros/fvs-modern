!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  IDANUW = I1
  IDANUW = I2
  RDANUW = ARRAY(1)
  RDANUW = ARRAY1(1)
