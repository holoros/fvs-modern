IF(NPER.EQ.1) GOTO 12
DO I=1,ITRN
  D1=DBH(I)
  IF(D1.LT.3.0) GOTO 6
  IS=ISP(I)
  P=PROB(I)
  D2=D1+DG(I)/BKRAT(ISP(I))
  B1=BACON*D1*D1
  B2=BACON*D2*D2
  CALL CCFCAL(IS,D1,HT(I),ICR(I),P,.FALSE.,C1,CW,1)
  CALL CCFCAL(IS,D2,HT(I),ICR(I),P,.FALSE.,C2,CW,1)
  BI=(B2-B1)/10.0
  CI=(C2-C1)/10.0
  K=0
  DO J=2,NPER
    K=K+KPER(J-1)
    PN=P*0.99**K
    RDNEXT(J)=RDNEXT(J)+K*CI
    BANEXT(J)=BANEXT(J)+K*BI*PN
  ENDDO
6 ENDDO
GOTO 12

!     IF CALLED FROM **ESTAB**, DIAMETERS AND DENSITY STATISTICS HAVE
!     BEEN UPDATED AND DENSITIES CORRESPONDING TO BEGINNINGS OF SUBCYCLES
!     CAN BE INTERPOLATED FROM DIFFERENCE BETWEEN OLD AND NEW STATS.  THE
!     TREES ARE SENT TO REGENT WITH A HEIGHT CORRESPONDING TO 5 YEARS AFTER
!     THE START OF THE CYCLE.

9 CONTINUE
BAYR=0.0
CCFYR=0.0
IF(LSKIPH) GOTO 10
BAYR=(BA-ATBA)/ITOT
CCFYR=(RELDEN-ATCCF)/ITOT
10 CONTINUE
NYR=5
DO J=1,NPER
  RDNEXT(J)=ATCCF+NYR*CCFYR
  BANEXT(J)=ATBA+NYR*BAYR
  NYR=NYR+KPER(J)
ENDDO
12 CONTINUE

!     STORE INITIAL HEIGHTS IN WK3

DO I=1,ITRN
  WK3(I)=HT(I)
ENDDO

