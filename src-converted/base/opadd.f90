SUBROUTINE OPADD (IDT,IACTK,ISTUS,NPRMS,PRMS,KODE)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     OPTION PROCESSING ROUTINE - NL CROOKSTON - JUNE 1981 - MOSCOW
!     FROM 1991 TO 2003 THERE WERE NO CHANGES TO THIS ROUTINE!
!     IN MAY 2003 CROOKSTON ADDED PROVISIONS FOR CHARACTER ARGUMENTS
!
!     OPADD IS USED TO DYNAMICALLY ADD AN ACTIVITY TO THE ACTIVITY
!     SCHEDULE.  WHEN ISTUS=0, YOU MUST CALL OPINCR (SEE BELOW)
!     AFTER ONE OR MORE CALLS TO OPADD.  THE ARGUMENTS:
!
!     KODE  = 0, ALL WENT WELL
!             1, ACTIVITY COULD NOT BE ADDED BECAUSE OPTION ARRAYS
!                ARE FULL.  A WARNING MSG IS ISSUED VIA ERRGRO.
!             2, IDT IS .LE. 0, YOU MUST SPECIFY A DATE (IDT MUST
!                BE OVER 0).
!     IDT   = THE DATE.
!     ISTUS = THE ACTIVITY STATUS CODE, 0=NEEDS TO BE DONE,
!             >0 ACCOMPLISHED ON DATE ISTUS, <0, SIGNAL DELETED.
!     IACTK = THE ACTIVITY CODE
!     NPRMS = THE NUMBER OF PARMETERS
!     PRMS  = THE PARMETER ARRAY.
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'OPCOM.f90'
!
!
!OMMONS
!
!     ISPACT= AN ARRAY OF ACTIVITIY CODES WHICH CORRESPOND TO
!             ACTIVITIES WHICH THE DATE AN EVENT OCCURES IS PLACED IN
!             POSITION 1 OF THE PARAMETER LIST.
!     NSPACT= THE LENGHT OF ISPACT.
!
INTEGER KODE,NPRMS,ISTUS,IACTK,IDT,NSPACT,ID,NREDTS,IYR1,IYR2
INTEGER I2,I,NCOPYS,II,NSCHDS,IP1,IP2,N,IA,I1,J,NCYC,ICYC
PARAMETER (NSPACT=4)
REAL PRMS(NPRMS)
INTEGER IY(1),ISPACT(NSPACT)
DATA ISPACT/33,427,428,429/
!
!     CHECK THE RANGE OF IDT
!
IF (IDT .GT. 0) GOTO 10
KODE=2
RETURN
10 CONTINUE
!
!     DEFINE 'ID' AS THE DATE CODE.
!
ID=IDT
ISEQDN=ISEQDN+1
ISEQ(IMGL)=ISEQDN
CALL OPNEW (KODE,ID,IACTK,NPRMS,PRMS)
!
!     SET THE OPTION STATUS TO ISTUS (IF LOPEVN IS TRUE, LEAVE THE
!     STATUS CODE UNCHANGED).
!
IF (.NOT.LOPEVN) IACT(IMGL-1,4)=ISTUS
RETURN
!
!
ENTRY OPREDT (IACTK,IYR1,IYR2,NREDTS)
!
!     OPREDT IS USED TO REDATE AND RESCHEDULE AN ACTIVITY TO OCCUR
!     A DIFFERENT DATE.  ACTIVITIES ALREADY ACCOMPLISHED OR DELETED
!     WILL NOT BE REDATED NOR RESCHEDULED.  YOU MUST CALL OPINCR
!     AFTER ONE OR MORE CALLS TO OPREDT (SEE OPINCR, BELOW).
!
!     IACTK = THE ACTIVITY CODE.
!     IYR1  = THE YEAR THE ACTIVITY IS SCHEDULED.
!     IYR2  = THE NEW YEAR YOU WISH THE ACTIVITY TO BE SCHEDULED.
!     NREDTS= THE NUMBER OF REDATES WHICH OCCURED.
!
NREDTS=0
I2=IMGL-1
DO 20 I=1,I2
ID=IDATE(I)
IF (ID.NE.IYR1 .OR. IACTK.NE.IACT(I,1) .OR. IACT(I,4).NE.0) &
      GOTO 20
NREDTS=NREDTS+1
IDATE(I)=IYR2
20 CONTINUE
RETURN
!
!
ENTRY OPCOPY (IACTK,IYR1,IYR2,NCOPYS,KODE)
!
!     OPCOPY IS USED TO COPY AN ACTIVITY (WHEATHER OR NOT IT HAS BEEN
!     ACCOMPLISHED, BUT NOT IF IT HAS BEEN DELETED) FROM ONE DATE TO
!     ANOTHER.  YOU MUST CALL OPINCR AFTER ONE OR MORE CALLS TO
!     OPREDT (SEE OPINCR, BELOW).  THE ARGUMENTS:
!
!     IACTK = THE ACTIVITY CODE.
!     IYR1  = THE YEAR THE ACTIVITY IS SCHEDULED.
!     IYR2  = THE NEW YEAR THE ACTIVITY IS TO BE COPIED TO.
!     NCOPYS= THE NUMBER OF ACTIVITIES COPIED.
!     KODE  = 0 IMPLIES NO ERRORS.
!             1 IMPLIES ONE OR MORE OF THE OCCURANCES OF IACTK IN
!               YEAR IYR1 WERE NOT COPIED BECAUSE THERE WAS NOT
!               ENOUGH ROOM IN THE ACTIVITY ARRAYS.  THE NUMBER
!               COPIED (NCOPYS) IS CORRECTLY REPORTED.  A WARNING
!               MSG IS ISSUED VIA ERRGRO.
!
KODE=0
NCOPYS=0
I2=IMGL-1
DO 40 II=1,I2
I=IOPSRT(II)
IF (IACTK.NE.IACT(I,1)) GOTO 40
ID=IDATE(I)
IF (ID.NE.IYR1.OR.IACT(I,4).LT.0) GOTO 40
IF (IMGL .LE. IEPT) GOTO 30
KODE=1
CALL ERRGRO (.TRUE.,10)
WRITE (16,*) 'ISSUED IN OPADD ENTRY OPCOPY'
RETURN
30 CONTINUE
NCOPYS=NCOPYS+1
IOPSRT(IMGL)=IMGL
IDATE(IMGL)=IYR2
ISEQ (IMGL)=IMGL
IACT(IMGL,1)=IACT(I,1)
IACT(IMGL,2)=IACT(I,2)
IACT(IMGL,3)=IACT(I,3)
IACT(IMGL,4)=0
IACT(IMGL,5)=IACT(I,5)
IMGL=IMGL+1
40 CONTINUE
RETURN
!
!
ENTRY OPSCHD (IDT,IP1,IP2,NSCHDS,KODE)
!
!     OPSCHD IS USED BY THE EVENT MONITORING SYSTEM TO ACTUALLY
!     SCHEDULE ACTIVITIES WHICH HAVE BEEN REQUESTED TO OCCUR
!     FOLLOWING A GIVEN EVENT.  YOU MUST CALL OPINCR AFTER ONE OR
!     MORE CALLS TO OPSCHD (SEE OPINCR, BELOW).  THE ARGUMENTS:
!
!     IDT   = THE DATE THE EVENT OCCURED.
!     IP1   = POINTS TO THE FIRST ACTIVITY TO BE SCHEDULED.
!     IP2   = POINTS TO THE LAST ACTIVITY TO BE SCHEDULED.
!     NSCHDS= THE NUMBER OF ACTIVITIES ACTUALLY SCHEDULED.
!     KODE  = RETURN CODE, WHERE:
!             0 IMPLIES NO ERRORS
!             1 IMPLIES THAT IP1<IP2, OR IP1 OR IP2 = 0; THEREFORE
!               NO ACTIVITIES ARE SCHEDULED.
!             2 IMPLIES THAT NONE OR SOME OF THE ACTIVITIES WERE
!               SCHEDULED BECAUSE THERE WAS NOT ENOUGH ROOM IN THE
!               ACTIVITY ARRAYS.  AN ERROR MSG. IS ISSUED VIA ERRGRO.
!
!     CHECK RANGE OF IP1 & IP2 AND SPACE IN ACTIVITY ARRAYS.
!
NSCHDS=0
KODE=0
N=IP1-IP2+1
IF (IP1*IP2.NE.0 .AND. N.GT.0) GOTO 50
KODE=1
RETURN
50 CONTINUE
IF (N+IMGL.LE.IEPT+1) GOTO 60
KODE=2
CALL ERRGRO (.TRUE.,10)
WRITE (16,*) 'ISSUED IN OPADD ENTRY OPSCHD 1'
RETURN
60 CONTINUE
!
!     MOVE THE ACTIVITIES
!
DO 120 I=1,N
II=IP1-I+1
!
!     SKIP DELETED ACTIVITIES.
!
IF (IACT(II,4).LT.0) GOTO 120
IOPSRT(IMGL)=IMGL
IDATE(IMGL)=(IDT+IDATE(II))
ISEQDN=ISEQDN+1
ISEQ(IMGL)=ISEQDN
IACT(IMGL,1)=IACT(II,1)
IACT(IMGL,2)=IACT(II,2)
IACT(IMGL,3)=IACT(II,3)
IACT(IMGL,4)=0
IACT(IMGL,5)=IACT(II,5)
!
!     FIND OUT IF THE SCHEDULED ACTIVITY IS ONE OF THE ONES WHICH
!     REQUIRES THE DATE THE EVENT OCCURED BE ENTERED IN THE PARMS.
!
IA=IACT(IMGL,1)
DO 70 II=1,NSPACT
IF (IA.EQ.ISPACT(II)) GOTO 80
70 CONTINUE
GOTO 110
80 CONTINUE
!
!     ADD THE DATE THE EVENT OCCURED TO THE PARMAETER LIST.
!     CASE 1: THE PARMETER LIST CONTAINS AT LEAST 1 ENTRY; COPY THE
!     PARAMETER LIST AND SET THE FIRST PARAMETER TO THE DATE.
!
I1=IACT(IMGL,2)
IF (I1.GT.0) THEN
   I2=IACT(IMGL,3)
   IF (IMPL+I2-I1.GT.ITOPRM) THEN
      KODE=2
      CALL ERRGRO (.TRUE.,10)
      WRITE (16,*) 'ISSUED IN OPADD ENTRY OPSCHD DATE ',IDT
      RETURN
   ENDIF
   IACT(IMGL,2)=IMPL
   DO J=I1,I2
      PARMS(IMPL)=PARMS(J)
      IMPL=IMPL+1
   ENDDO
   IACT(IMGL,3)=IMPL-1
   PARMS(IACT(IMGL,2))=FLOAT(IDT)
ELSE
!
!     CASE 2: THE PARAMETER LIST IS EMPTY, MAKE AN ENTRY AND STORE
!     THE VALUE.
!
   IF (IMPL.GT.ITOPRM) THEN
      KODE=2
      CALL ERRGRO (.TRUE.,10)
      WRITE (16,*) 'ISSUED IN OPADD ENTRY OPSCHD NO PARAMS'
      RETURN
   ENDIF
   PARMS(IMPL)=FLOAT(IDT)
   IACT(IMGL,2)=IMPL
   IACT(IMGL,3)=IMPL
   IMPL=IMPL+1
ENDIF
110 CONTINUE
IMGL=IMGL+1
NSCHDS=NSCHDS+1
120 CONTINUE
RETURN
!
!
ENTRY OPINCR (IY,ICYC,NCYC)
!
!     OPINCR IS USED TO INCORPORATE THE RESULTS OF PREVIOUS CALLS
!     TO OPADD, OPREDT, OPCOPY, OR OPSCHD INTO THE ACTIVITY
!     SCHEDULE.  THE REASON YOU MUST CALL OPINCR IS THAT CALLING
!     OPADD, OPREDT, OPCOPY, OR OPSCHD DOES NOT COMPLETE THE JOB
!     REQUIRED TO RESET THE POINTERS USED TO ACCESS THE ACTIVITIES
!     VIA CALLS TO MOST OF THE OTHER ROUTINES.  SUBROUTINE OPINCR
!     IS SEPERATE FROM THE ROUTINES IT FOLLOWS BECAUSE IT SAVES
!     CPU TIME TO EXECUTE SEVERAL ADDS, COPIES, ETC PRIOR TO
!     REALIGNING THE POINTER ARRAYS.  NOTE THAT OPINCR IS NOT
!     NEEDED FOLLOWING CALLS TO OPDEL1, OPDEL2, OR OPDEL3 NOR
!     FOLLOWING OPADD WHERE ISTUS > 0.
!     THE AGRUMENTS:
!     IY   =THE PROGNOSIS MODEL'S CYCLE DATE ARRAY.
!     ICYC =THE CURRENT CYCLE NUMBER.
!     NCYC =THE NUMBER OF CYCLES.
!
CALL OPSORT (IMGL-1,IDATE,ISEQ,IOPSRT,.FALSE.)
CALL OPCYCL (NCYC,IY(1))
CALL OPCSET (ICYC)
RETURN
END
