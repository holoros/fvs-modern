SUBROUTINE LBINTR (LEN1,SET1,LEN2,SET2,LNINTR,INTRST,KODE)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     SET INTERSECTION OPERATION
!
!     PART OF THE LABEL PROCESSING COMPONENT OF THE PROGNOSIS SYSTEM
!     N.L. CROOKSTON -- INTERMOUNTAIN RESEARCH STATION -- JAN 1987
!
!     LEN1  = LENGTH OF THE DEFINED PART OF THE STRING SET1
!     SET1  = C*250 STRING CONTAINING THE FIRST SET
!     LEN2  = LENGTH OF SECOND STRING
!     SET2  = C*250 STRING CONTAINING THE SECOND SET
!     LNINTR= THE LENGTH OF THE INTRST SET STRING
!     INTRST= C*250 SET CONTAINING THE INTERSECTION
!     KODE  = RETURN CODES: 0= OK, 1=INTRST IS OVER 250 CHARS
!             (THE ROUTINE CREATES AS MUCH OF AN INTERSECTION AS
!             POSSIBLE WITHOUT CREATING A PARTIAL LABEL)
!
CHARACTER*250 SET1,SET2,INTRST,WRKS1,WRKS2
LOGICAL LBMEMR
INTEGER KODE,LNINTR,LEN1,LEN2,IP,LENWRK
!
!     INITIALIZE THE RETURN CODE TO ZERO AND THE INTRST TO ZERO LENGTH.
!
KODE=0
LNINTR=0
INTRST=' '
!
!     IF EITHER OF THE SETS HAVE ZERO LENGTH OR IF EATHER SET IS
!     UNDEFINED, THE INTERSECTION IS AN EMPTY SET.
!
IF (LEN1.LE.0 .OR. LEN2.LE.0) THEN
   LNINTR=0
   GOTO 20
ENDIF
!
!     BOTH SETS HAVE GREATER THAN ZERO LENGTH...PROCEED WITH THE
!     INTERSECTION.
!
!     LOOP THRU ALL OF THE MEMBERS OF THE FIRST SET.
!
IP=1
10 CONTINUE
!
!     IF IP IS EQUAL TO THE LENGTH OF THE FIRST SET, END THE LOOP.
!
IF (IP.GT.LEN1) GOTO 20
!
!     LET WRKS1 BE THE FIRST MEMBER OF SET1 BETWEEN LOCATION IP
!     AND THE END OF THE STRING HOLDING SET1.
!
CALL LB1MEM (IP,LEN1,SET1,LENWRK,WRKS1)
!
!     FIND OUT IF WRKS1 IS IN SET2.  IF IT IS, THEN ADD IT
!     ONTO THE END OF THE INTERSECTION SET (IF THERE IS ROOM).
!
IF (LBMEMR(LENWRK,WRKS1,LEN2,SET2)) THEN
   IF (LNINTR.EQ.0) THEN
      INTRST = WRKS1
      LNINTR = LENWRK
   ELSE
      IF (LNINTR+LENWRK+2.LE.250) THEN
         WRKS2 = INTRST(1:LNINTR) // ', ' // WRKS1(1:LENWRK)
         LNINTR = LNINTR+LENWRK+2
         INTRST = WRKS2
      ELSE
         KODE = 1
      ENDIF
   ENDIF
ENDIF
!
!     SET THE IP TO THE LAST CHARACTER OF THE CURRENT MEMBER PLUS 2
!     (ONE FOR THE COMMA AND ANOTHER FOR THE BLANK THAT PRECEDES EACH
!     OF THE MEMBERS PAST THE FIRST ONE).
!
IP=IP+LENWRK+2
!
!     BRANCH BACK TO PROCESS THE NEXT LABEL.
!
GOTO 10
20 CONTINUE
RETURN
END
