#!/bin/bash
# ==========================================================================
# FVS-Online SSL/TLS Setup with Let's Encrypt
# ==========================================================================
# Usage: sudo ./setup_ssl.sh DOMAIN [EMAIL]
#
# Example:
#   sudo ./setup_ssl.sh fvs.example.edu admin@example.edu
#
# This script:
#   1. Installs certbot and the nginx plugin
#   2. Obtains a Let's Encrypt certificate for the domain
#   3. Generates a production nginx config with SSL
#   4. Enables automatic certificate renewal
#   5. Configures firewall rules for HTTPS
# ==========================================================================

set -euo pipefail

DOMAIN="${1:-}"
EMAIL="${2:-}"

if [ -z "$DOMAIN" ]; then
    echo "Usage: sudo $0 DOMAIN [EMAIL]"
    echo "Example: sudo $0 fvs.example.edu admin@example.edu"
    exit 1
fi

if [ "$EUID" -ne 0 ]; then
    echo "ERROR: This script must be run as root (use sudo)"
    exit 1
fi

info()  { echo "[INFO]  $1"; }
warn()  { echo "[WARN]  $1"; }
error() { echo "[ERROR] $1"; exit 1; }

# =========================================================================
info "Step 1: Installing certbot..."

if command -v dnf &>/dev/null; then
    dnf install -y certbot python3-certbot-nginx
elif command -v apt-get &>/dev/null; then
    apt-get update && apt-get install -y certbot python3-certbot-nginx
else
    error "Unsupported package manager. Install certbot manually."
fi

# =========================================================================
info "Step 2: Verifying nginx is running..."

if ! systemctl is-active --quiet nginx; then
    systemctl start nginx
    systemctl enable nginx
fi

# =========================================================================
info "Step 3: Generating production nginx config..."

cat > /etc/nginx/conf.d/fvsonline.conf << NGINX_EOF
# FVS-Online Production Configuration
# Generated by setup_ssl.sh on $(date +%Y-%m-%d)
# Domain: ${DOMAIN}

map \$http_upgrade \$connection_upgrade {
    default upgrade;
    ''      close;
}

# Redirect all HTTP traffic to HTTPS
server {
    listen 80;
    server_name ${DOMAIN};
    return 301 https://\$host\$request_uri;
}

server {
    listen 443 ssl http2;
    server_name ${DOMAIN};

    # SSL certificates (managed by certbot)
    ssl_certificate     /etc/letsencrypt/live/${DOMAIN}/fullchain.pem;
    ssl_certificate_key /etc/letsencrypt/live/${DOMAIN}/privkey.pem;

    # Modern SSL configuration
    ssl_protocols TLSv1.2 TLSv1.3;
    ssl_ciphers ECDHE-ECDSA-AES128-GCM-SHA256:ECDHE-RSA-AES128-GCM-SHA256:ECDHE-ECDSA-AES256-GCM-SHA384:ECDHE-RSA-AES256-GCM-SHA384;
    ssl_prefer_server_ciphers off;
    ssl_session_cache shared:SSL:10m;
    ssl_session_timeout 1d;
    ssl_session_tickets off;

    # HSTS (uncomment after confirming SSL works)
    # add_header Strict-Transport-Security "max-age=63072000" always;

    # Security headers
    add_header X-Frame-Options SAMEORIGIN;
    add_header X-Content-Type-Options nosniff;
    add_header X-XSS-Protection "1; mode=block";

    # Project Builder
    location /FVSPrjBldr/ {
        proxy_pass http://127.0.0.1:3839/;
        proxy_http_version 1.1;
        proxy_set_header Upgrade \$http_upgrade;
        proxy_set_header Connection \$connection_upgrade;
        proxy_set_header Host \$host;
        proxy_set_header X-Real-IP \$remote_addr;
        proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto \$scheme;
        proxy_read_timeout 300s;
        proxy_send_timeout 300s;
        proxy_connect_timeout 60s;
    }

    # FVS-Online main application
    location /FVSwork/ {
        proxy_pass http://127.0.0.1:3838/;
        proxy_http_version 1.1;
        proxy_set_header Upgrade \$http_upgrade;
        proxy_set_header Connection \$connection_upgrade;
        proxy_set_header Host \$host;
        proxy_set_header X-Real-IP \$remote_addr;
        proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto \$scheme;
        proxy_read_timeout 600s;
        proxy_send_timeout 600s;
        proxy_connect_timeout 60s;
        client_max_body_size 500M;
    }

    # Data converter
    location /FVSDataConvert/ {
        proxy_pass http://127.0.0.1:3840/;
        proxy_http_version 1.1;
        proxy_set_header Upgrade \$http_upgrade;
        proxy_set_header Connection \$connection_upgrade;
        proxy_set_header Host \$host;
        proxy_set_header X-Real-IP \$remote_addr;
        proxy_read_timeout 300s;
        client_max_body_size 200M;
    }

    # Static landing page
    location / {
        root /srv/fvs/www;
        index index.html;
    }
}
NGINX_EOF

# =========================================================================
info "Step 4: Testing nginx configuration..."

nginx -t || error "Nginx configuration test failed"

# =========================================================================
info "Step 5: Obtaining SSL certificate..."

CERTBOT_ARGS="--nginx -d ${DOMAIN} --non-interactive --agree-tos"
if [ -n "$EMAIL" ]; then
    CERTBOT_ARGS="$CERTBOT_ARGS --email ${EMAIL}"
else
    CERTBOT_ARGS="$CERTBOT_ARGS --register-unsafely-without-email"
    warn "No email provided. Certificate expiration warnings will not be sent."
fi

# Check if certificate already exists
if [ -d "/etc/letsencrypt/live/${DOMAIN}" ]; then
    info "Certificate already exists for ${DOMAIN}. Renewing..."
    certbot renew --dry-run
else
    certbot $CERTBOT_ARGS
fi

# =========================================================================
info "Step 6: Configuring firewall..."

if command -v firewall-cmd &>/dev/null; then
    firewall-cmd --permanent --add-service=https 2>/dev/null || true
    firewall-cmd --reload 2>/dev/null || true
    info "Firewall: HTTPS allowed (firewalld)"
elif command -v ufw &>/dev/null; then
    ufw allow 443/tcp 2>/dev/null || true
    info "Firewall: HTTPS allowed (ufw)"
fi

# =========================================================================
info "Step 7: Configuring SELinux (if applicable)..."

if command -v setsebool &>/dev/null; then
    setsebool -P httpd_can_network_connect 1 2>/dev/null || true
    info "SELinux: httpd_can_network_connect enabled"
fi

# =========================================================================
info "Step 8: Verifying automatic renewal..."

# Certbot installs a systemd timer for automatic renewal
if systemctl list-timers | grep -q certbot; then
    info "Automatic renewal timer is active."
else
    # Create renewal cron job as fallback
    echo "0 0,12 * * * root certbot renew --quiet --deploy-hook 'systemctl reload nginx'" > /etc/cron.d/certbot-renew
    info "Created cron job for automatic renewal (twice daily)."
fi

# =========================================================================
info "Step 9: Reloading nginx with SSL..."

systemctl reload nginx

# =========================================================================
echo ""
echo "============================================================"
echo "  SSL/TLS SETUP COMPLETE"
echo "============================================================"
echo ""
echo "  Domain:       https://${DOMAIN}"
echo "  Certificate:  /etc/letsencrypt/live/${DOMAIN}/"
echo "  Nginx config: /etc/nginx/conf.d/fvsonline.conf"
echo ""
echo "  Application URLs:"
echo "    Project Builder:  https://${DOMAIN}/FVSPrjBldr/"
echo "    FVS-Online:       https://${DOMAIN}/FVSwork/"
echo "    Data Converter:   https://${DOMAIN}/FVSDataConvert/"
echo ""
echo "  After verifying SSL works, uncomment the HSTS header"
echo "  in /etc/nginx/conf.d/fvsonline.conf for added security."
echo "============================================================"
