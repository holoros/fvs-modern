SUBROUTINE DGDRIV
IMPLICIT NONE
!----------
! WS $Id$
!----------
!  THIS SUBROUTINE SERVES TWO PURPOSES:
!
!    1)  IT IS CALLED PRIOR TO PROJECTION IN ORDER TO CALIBRATE
!        THE IMBEDDED GROWTH EQUATIONS SO THAT THEY MORE NEARLY
!        MATCH PAST GROWTH RATES MEASURED IN THE STAND.  THE
!        CALIBRATION SEGMENT BEGINS AT STATEMENT 100, AND IS
!        ACCESSED ONLY WHEN THE VALUE OF LSTART IS TRUE.
!
!    2)  SUBSEQUENTLY, IT IS CALLED EACH CYCLE TO CALCULATE
!        DIAMETER INCREMENT FOR THE NEXT PERIOD.
!
!  **DGF** IS ACCESSED TO LOAD LN(DDS) INTO THE ARRAY WK3.  DDS
!  REPRESENTS CHANGE IN SQUARED DIAMETER.
!----------
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'CALCOM.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'COEFFS.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
INCLUDE 'PLOT.f90'
!
!
INCLUDE 'MULTCM.f90'
!
!
INCLUDE 'ESTCOR.f90'
!
!
INCLUDE 'WORKCM.f90'
!
!
!OMMONS
!----------
!  DIMENSIONS AND DATA STATEMENTS FOR INTERNAL VARIABLES.
!
!    BKRAT2 -- MULTIPLIER WHICH COMPENSATES FOR BARK GROWTH WHEN
!              DIAMETER GROWTH IS MEASURED INSIDE BARK.
!    CORTEM -- ARRAY USED TO TEMPORARILY STORE CALCULATED CORRECTION
!              TERMS IN THE ORDER THAT SPECIES WERE ENCOUNTERED IN
!              THE INPUT TREE LIST.  THIS ARRAY IS PRINTED TO DISPLAY
!              CORRECTION TERMS BEING USED FOR A STAND.
!        FU -- ARRAYS WHICH ARE DIMENSIONED BY SPECIES AND CARRY
!        FM    RECORD TRIPLING GROWTH MULTIPLIERS.  WE ASSUME THAT
!        FL    LN(DDS) IS NORMALLY DISTRIBUTED.  THEN, EACH RECORD
!              IS PARTITIONED INTO TRIPLES.  THE FIRST TRIPLE
!              REPRESENTS 60% OF THE ORIGINAL RECORD, AND RECEIVES
!              A GROWTH RATE BASED ON A VALUE OF LN(DDS) WHICH HAS
!              BEEN DEFLATED BY 0.14228 STANDARD DEVIATIONS.  THE
!              SECOND TRIPLE REPRESENTS 25% OF THE ORIGINAL RECORD,
!              AND RECEIVES A GROWTH RATE BASED ON A VALUE OF
!              LN(DDS) WHICH HAS BEEN INFLATED BY 1.271 STANDARD
!              DEVIATIONS.  THE FINAL TRIPLE REPRESENTS 15% OF THE
!              ORIGINAL RECORD, AND RECEIVES A GROWTH RATE WHICH IS
!              BASED ON A VALUE OF LN(DDS) THAT HAS BEEN DEFLATED BY
!              1.549 STANDARD DEVIATIONS.
!     SIGMA -- ARRAY, DIMENSIONED BY SPECIES, THAT CONTAINS THE
!              POOLED STANDARD ERRORS ABOUT PREDICTED DIAMETER
!              GROWTH.  THESE STANDARD ERRORS ARE WEIGHTED AVERAGES
!              OF THE REGRESSION ESTIMATES FOR THE IMBEDDED MODELS
!              AND THE ESTIMATES FOR THE INPUT GROWTH SAMPLE TREES.
!----------
EXTERNAL RANN
LOGICAL DEBUG
CHARACTER DAT*10,TIM*8,SPEC*2,REV*10
INTEGER NUMCAL(MAXSP)
INTEGER ISI,IREFI,N
INTEGER I,ISPC,I1,I2,I3,ITRIPU,ITRIPL,KOUT,K,ISPEC,KK
REAL MISDGF
REAL STDRAT(MAXSP),CORTEM(MAXSP),PSIGSQ(MAXSP)
REAL SCALE,CORNEW,DEV,DEVSQ,XNOB,FN,SPOPN,SPOPX,SNP,SNX,SNXY
REAL SNYY,DN,DX,PN,PX,P,BARK,EDDS,TERM,RESLOG,WC,BPOPX,BNX,BNY
REAL CSNXY,CSNXX,SLOP,RATIO,VTEMP,Z,TEMP,PVAR,CORI,SVAR,SIGMR1
REAL RN,RX,REGCOR,DIST,SDPRED,SNXX,SNY,FRMT,DSQ,DDS,D,WKI,CORMLT
REAL FRM,FRU,PVMLT,CORR,SFINT,VARYP1,EVARP1,SIG1,VARYP2,EVARP2
REAL SSIGMA,RHO,RHOCP,XDGROW,FRL,BRATIO,BACHLO
REAL RNPAR
!----------
!     SPECIES LIST FOR WESTERN SIERRAS VARIANT.
!
!     1 = SUGAR PINE (SP)                   PINUS LAMBERTIANA
!     2 = DOUGLAS-FIR (DF)                  PSEUDOTSUGA MENZIESII
!     3 = WHITE FIR (WF)                    ABIES CONCOLOR
!     4 = GIANT SEQUOIA (GS)                SEQUOIADENDRON GIGANTEAUM
!     5 = INCENSE CEDAR (IC)                LIBOCEDRUS DECURRENS
!     6 = JEFFREY PINE (JP)                 PINUS JEFFREYI
!     7 = CALIFORNIA RED FIR (RF)           ABIES MAGNIFICA
!     8 = PONDEROSA PINE (PP)               PINUS PONDEROSA
!     9 = LODGEPOLE PINE (LP)               PINUS CONTORTA
!    10 = WHITEBARK PINE (WB)               PINUS ALBICAULIS
!    11 = WESTERN WHITE PINE (WP)           PINUS MONTICOLA
!    12 = SINGLELEAF PINYON (PM)            PINUS MONOPHYLLA
!    13 = PACIFIC SILVER FIR (SF)           ABIES AMABILIS
!    14 = KNOBCONE PINE (KP)                PINUS ATTENUATA
!    15 = FOXTAIL PINE (FP)                 PINUS BALFOURIANA
!    16 = COULTER PINE (CP)                 PINUS COULTERI
!    17 = LIMBER PINE (LM)                  PINUS FLEXILIS
!    18 = MONTEREY PINE (MP)                PINUS RADIATA
!    19 = GRAY PINE (GP)                    PINUS SABINIANA
!         (OR CALIFORNIA FOOTHILL PINE)
!    20 = WASHOE PINE (WE)                  PINUS WASHOENSIS
!    21 = GREAT BASIN BRISTLECONE PINE (GB) PINUS LONGAEVA
!    22 = BIGCONE DOUGLAS-FIR (BD)          PSEUDOTSUGA MACROCARPA
!    23 = REDWOOD (RW)                      SEQUOIA SEMPERVIRENS
!    24 = MOUNTAIN HEMLOCK (MH)             TSUGA MERTENSIANA
!    25 = WESTERN JUNIPER (WJ)              JUNIPERUS OCIDENTALIS
!    26 = UTAH JUNIPER (UJ)                 JUNIPERUS OSTEOSPERMA
!    27 = CALIFORNIA JUNIPER (CJ)           JUNIPERUS CALIFORNICA
!    28 = CALIFORNIA LIVE OAK (LO)          QUERCUS AGRIFOLIA
!    29 = CANYON LIVE OAK (CY)              QUERCUS CHRYSOLEPSIS
!    30 = BLUE OAK (BL)                     QUERCUS DOUGLASII
!    31 = CALIFORNIA BLACK OAK (BO)         QUERQUS KELLOGGII
!    32 = VALLEY OAK (VO)                   QUERCUS LOBATA
!         (OR CALIFORNIA WHITE OAK)
!    33 = INTERIOR LIVE OAK (IO)            QUERCUS WISLIZENI
!    34 = TANOAK (TO)                       LITHOCARPUS DENSIFLORUS
!    35 = GIANT CHINQUAPIN (GC)             CHRYSOLEPIS CHRYSOPHYLLA
!    36 = QUAKING ASPEN (AS)                POPULUS TREMULOIDES
!    37 = CALIFORNIA-LAUREL (CL)            UMBELLULARIA CALIFORNICA
!    38 = PACIFIC MADRONE (MA)              ARBUTUS MENZIESII
!    39 = PACIFIC DOGWOOD (DG)              CORNUS NUTTALLII
!    40 = BIGLEAF MAPLE (BM)                ACER MACROPHYLLUM
!    41 = CURLLEAF MOUNTAIN-MAHOGANY (MC)   CERCOCARPUS LEDIFOLIUS
!    42 = OTHER SOFTWOODS (OS)
!    43 = OTHER HARDWOODS (OH)
!
!  SURROGATE EQUATION ASSIGNMENT:
!
!    FROM EXISTING WS EQUATIONS --
!      USE 1(SP) FOR 11(WP) AND 24(MH)
!      USE 2(DF) FOR 22(BD)
!      USE 3(WF) FOR 13(SF)
!      USE 4(GS) FOR 23(RW)
!      USE 8(PP) FOR 18(MP)
!      USE 34(TO) FOR 35(GC), 36(AS), 37(CL), 38(MA), AND 39(DG)
!      USE 31(BO) FOR 28(LO), 29(CY), 30(BL), 32(VO), 33(IO), 40(BM), AND
!                     43(OH)
!
!    FROM CA VARIANT --
!      USE CA11(KP) FOR 12(PM), 14(KP), 15(FP), 16(CP), 17(LM), 19(GP), 20(WE),
!                       25(WJ), 26(WJ), AND 27(CJ)
!      USE CA12(LP) FOR 9(LP) AND 10(WB)
!
!    FROM SO VARIANT --
!      USE SO30(MC) FOR 41(MC)
!
!    FROM UT VARIANT --
!      USE UT17(GB) FOR 21(GB)
!----------
!  DATA STATEMENTS
!  NOTE: THESE PSIGSQ VALUES ARE FROM THE NI VARIANT
!----------
DATA PSIGSQ/ &
     0.0586,  0.1556,  0.0970,  0.0858,  0.1433, &
     0.0970,  0.0970,  0.0636,  0.0898,  0.0898, &
     0.0586,  0.0898,  0.0970,  0.0898,  0.0898, &
     0.0898,  0.0898,  0.0636,  0.0898,  0.0898, &
       0.07,  0.1556,  0.0858,  0.0586,  0.0898, &
     0.0898,  0.0898,  0.0636,  0.0636,  0.0636, &
     0.0636,  0.0636,  0.0636,  0.0858,  0.0858, &
     0.0858,  0.0858,  0.0858,  0.0858,  0.0636, &
     0.0898,  0.0408,  0.0636/
!-----------
!  CHECK FOR DEBUG.
!-----------
CALL DBCHK (DEBUG,'DGDRIV',6,ICYC)
!----------
!  BEGIN GROWTH CALCULATION SEGMENT.  BRANCH TO STATEMENT 100
!  FOR CALIBRATION.
!----------
IF(LSTART) GO TO 100
!----------
!  CALL MULTS TO PROCESS THE MANAGED KEYWORD.
!----------
WORK2(1)=REAL(MANAGD)
CALL MULTS (7,IY(ICYC),WORK2)
MANAGD=INT(WORK2(1))
!----------
!  RECALL THE VARIANCE MULTIPLIER FROM THE PRECEDING CYCLE (PVMLT).
!----------
PVMLT=VMLT
!----------
!  CALCULATE THE VARIANCE AND COVARIANCE MULTIPLIERS FOR THE CURRENT
!  CYCLE.  COVARIANCE IS ZERO IN THE FIRST CYCLE.
!----------
CALL AUTCOR(COVMLT,VMLT)
!----------
!  COMPUTE SERIAL CORRELATION FOR PREDICTION ERRORS.
!----------
CORR=COVMLT/SQRT(VMLT*PVMLT)
!----------
!  LOAD WK1 WITH DBH INCREMENT FROM PREVIOUS CYCLE FOR USE IN
!  MORTS.
!----------
DO 5 I=1,ITRN
WK1(I)=DG(I)
5 CONTINUE
!----------
!  CALL **DGF** TO LOAD WK2 WITH EXPECTED VALUES OF LN(DDS).
!----------
CALL DGF(DBH)
!----------
!  LOOP THROUGH TREE LIST IN SPECIES ORDER TO COMPUTE DIAMETER
!  GROWTH FROM DDS.  ASSIGN SPECIES DEPENDENT VARIABLES.
!----------
CALL MULTS (1,IY(ICYC),XDMULT)
CALL CLGMULT(WK4)
!----------
!  OUTPUT MODEL COEFFICIENTS FOR DEBUG
!----------
IF(DEBUG) WRITE(JOSTND,9002) SIGMA,XDMULT,BKRAT,COR
9002 FORMAT(/11(F11.5))
SFINT=IY(ICYC+1)-IY(1)
DO 50 ISPC=1,MAXSP
!----------
!  COMPUTE SERIAL CORRELATION ADJUSTED FPR PERIOD LENGTH.
!----------
VARYP1=VARDG(ISPC)*PVMLT
EVARP1=(SQRT(1.0+4.0*VARYP1)+1.0)/2.0
SIG1=SQRT(ALOG(EVARP1))
VARYP2=VARDG(ISPC)*VMLT
EVARP2=(SQRT(1.0+4.0*VARYP2)+1.0)/2.0
SSIGMA=SQRT(ALOG(EVARP2))
RHO=ALOG(1.0+CORR*SQRT((EVARP1-1.0)*(EVARP2-1.0)))/ &
            (SIG1*SSIGMA)
RHOCP=SQRT(1.0-RHO*RHO)
XDGROW=ALOG(XDMULT(ISPC))
!----------
!  IN THE FIRST CYCLE DEFINE THE GOAL FOR ATTENUATING CORRECTION
!  TERMS FOR THE SMALL TREE HEIGHT INCREMENT MODEL AND THE LARGE
!  TREE DBH INCREMENT MODEL.  CORRECTION TERMS FOR BOTH MODELS
!  WILL ATTENUATE TO 1/2 THE INITIAL VALUE OF THE CORRECTION TERM
!  FOR THE DBH INCREMENT MODEL, AND 1/2 OF THE ATTENUATION WILL
!  TAKE PLACE IN THE FIRST 25 YEARS.
!----------
IF(ICYC.NE.1) GO TO 6
WCI(ISPC)=0.5*COR(ISPC)
DIFH(ISPC)=HCOR(ISPC)-WCI(ISPC)
6 CONTINUE
I1=ISCT(ISPC,1)
IF(I1.EQ.0) GO TO 50
I2=ISCT(ISPC,2)
XDGROW=ALOG(XDMULT(ISPC))
!----------
!  IF TRIPLING, ASSIGN ERROR TO TRIPLES; ADJUST FOR SERIAL
!  CORRELATION.
!----------
IF(.NOT.LTRIP) GO TO 10
FRL=FL(ISPC)*SSIGMA*RHOCP
FRM=FM(ISPC)*SSIGMA*RHOCP
FRU=FU(ISPC)*SSIGMA*RHOCP
10 CONTINUE
!----------
!  ATTENUATE THE CORRECTION TERM FOR THE NEXT CYCLE.
!----------
IF (.NOT.LDGCAL(ISPC)) GO TO 15
CORMLT=EXP(-0.02773*SFINT)
COR(ISPC)=WCI(ISPC)+CORMLT*WCI(ISPC)
HCOR(ISPC)=WCI(ISPC)+CORMLT*DIFH(ISPC)
IF(DEBUG) WRITE(JOSTND,9011) ISPC,COR(ISPC),HCOR(ISPC),WCI(ISPC)
9011 FORMAT('FOR SPECIES ',I2,' NEW DGCOR = ',F7.4,' NEW HTCOR = ' &
     ,F7.4,' ATTENUATION GOAL = ',F7.4)
15 CONTINUE
!----------
!  BEGIN TREE LOOP WITHIN SPECIES.
!----------
DO 40 I3=I1,I2
I=IND1(I3)
WKI=0.0
D=DBH(I)*BRATIO(ISPC,DBH(I),HT(I))
DDS=EXP(WK2(I) + XDGROW) * WK4(I)
DSQ=D*D
WKI=(SQRT(DSQ+DDS)-D)
IF(LTRIP) GO TO 30
!----------
!  CALL DGSCOR TO ASSIGN ERROR TO DIAMETER GROWTH PREDICTION.
!----------
CALL DGSCOR (SSIGMA,FRM,RHO,RHOCP,I)
DG(I)=(SQRT(DSQ+DDS*FRM)-D)
!----------
!  ACCOUNT FOR THE NEGATIVE EFFECTS OF DWARF MISTLETOE ON DIAMETER
!  GROWTH, IF ANY.
!----------
DG(I) = DG(I) * MISDGF(I,ISPC)
!----------
!   CALL DGBND TO INSURE THAT DG DOES NOT EXCEED A MAXIMUM DG VALUE.
!----------
CALL DGBND(ISPC,DBH(I),DG(I))
!----------
!  PRINT DEBUG INFO IF DESIRED THEN BRANCH TO END OF TREE LOOP.
!----------
IF(.NOT.DEBUG) GO TO 40
WRITE(JOSTND,9000) I,ISPC,D,HT(I),WKI,DG(I),FRM
9000 FORMAT('IN DGDRIV, I=',I4,', ISPC=',I3,', DBH=',F7.2, &
          ', HT=',F7.2,', EXP.GR.=',F7.4,', PRED.GR.=',F7.4, &
          ', FRM=',F7.4)
GO TO 40
!----------
!  IF TRIPLING RECORDS, ASSIGN DIAMETER GROWTH TO ORIGINAL RECORD
!  AND TRIPLES.  DBH IS ALSO TRIPLED HERE.
!----------
30 ITRIPU=ITRN+2*I-1
ITRIPL=ITRIPU+1
RNPAR=OLDRN(I)
FRMT=FRM+CORR*OLDRN(I)
DG(I)=SQRT(DSQ+DDS*EXP(FRMT))-D
!----------
!  ACCOUNT FOR THE NEGATIVE EFFECTS OF DWARF MISTLETOE ON DIAMETER
!  GROWTH, IF ANY.
!----------
DG(I) = DG(I) * MISDGF(I,ISPC)
OLDRN(I)=FRMT
FRMT=FRU+CORR*RNPAR
DG(ITRIPU)=(SQRT(DSQ+DDS*EXP(FRMT))-D)
!----------
!  DWARF MISTLETOE EFFECTS.
!----------
DG(ITRIPU) = DG(ITRIPU) * MISDGF(I,ISPC)
DBH(ITRIPU)=DBH(I)
OLDRN(ITRIPU)=FRMT
FRMT=FRL+CORR*RNPAR
DG(ITRIPL)=(SQRT(DSQ+DDS*EXP(FRMT))-D)
!----------
!  DWARF MISTLETOE EFFECTS.
!----------
DG(ITRIPL) = DG(ITRIPL) * MISDGF(I,ISPC)
DBH(ITRIPL)=DBH(I)
OLDRN(ITRIPL)=FRMT
!----------
!  CALL DGBND TO INSURE THAT DG DOES NOT EXCEED A MAXIMUM DG VALUE.
!----------
CALL DGBND(ISPC,DBH(I),DG(I))
CALL DGBND(ISPC,DBH(I),DG(ITRIPU))
CALL DGBND(ISPC,DBH(I),DG(ITRIPL))
!----------
!  PRINT DEBUG INFO IF DESIRED.
!----------
IF(.NOT.DEBUG) GO TO 40
WRITE(JOSTND,9001) ISPC,D,HT(I),WKI,I,DG(I),FRM, &
         ITRIPU,DG(ITRIPU),FRU,ITRIPL,DG(ITRIPL),FRL
9001 FORMAT('IN DGDRIV, ISPC=',I3,', DBH=',F7.2, &
          ', HT=',F7.2,', EXP.GR.=',F7.4,', DG(',I4,')=',F7.4, &
          ', FRM=',F7.4/T13,'DG(',I4,')U=',F7.4,', FRU=', &
          F7.4,', DG(',I4,')L=',F7.4,', FRL=',F7.4)
!----------
!  END OF TREE LOOP.
!----------
40 CONTINUE
!----------
!  END OF SPECIES LOOP.
!----------
50 CONTINUE
RETURN
!----------
!  THIS PROGRAM SEGMENT IS FOR CALIBRATION.  IT IS EXECUTED ONLY
!  WHEN LSTART IS TRUE.
!----------
100 CONTINUE
!----------
!  IF CALBSTAT KEYWORD WAS PRESENT, WRITE HEADER RECORD.
!----------
IF(JOCALB .GT. 0) THEN
  CALL REVISE (VARACD,REV)
  CALL GRDTIM(DAT,TIM)
  IF(ISISP .LE. 0)THEN
    ISI=0
  ELSE
    ISI=NINT(SITEAR(ISISP))
  ENDIF
  WRITE(JOCALB,1000)VARACD,DAT,TIM,NPLT,MGMID,KODTYP, &
     ISISP,ISI,IAGE,REV
1000   FORMAT(' CALBSTAT ',A2,6X,A10,1X,A8,1X,A26,1X,A4, &
     1X,I3,1X,I2,1X,I3,1X,I3,1X,A)
ENDIF
!----------
!  COMPUTE VARIANCE MULTIPLIER FOR YR PERIOD AND INITIALIZE VMLT.
!----------
CALL AUTCOR(COVYR,VMLTYR)
VMLT=VMLTYR
!----------
!  INITIALIZE SFINT TO RESTART ATTENUATION FOR THE NEXT PROJECTION.
!----------
SFINT=0.0
IF (ITRN.LE.0) GOTO 115
!----------
!
!     *******   CALIBRATION SECTION   *******
!
!----------
!  DEFINE SCALING FACTOR WHICH CONVERTS TERM TO A FINT-YEAR
!  PERIOD BASIS.
!----------
SCALE=YR/FINT
!----------
!  IF INCREMENT IS INPUT AS DIFFERENCE BETWEEN TWO DBH MEASUREMENTS
!  (IDG=1 OR 3), CONVERT TO INSIDE BARK ESTIMATE.
!----------
IF(IDG.EQ.0.OR.IDG.EQ.2) GO TO 110
DO 105 I=1,ITRN
ISPC=ISP(I)
DG(I)=DG(I)*BRATIO(ISPC,DBH(I),HT(I))
105 CONTINUE
110 CONTINUE
!----------
!  WK3 WAS LOADED IN **DENSE** WITH DBH AS OF THE START OF THE
!  CALIBRATION PERIOD.  **DGF** IS CALLED TO LOAD WK2 WITH PREDICTED
!  LN(DDS).
!----------
CALL DGF(WK3)
!----------
!  BEGIN PROCESSING TREE LIST IN SPECIES ORDER.
!----------
115 CONTINUE
DO 150 I=1,MAXSP
STDRAT(I)=1.0
CORTEM(I)=1.0
WCI(I)=0.0
NUMCAL(I)=0
150 CONTINUE
DO 200 ISPC=1,MAXSP
CORNEW=1.0
SIGMA(ISPC)=SIGMAR(ISPC)
I1=ISCT(ISPC,1)
!----------
!  DO NOT CALCULATE CORRECTION TERMS NOR CALIBRATE BAI MODEL
!  VARIANCE IF IFINT=0 OR THERE ARE NO TREES FOR THIS SPECIES.
!----------
IF(I1.EQ.0.OR.IFINT.EQ.0) GO TO 195
I2=ISCT(ISPC,2)
IREFI= IREF(ISPC)
DEV=0.0
DEVSQ=0.0
N=0
XNOB=ATTEN(ISPC)
FN=0.0
SPOPN=0.0
SPOPX=0.0
SNP=0.0
SNX=0.0
SNY=0.0
SNXY=0.0
SNXX=0.0
SNYY=0.0
!----------
!  FIRST LOOP TO FIND MINIMUM AND MAXIMUM DBH FOR GSTS OF THIS SPECIES
!  AND ASSOCIATED PREDICTED DDS.
!----------
DN=999.0
DX=0.0
DO 155 I3=I1,I2
I=IND1(I3)
IF(WK3(I).LT.3.0 .OR.DG(I).LE.0.0) GO TO 155
IF(WK3(I).LT.DN) THEN
  DN=WK3(I)
  PN=EXP(WK2(I))
ENDIF
IF(WK3(I).GT.DX) THEN
  DX=WK3(I)
  PX=EXP(WK2(I))
ENDIF
155 CONTINUE
!----------
!  BEGIN TREE LOOP WITHIN SPECIES.  IF MEASURED DIAMETER GROWTH
!  IS LESS THAN ZERO, OR DBH IS LESS THAN 3.0 INCHES, THE RECORD
!  WILL NOT BE INCLUDED IN THE CALIBRATION.
!----------
DO 160 I3=I1,I2
I=IND1(I3)
!----------
!  INITIALIZE OLDRN.
!----------
OLDRN(I)=0.0
P=PROB(I)
BARK=BRATIO(ISPC,DBH(I),HT(I))
!----------
!  EXCLUDE TREES THAT ARE OUTSIDE OF THE GST DBH RANGE FROM THE
!  POPULATION TOTALS.
!----------
IF(WK3(I).LT.DN .OR. WK3(I).GT.DX) GO TO 160
EDDS=EXP(WK2(I))
SPOPN=SPOPN+P
SPOPX=SPOPX+EDDS*P
IF(DG(I).LE.0.0) GO TO 160
TERM=DG(I)*(2.0*BARK*WK3(I)+DG(I))*SCALE
!----------
!  SKIP CALCULATION OF STANDARD DEVIATION IF TERM=0.0
!----------
IF(TERM.EQ.0.0) GO TO 159
FN=FN+1.0
RESLOG=ALOG(TERM)-WK2(I)
!----------
!  INITIALIZE OLDRN WITH LN-SCALE RESIDUAL DDS.
!----------
IF(DGSD.GE.1.0) OLDRN(I)=RESLOG
DEV=DEV+RESLOG
DEVSQ=DEVSQ+RESLOG*RESLOG
159 CONTINUE
IF(.NOT.LDGCAL(ISPC)) GO TO 160
!----------
!  COLLECT SUMS TO REGRESS LN(RESIDUAL) ON PREDICTED DDS; WEIGHT
!  OBSERVATIONS BY PROB.
!----------
SNP=SNP+P
SNX=SNX+P*EDDS
SNY=SNY+P*RESLOG
SNXX=SNXX+P*EDDS*EDDS
SNXY=SNXY+P*RESLOG*EDDS
SNYY=SNYY+P*RESLOG*RESLOG
N=N+1
!----------
!  PRINT DEBUG INFO IF DESIRED.
!----------
IF(.NOT.DEBUG) GO TO 160
WRITE(JOSTND,9003) I,ISPC,DG(I),TERM,DEV,DEVSQ,RESLOG
9003 FORMAT('IN DGDRIV,  I=',I4,',  ISPC=',I3,',  OBS. DG=', &
          F7.4,',  TERM=',F10.4,',  CUM. DEV.=',E15.6/ &
          11X,'CUM. DEV. SQ=',E15.6,',  DEV=',E15.6)
!----------
!  END OF TREE WITHIN SPECIES LOOP.
!----------
160 CONTINUE
IF(DEBUG) WRITE(JOSTND,9010) ISPC,SPOPN,SPOPX,FN,SNP, &
                                SNX,SNY,SNXX,SNXY,SNYY
9010 FORMAT(/'SUMS FOR SPECIES ',I2,':  SPOPN=',F10.2,';  SPOPX=', &
     F10.2,';  FN=',F6.1,';  SNP=',F10.2,';  SNX=',F10.2,';'/22X, &
     'SNY=',F10.2,';  SNXX=',F10.2,';  SNXY=',F10.2,';  SNYY=', &
     F10.2)
!----------
!  COMPUTE CALIBRATION TERMS.
!----------
WC=0.0
!----------
!  DO NOT CALCULATE CORRECTION TERMS NOR CALIBRATE BAI MODEL
!  VARIANCE IF THERE ARE FEWER THAN FNMIN (DEFAULT=5.)
!  OBSERVATIONS FOR A SPECIES.
!----------
!  ADDED 'OR' CONDITION SO THAT NO CALIBRATION SPECIFIED IS THE
!  SAME AS HAVING TOO FEW GROWTH MEASUREMENTS FOR CALIBRATION.
!  8/17/2021 -LRD

IF(FN.LT.FNMIN .OR. .NOT.LDGCAL(ISPC)) GO TO 190

!----------
!  CALCULATE MEAN RESIDUAL FOR GROWTH SAMPLE AND MEAN PREDICTED DDS
!  FOR THE POPULATION AND SAMPLE.
!----------
BPOPX=SPOPX/SPOPN
BNX=SNX/SNP
BNY=SNY/SNP
!----------
!  CALCULATE CORRECTED SUMS OF SQUARES FOR RESIDUAL REGRESSION.
!----------
CSNXY=SNXY-BNX*BNY*SNP
CSNXX=SNXX-BNX*BNX*SNP
IF(CSNXX .LT. 0.)THEN
  WRITE(JOSTND,161)JSP(ISPC)
161   FORMAT(//'POOR SELECTION OF GROWTH SAMPLE TREES DETECTED ', &
     'FOR SPECIES ',A3,'. LARGE TREE DG CALIBRATION ABORTED FOR ', &
     'THIS SPECIES.')
  GO TO 190
ENDIF
!----------
!  CALCULATE RATIO AND REGRESSION ESTIMATORS.
!----------
SLOP=CSNXY/CSNXX
RATIO=BNY
!----------
!  CALCULATE DISTANCE BETWEEN MEAN PREDICTED VALUES FOR POPULATION AND
!  GROWTH SAMPLE WEIGHTED BY THE STANDARD DEVIATION OF THE GROWTH SAMPLE
!  PREDICTIONS (ADJUSTED SO THAT SUM OF PROB IS EQUAL TO NUMBER OF
!  GROWTH SAMPLE TREES).
!----------
SDPRED=SQRT(CSNXX/(SNP*(1.0-1.0/FN)))
DIST=ABS(BPOPX-BNX)/SDPRED
IF (DEBUG) WRITE(JOSTND,876) SLOP,RATIO,DIST
876 FORMAT('SLOP RATIO DIST=',3(2X,F10.2))
!----------
!  REGRESSION ESTIMATE OF CORRECTION FACTOR IS OBTAINED BY SOLVING
!  THE RESIDUAL REGRESSION AT THE MEAN PREDICTED DDS FOR THE POPULATION.
!----------
REGCOR=BNY+(BPOPX-BNX)*SLOP
!----------
!  ACTUAL CORRECTION FACTOR IS A WEIGHTED AVERAGE OF THE REGRESSION AND
!  RATIO ESTIMATORS WHERE THE WEIGHT IS DEPENDENT ON DIST.  FOR DIST
!  LESS THAN OR EQUAL TO 1, USE THE REGRESSION ESTIMATOR; FOR DIST
!  GREATER THAN 3, USE THE RATIO ESTIMATOR; FOR 1 < DIST < 3, USE
!  DIST/2*(RATIO ESTIMATE) +(1-DIST/2)*(REGRESSION ESTIMATE).
!----------
IF(DIST.GT.3.0) CORNEW=RATIO
IF(DIST.LE.1.0) CORNEW=REGCOR
IF(DIST.GT.1.0 .AND. DIST.LE.3.0) &
          CORNEW=RATIO*(DIST/2.0)+REGCOR*(1.0-DIST/2.0)
COR(ISPC)=CORNEW
!----------
!  LOAD INITIAL ERRORS FOR THE SERIAL CORRELATION CORRECTION (OLDRN)
!  FOR NON-GSTS FROM THE PREDICTED LN(DDS) VALUES OBTAINED BY SOLVING
!  THE CALIBRATION REGRESSION FOR THE PREDICTED DDS VALUE. TREES THAT
!  HAVE GREATER DBH THAN THE LARGEST TREE IN THE GROWTH SAMPLE ARE
!  ASSIGNED THE PREDICTED RESIDUAL FOR THE LARGEST TREE IN THE GROWTH
!  SAMPLE;  TREES WITH DBH LESS THAN THE SMALLEST TREE IN THE GROWTH
!  SAMPLE ARE ASSIGNED THE PREDICTED RESIDUAL FOR THE SMALLEST TREE IN
!  THE GROWTH SAMPLE. ASSIGNMENT IS BYPASSED IF RANDOM EFFECTS ARE
!  SUPPRESSED (DGSD < 1)
!----------
IF(DGSD.GE.1.0) THEN
  RX=BNY+(PX-BNX)*SLOP
  RN=BNY+(PN-BNX)*SLOP
  DO 170 I3=I1,I2
   I=IND1(I3)
   IF(OLDRN(I).NE.0.0) GO TO 170
   EDDS=EXP(WK2(I))
   OLDRN(I)=BNY+(EDDS-BNX)*SLOP
   IF(WK3(I).LT.DN) OLDRN(I)=RN
   IF(WK3(I).GT.DX) OLDRN(I)=RX
170   CONTINUE
ENDIF
!----------
!  CALIBRATE THE BAI MODEL VARIANCE.
!  USE A POOLED ESTIMATE OF THE VARIANCE CALCULATED FROM THE SAMPLE
!  AND FROM THE BASE MODELS.
!----------
SIGMR1=SIGMAR(ISPC)**2
SVAR=DEVSQ-(DEV*DEV)/FN
SIGMA(ISPC)=SQRT((SVAR+XNOB*SIGMR1)/(FN+XNOB))
SVAR=SVAR/(FN-1.0)
STDRAT(IREFI)=SQRT(SVAR/SIGMR1)
!----------
!  IF NOCALIB WAS SPECIFIED DO NOT SCALE THE CORRECTION TERMS.
!----------
IF(.NOT.LDGCAL(ISPC)) GO TO 194
CORI=COR(ISPC)
!----------
!  USE THE BASE MODEL VARIANCE AND ASSOCIATED DEGREES OF FREEDOM
!  TO DISCRIBE THE PRIOR DISTRIBUTION OF BASAL AREA INCREMENTS.
!
!  USE THIS PRIOR DISTRIBUTION WITH AN EMPIRICAL BAYES ESTIMATION TO
!  ADJUST THE CORRECTION TERMS.  THIS IS DONE FOR EACH SPECIES.
!----------
SVAR=SVAR/FN
PVAR=PSIGSQ(ISPC)
TEMP=CORI*CORI/PVAR
!----------
! THE CONSTRAINT HERE WAS 176.0 FROM THE ORIGINAL NI CODE. THIS CAUSES
! AN UNDERFLOW ERROR IF IMPOSED. CHANGED TO 72.0 8/31/04 BY GARY DIXON.
! THE VALUE OF E(-36) IS 2.3(-16) WHICH IS AROUND THE LIMIT THAT A
! NUMBER CAN BE DISTINGUISHED FROM 0.0 IN SINGLE PRECISION.
!----------
IF(TEMP.GT.72.0) TEMP=72.0
WC=1.0/(1.0+EXP(-0.5*TEMP)*SQRT(SVAR/PVAR))
COR(ISPC)= WC * CORI
IF (DEBUG) WRITE (JOSTND,9009) ISPC,COR(ISPC)
9009 FORMAT ('IN DGDRIV, ISPC=',I3,' COR=',E15.7)
GO TO 193
190 CONTINUE
!----------
!  FEWER THAN 5 GSTS; ASSIGN OLDRN WITH RANDOM NUMBER GENERATOR.
!  BYPASS ASSIGNMENT IF DGSD < 1.
!----------
IF(DGSD.LT.1) GO TO 193
DO 192 I3=I1,I2
I=IND1(I3)
191 CONTINUE
Z=BACHLO(0.0,SIGMA(ISPC),RANN)
IF(Z.GT.DGSD*SIGMA(ISPC)) GO TO 191
OLDRN(I)=Z
192 CONTINUE
193 CONTINUE
!----------
!  PREPARE AND PRINT THE CALIBRATION OUTPUT.
!----------
WCI(IREFI)= WC
CORTEM(IREFI)= EXP(COR(ISPC))
!----------
!  TRAP CALIBRATION VALUES OUTSIDE 2.5 STANDARD DEVIATIONS FROM THE
!  MEAN. IF C IS THE CALIBRATION TERM, WITH A DEFAULT OF 1.0, THEN
!  LN(C) HAS A MEAN OF 0.  -2.5 < LN(C) < 2.5 IMPLIES
!  0.0821 < C < 12.1825
!----------
IF(CORTEM(IREFI).LT.0.0821 .OR. CORTEM(IREFI).GT.12.1825) THEN
  CALL ERRGRO(.TRUE.,27)
  WRITE(JOSTND,9194)ISPC,JSP(ISPC),CORTEM(IREFI)
9194   FORMAT(T28,'LARGE TREE DG: SPECIES = ',I2,' (',A3, &
     ') CALCULATED CALIBRATION VALUE = ',F8.2)
  CORTEM(IREFI)=1.0
  COR(ISPC)=0.0
ENDIF
194 CONTINUE
NUMCAL(IREFI)= N
195 CONTINUE
!----------
!  COMPUTE VARIANCE OF ANNUAL DG AND STORE.
!----------
VTEMP=EXP(SIGMA(ISPC)**2)
VARDG(ISPC)=(VTEMP-1.0)*VTEMP/VMLT
!----------
!  LOAD RECORD TRIPLING GROWTH MULTIPLIERS.
!----------
FU(ISPC)=  1.271
FM(ISPC)=  -.14228
FL(ISPC)=  -1.549
!----------
!  END OF CALIBRATION LOOP
!----------
200 CONTINUE
!----------
!  LIMIT INITIAL VALUES OF OLDRN() TO + OR - DGSD*SIGMA, TO AVOID
!  AN INFINITE LOOP IN ** DGSCOR ** .   GED 12/18/96.
!----------
DO 202 I=1,ITRN
ISPC=ISP(I)
IF(OLDRN(I) .GT.  DGSD*SIGMA(ISPC)) OLDRN(I)= DGSD*SIGMA(ISPC)
IF(OLDRN(I) .LT. -DGSD*SIGMA(ISPC)) OLDRN(I)=-DGSD*SIGMA(ISPC)
202 CONTINUE
IF(IFINT.EQ.0) GO TO 210
IF (ITRN.EQ.0) RETURN
!
WRITE(JOSTND,9004)(NUMCAL(I),I=1,NUMSP)
9004 FORMAT (/,'NUMBER OF RECORDS AVAILABLE FOR SCALING'/ &
          'THE DIAMETER INCREMENT MODEL',((T48,11(I5,1X)/)))
!
WRITE (JOSTND,9008) (STDRAT(I),I=1,NUMSP)
9008 FORMAT(/'RATIO OF STANDARD ERRORS'/ &
          '(INPUT DBH GROWTH DATA : MODEL)',((T48,11(F5.2,1X)/)))
!
WRITE (JOSTND,9005) (WCI(I), I=1, NUMSP)
9005 FORMAT (/,'WEIGHT GIVEN TO THE INPUT GROWTH DATA WHEN'/ &
           'DBH GROWTH MODEL SCALE FACTORS WERE COMPUTED', &
           ((T48,11(F5.2,1X)/)))
!
WRITE(JOSTND,9006) (CORTEM(I), I= 1, NUMSP)
9006 FORMAT (/'INITIAL SCALE FACTORS FOR THE'/ &
          'DBH INCREMENT MODEL',((T48,11(F5.2,1X)/)))
!----------
! OUTPUT CALIBRATION TO DATABASE.
!----------
CALL DBSCALIB(1,CORTEM,NUMCAL,STDRAT)
!----------
! OUTPUT CALIBRATION TERMS IF CALBSTAT KEYWORD WAS PRESENT.
!----------
IF(JOCALB .GT. 0) THEN
  KOUT=0
  DO 207 K=1,MAXSP
  IF(CORTEM(K).NE.1.0 .OR. NUMCAL(K).GE.FNMIN) THEN
    SPEC=NSP(MAXSP,1)(1:2)
    ISPEC=MAXSP
    DO 203 KK=1,MAXSP
    IF(K .NE. IREF(KK)) GO TO 203
    ISPEC=KK
    SPEC=NSP(KK,1)(1:2)
    GO TO 2031
203     CONTINUE
2031     WRITE(JOCALB,204)ISPEC,SPEC,NUMCAL(K),CORTEM(K),STDRAT(K), &
       WCI(K)
204     FORMAT(' CAL: LD',1X,I2,1X,A2,1X,I4,3(1X,F6.3))
    KOUT=KOUT+1
  ENDIF
207   CONTINUE
  IF(KOUT .EQ. 0) WRITE(JOCALB,209)
209   FORMAT(' NO LD VALUES COMPUTED')
ENDIF
GO TO 215
210 CONTINUE
WRITE(JOSTND,9007)
9007 FORMAT(/,'NO CORRECTION TERMS ARE CALCULATED WHEN GROWTH', &
           ' MEASUREMENT PERIOD IS ZERO.')
!----------
!  DUB IN DBH INCREMENT FOR TREES ON WHICH IT WAS NOT MEASURED.
!----------
215 CONTINUE
CALL DGF(WK3)
!
!     SET ICL6 AND SCALE
!
ICL6=IFINT
SCALE=1./SCALE
!----------
!  CHECK FOR A VALID ESTIMATE OF DIAMETER INCREMENT.  COMPUTE A NEW
!  INCREMENT IF INPUT VALUE IS MISSING; IF INPUT INCREMENT EXCEEDS
!  CURRENT DIAMETER, REPLACE AND PRINT WARNING MESSAGE.
!----------
DO 220 I = 1,ITRN
ISPC=ISP(I)
BARK=BRATIO(ISPC,DBH(I),HT(I))
D=WK3(I)*BARK
IF(DG(I).GT.0.0.AND.HT(I).GT.4.5) THEN
  IF(IDG.LT.2 .AND. DG(I).GT.DBH(I)*BARK) THEN
    WRITE(JOSTND,9012) I,DG(I),DBH(I)*BARK
9012     FORMAT(/'NOTE: FOR TREE ',I4,' DG (',F5.2, &
               ') IS GREATER THAN IB DBH (',F5.2,')'/ &
               '      DG RESET TO INSIDE BARK DBH.')
    DG(I)=DBH(I)*BARK
  ENDIF
  WORK1(I)=DG(I)
ELSE
  IF(HT(I).LE.4.5) THEN
    DG(I)=0.0
  ELSE
    DG(I)=SQRT(D*D+EXP((WK2(I)+OLDRN(I)))*SCALE)-D
    IF(DG(I) .GT. D) DG(I)=D
!----------
!   CALL DGBND TO SET BOUNDS ON VALUE OF DG.
!----------
    CALL DGBND (ISPC,DBH(I),DG(I))
  ENDIF
  WORK1(I)=0.
ENDIF
220 CONTINUE
RETURN
END
