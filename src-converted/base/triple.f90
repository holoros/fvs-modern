SUBROUTINE TRIPLE
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!  **TRIPLE** PARTITIONS EACH TREE RECORD INTO THREE RECORDS.
!  EACH OF THE TRIPLES HAS BEEN ASSIGNED A DIAMETER GROWTH IN
!  **DGDRIV** (TREES WITH LESS THAN 4.5 INCH DBH ARE ASSIGNED
!  DIAMETER GROWTH IN REGENT).  DBH IS ALSO TRIPPLED IN **DGDRIV**.
!  NOW, OTHER TREE ATTRIBUTES ARE COPIED TO THE TRIPLES.
!----------
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
INCLUDE 'ESTREE.f90'
!
!
INCLUDE 'STDSTK.f90'
!
!OMMONS
!----------
!
INTEGER I,ITFN,IDMR
REAL WEIGHT
!
!----------
! LOOP THROUGH ALL TREE RECORDS
!----------
DO 30 I=1,ITRN
!----------
!  THE WEIGHT FOR THE SECOND TRIPLE IS 0.25.
!----------
WEIGHT=0.25
ITFN=ITRN+2*I-1
!----------
!  ADJUST THE VISULIZATION FOR RECORD TRIPLING
!----------
CALL SVTRIP (I,ITFN)
!----------
!  RETURN HERE FOR THIRD TRIPLE.
!----------
10 CONTINUE
BFV(ITFN)=BFV(I)
CFV(ITFN)=CFV(I)
MCFV(ITFN)=MCFV(I)
SCFV(ITFN)=SCFV(I)
PTOCFV(ITFN)=PTOCFV(I)
PMRBFV(ITFN)=PMRBFV(I)
PMRCFV(ITFN)=PMRCFV(I)
PSCFV(ITFN)=PSCFV(I)
PDBH(ITFN)=PDBH(I)
PHT(ITFN)=PHT(I)
NCFDEF(ITFN)=NCFDEF(I)
NBFDEF(ITFN)=NBFDEF(I)
HT(ITFN)=HT(I)
OLDPCT(ITFN)=OLDPCT(I)
PCT(ITFN)=PCT(I)
PROB(ITFN)=PROB(I)*WEIGHT
WK1(ITFN)=WK1(I)
WK2(ITFN)=WK2(I)*WEIGHT
WK3(ITFN)=WK3(I)
ICR(ITFN)=ICR(I)
IMC(ITFN)=IMC(I)
ISP(ITFN)=ISP(I)
ITRE(ITFN)=ITRE(I)
ITRUNC(ITFN)=ITRUNC(I)
NORMHT(ITFN)=NORMHT(I)
KUTKOD(ITFN)=KUTKOD(I)
DEFECT(ITFN)=DEFECT(I)
ISPECL(ITFN)=ISPECL(I)
IESTAT(ITFN)=IESTAT(I)
ABIRTH(ITFN)=ABIRTH(I)
LBIRTH(ITFN)=LBIRTH(I)
IDTREE(ITFN)=IDTREE(I)
ZRAND(ITFN)=ZRAND(I)
CRWDTH(ITFN)=CRWDTH(I)
HT2TD(ITFN,1)=HT2TD(I,1)
HT2TD(ITFN,2)=HT2TD(I,2)
CULL(ITFN) = CULL(I)
DECAYCD(ITFN) = DECAYCD(I)
WDLDSTEM(ITFN) = WDLDSTEM(I)
ABVGRD_BIO(ITFN) = ABVGRD_BIO(I)
MERCH_BIO(ITFN) = MERCH_BIO(I)
CUBSAW_BIO(ITFN) = CUBSAW_BIO(I)
FOLI_BIO(ITRN) = FOLI_BIO(I)
ABVGRD_CARB(ITRN) = ABVGRD_CARB(I)
MERCH_CARB(ITRN) = MERCH_CARB(I)
CUBSAW_CARB(ITRN) = CUBSAW_CARB(I)
FOLI_CARB(ITRN) = FOLI_CARB(I)
CARB_FRAC(ITRN) = CARB_FRAC(I)
!----------
!  START ORGANON
!
CALL ORGTRIP(I,ITFN)
!
!  END ORGANON
!----------
!----------
!     GET MISTLETOE RATING FROM POSITION I AND PUT IT AT POSITION ITFN.
!
CALL MISGET(I,IDMR)
CALL MISPUT(ITFN,IDMR)
!----------
CALL RDTRIP (ITFN,I,WEIGHT)
CALL BRTRIP (ITFN,I,WEIGHT)
CALL BMTRIP (ITFN,I,WEIGHT)
CALL FMTRIP (ITFN,I,WEIGHT)
IF(WEIGHT.LT.0.2) GO TO 20
!----------
!  REASSIGN WEIGHT AND ITFN FOR THIRD TRIPLE.
!----------
WEIGHT=0.15
ITFN=ITFN+1
GO TO 10
!----------
!  ALL ATTRIBUTES FOR EACH TRIPLE HAVE BEEN ASSIGNED.  ADJUST
!  THE PROB AND WK2 (MORTALITY) FOR THE ORIGINAL RECORD.
!----------
20 CONTINUE
WK2(I) = WK2(I)*.6
PROB(I)=PROB(I)*0.60
!----------
!     CALL WESTERN ROOT DISEASE MODEL VER. 3.0 SUBROUTINE TO TRIPLE
!     THE WESTERN ROOT DISEASE TREELIST
!----------
CALL RDTRIP (ITFN,I,0.6)
!----------
!     CALL BLISTER RUST SUBROUTINE TO TRIPLE BLISTER RUST TREELIST.
!----------
CALL BRTRIP (ITFN,I,0.6)
!----------
!     CALL BEETLE MODEL TO TRIPLE BEETLE MODEL PARAMETERS
!----------
CALL BMTRIP (ITFN,I,0.6)
!----------
!     CALL FIRE MODEL TO TRIPLE ITS PARAMETERS
!----------
CALL FMTRIP (ITFN,I,0.6)
30 CONTINUE
!----------
!  UPDATE MULTIPLIER WHICH INFLATES PROB FOR SAMPLE TREE DISPLAY.
!----------
TRM=TRM*0.6
RETURN
END
