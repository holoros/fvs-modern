SUBROUTINE SICHG(ISISP,SSITE,SIAGE)
IMPLICIT NONE
!----------
! EC $Id$
!----------
!  THIS ROUTINE TAKES THE SITE FROM THE STDINFO CARD AND/OR
!  THE DEFAULT VALUE FROM SITSET, AND DETERMINES IF IT IS A
!  BHA OR TTA SITE CURVE.  THE RETURN VARIABLE SIAGE REPRESENTS THE
!  SPECIES BASE AGE + AGE2BH IF THE SITE SPECIES SITE CURVE
!  IS A TTA CURVE AND SPECIES CURVE A BHA CURVE, AND SO ON.
!  THEN, THE SITE FOR EACH SPECIES MAY BE ESTIMATED USING
!  THE SITE SPECIES CURVE IN HTCALC.
!  DATA STATEMENTS FOR A AND B CONTAIN COEFFICIENTS FOR A LINEAR
!  RELATION OF AGE TO BREAST HEIGHT AS A FUNCTION OF SITE INDEX.
!  THE LINEAR RELATION IS AN ESTIMATE FROM THE LOWER END POINT OF
!  A HEIGHT GROWTH CURVE TO THE ORIGIN (0 AGE; 0 TOT TREE HT.)
!  THESE COEFFICIENTS SHOULD BE COMPATIBLE WITH SIMILAR VALUES USED
!  IN THE SMHTGF SUBROUTINE.
!
!  CALLED FROM **SITSET
!----------
!OMMONS
!
INCLUDE 'PRGPRM.f90'
!
!OMMONS
!----------
CHARACTER*1 ISILOC,REFLOC(MAXSP)
INTEGER DIFF,ISISP,I
INTEGER REFAGE(MAXSP)
REAL SSITE,AGE2BH
REAL A(MAXSP),B(MAXSP),SIAGE(MAXSP)
!----------
!  SPECIES LIST FOR EAST CASCADES VARIANT.
!
!   1 = WESTERN WHITE PINE      (WP)    PINUS MONTICOLA
!   2 = WESTERN LARCH           (WL)    LARIX OCCIDENTALIS
!   3 = DOUGLAS-FIR             (DF)    PSEUDOTSUGA MENZIESII
!   4 = PACIFIC SILVER FIR      (SF)    ABIES AMABILIS
!   5 = WESTERN REDCEDAR        (RC)    THUJA PLICATA
!   6 = GRAND FIR               (GF)    ABIES GRANDIS
!   7 = LODGEPOLE PINE          (LP)    PINUS CONTORTA
!   8 = ENGELMANN SPRUCE        (ES)    PICEA ENGELMANNII
!   9 = SUBALPINE FIR           (AF)    ABIES LASIOCARPA
!  10 = PONDEROSA PINE          (PP)    PINUS PONDEROSA
!  11 = WESTERN HEMLOCK         (WH)    TSUGA HETEROPHYLLA
!  12 = MOUNTAIN HEMLOCK        (MH)    TSUGA MERTENSIANA
!  13 = PACIFIC YEW             (PY)    TAXUS BREVIFOLIA
!  14 = WHITEBARK PINE          (WB)    PINUS ALBICAULIS
!  15 = NOBLE FIR               (NF)    ABIES PROCERA
!  16 = WHITE FIR               (WF)    ABIES CONCOLOR
!  17 = SUBALPINE LARCH         (LL)    LARIX LYALLII
!  18 = ALASKA CEDAR            (YC)    CALLITROPSIS NOOTKATENSIS
!  19 = WESTERN JUNIPER         (WJ)    JUNIPERUS OCCIDENTALIS
!  20 = BIGLEAF MAPLE           (BM)    ACER MACROPHYLLUM
!  21 = VINE MAPLE              (VN)    ACER CIRCINATUM
!  22 = RED ALDER               (RA)    ALNUS RUBRA
!  23 = PAPER BIRCH             (PB)    BETULA PAPYRIFERA
!  24 = GIANT CHINQUAPIN        (GC)    CHRYSOLEPIS CHRYSOPHYLLA
!  25 = PACIFIC DOGWOOD         (DG)    CORNUS NUTTALLII
!  26 = QUAKING ASPEN           (AS)    POPULUS TREMULOIDES
!  27 = BLACK COTTONWOOD        (CW)    POPULUS BALSAMIFERA var. TRICHOCARPA
!  28 = OREGON WHITE OAK        (WO)    QUERCUS GARRYANA
!  29 = CHERRY AND PLUM SPECIES (PL)    PRUNUS sp.
!  30 = WILLOW SPECIES          (WI)    SALIX sp.
!  31 = OTHER SOFTWOODS         (OS)
!  32 = OTHER HARDWOODS         (OH)
!
!  SURROGATE EQUATION ASSIGNMENT:
!
!  FROM THE EC VARIANT:
!      USE 6(GF) FOR 16(WF)
!      USE OLD 11(OT) FOR NEW 12(MH) AND 31(OS)
!
!  FROM THE WC VARIANT:
!      USE 19(WH) FOR 11(WH)
!      USE 33(PY) FOR 13(PY)
!      USE 31(WB) FOR 14(WB)
!      USE  7(NF) FOR 15(NF)
!      USE 30(LL) FOR 17(LL)
!      USE  8(YC) FOR 18(YC)
!      USE 29(WJ) FOR 19(WJ)
!      USE 21(BM) FOR 20(BM) AND 21(VN)
!      USE 22(RA) FOR 22(RA)
!      USE 24(PB) FOR 23(PB)
!      USE 25(GC) FOR 24(GC)
!      USE 34(DG) FOR 25(DG)
!      USE 26(AS) FOR 26(AS) AND 32(OH)
!      USE 27(CW) FOR 27(CW)
!      USE 28(WO) FOR 28(WO)
!      USE 36(CH) FOR 29(PL)
!      USE 37(WI) FOR 30(WI)
!----------
!  DATA STATEMENTS
!----------
DATA A/ &
    18.43316,  8.11680,  8.50000,  9.01840, 24.62857, &
     9.01840,  7.78447, 33.72545, 13.88200,  8.00000, &
     6.15767, 45.24969, 11.56252, 11.56252, 7.938059, &
     9.01840,  8.11668, 11.56252, 11.56252, 11.56252, &
    11.56252,  3.28241, 11.56252, 11.56252, 11.56252, &
    11.56252, 11.56252,  4.94166, 11.56252, 11.56252, &
    45.24960, 11.56252/
!
DATA B/ &
    -0.13399, -0.05661, -0.05000, -0.05700, -0.20465, &
    -0.05700, -0.05013,-0.274509, -0.06590, -0.04286, &
    -0.03596, -1.28885, -0.05586, -0.05586, -0.02873, &
    -0.05700, -0.05661, -0.05586, -0.05586, -0.05586, &
    -0.05586, -0.02987, -0.05586, -0.05586, -0.05586, &
    -0.05586, -0.05586, -0.02419, -0.05586, -0.05586, &
    -1.28885, -0.05586/
!
DATA REFLOC/ &
     'T', 'B', 'B', 'B', 'T', 'B', 'T', 'B', 'B', 'B', &
     'B', 'B', 'B', 'B', 'B', 'B', 'B', 'B', 'B', 'B', &
     'B', 'T', 'B', 'B', 'B', 'B', 'B', 'B', 'B', 'B', &
     'B', 'B'/
!
DATA REFAGE/ &
      50,  50,  50,  50, 100,  50, 100, 100, 100, 100, &
      50, 100, 100, 100, 100,  50,  50, 100, 100, 100, &
     100,  20, 100, 100, 100, 100, 100,  50, 100, 100, &
     100, 100/
!----------
! ISILOC IS THE PLACE THE AGE FOR THE SITE SPECIES.
!----------
ISILOC = REFLOC(ISISP)
DO 100 I = 1,MAXSP
!----------
!  SET UP THE ARRAY TO TELL WHETHER YOU NEED TO SLIDE UP OR DOWN THE SIT
!  LINE TO ADJUST FOR TOTAL AGE OR BREAST HIGH AGE
!----------
IF(ISILOC .EQ. 'T' .AND. REFLOC(I) .EQ. 'B')DIFF=-1
IF(ISILOC .EQ. REFLOC(I))DIFF=0
IF(ISILOC .EQ. 'B' .AND. REFLOC(I) .EQ. 'T')DIFF=1
AGE2BH=0.0
IF(DIFF .LT. 0 .OR. DIFF .GT. 0)THEN
  AGE2BH=A(I) + B(I)*SSITE
  IF((ISISP .NE. 31) .AND. (I .EQ. 31)) &
       AGE2BH=A(I) + B(I)*(SSITE/3.281)
  IF((ISISP .EQ. 31) .AND. (I .NE. 31)) &
       AGE2BH=A(I) + B(I)*(SSITE*3.281)
  IF((ISISP .NE. 12) .AND. (I .EQ. 12)) &
       AGE2BH=A(I) + B(I)*(SSITE/3.281)
  IF((ISISP .EQ. 12) .AND. (I .NE. 12)) &
       AGE2BH=A(I) + B(I)*(SSITE*3.281)
END IF
SIAGE(I) = REFAGE(I) + AGE2BH*DIFF
!
100 CONTINUE
!
RETURN
END
