  RDANUW = GROSPC
  RDANUW = PREM2
  IDANUW = IS
  IDANUW = IT
  IDANUW = ICYC
  IDANUW = IY(1)
RETURN
!----------
! ENTRY ECCALC CALLED FROM GRADD
!----------
ENTRY ECCALC(IARRAY,II,CARRAY, CH1_4, NPLT, ITITLE)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  IDANUW = IARRAY(1)
  IDANUW = II
  CDANUW = CARRAY(1)(1:1)
  CDANUW = CH1_4(1:1)
  CDANUW = NPLT(1:1)
  CDANUW = ITITLE(1:1)
RETURN
!----------
! ENTRY ECLOAD CALLED FROM CUTS
!----------
ENTRY GETISPRETENDACTIVE(LPRTND)
