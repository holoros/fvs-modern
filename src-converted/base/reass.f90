SUBROUTINE REASS
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!  **REASS** REALLIGNS THE POINTERS VECTORS IND1 AND IND, AND
!  THE ARRAY ISCT FOLLOWING RECORD TRIPLING.  THE VECTOR IND2
!  IS USED FOR WORK SPACE.
!----------
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
INTEGER J,ISPC,I1,I2,I,K,ITFN,LAST,IKNT
!
!
!OMMONS
!----------
!  COPY IND1 INTO IND2 IN SPECIES ORDER.
!----------
J=0
DO 20 ISPC=1,MAXSP
I1=ISCT(ISPC,1)
IF(I1.EQ.0) GO TO 20
I2=ISCT(ISPC,2)
DO 10 I=I1,I2
J=J+1
IND2(J)=IND1(I)
10 CONTINUE
20 CONTINUE
!----------
!  REALLIGN THE POINTERS IN IND1.
!----------
J=1
DO 30 K=1,ITRN,3
I=IND2(J)
ITFN=IREC1+2*I-1
IND1(K)=ITFN
IND1(K+1)=I
IND1(K+2)=ITFN+1
J=J+1
30 CONTINUE
!----------
!  REALLIGN POINTERS TO THE SPECIES SEGMENTS IN IND1.
!----------
LAST=0
DO 40 ISPC=1,MAXSP
IF(ISCT(ISPC,1).EQ.0) GO TO 40
IKNT=ISCT(ISPC,2)-ISCT(ISPC,1)+1
ISCT(ISPC,1)=LAST+1
LAST=LAST+IKNT*3
ISCT(ISPC,2)=LAST
40 CONTINUE
!----------
!  COPY IND INTO IND2.
!----------
DO 50 I=1,IREC1
IND2(I)=IND(I)
50 CONTINUE
!----------
!  REALLIGN POINTERS IN IND.
!----------
J=1
DO 60 K=1,ITRN,3
I=IND2(J)
ITFN=IREC1+2*I-1
IND(K)=ITFN
IND(K+1)=I
IND(K+2)=ITFN+1
J=J+1
60 CONTINUE
RETURN
END
