!ODE SEGMENT SSTGMC
!----------
! COMMON $Id$
!----------
!     COMMON AREA FOR THE STAND STRUCTURAL STAGE ROUTINE.
!----------
!
LOGICAL LCALC,LPRNT
!
INTEGER IRREF,ISTRCL
!
REAL    CCMIN,DBHDOM,GAPPCT,PCTSMX,SAWDBH,SSDBH,TPAMIN
!
REAL    OSTRST(33,2)
!
COMMON /SSTGMC/ CCMIN,DBHDOM,GAPPCT,IRREF,ISTRCL,LCALC,LPRNT, &
                   OSTRST,PCTSMX,SAWDBH,SSDBH,TPAMIN
!
!----------
!  VARIABLE DEFINITIONS:
!----------
!     CCMIN = THE MINIMUM COVER PERCENT TO QUALIFY A POTENTIAL STRATUM.
!    DBHDOM = AVERAGE DBH OF THE 9 TREES NEAREST THE
!             70TH PERCENTILE TREE IN THE DOMINATE STRATUM.
!    GAPPCT = THE PERCENTAGE OF A TREES HEIGHT THAT A GAP MUST EXCEED.
!     IRREF = REPORT REFERENCE NUMBER (FOR GENRPT FACILITY).
!    ISTRCL = STRUCTURAL CLASS
!     LCALC = TRUE IF STAGES ARE BEING COMPUTED (REQUESTED BY
!             KEYWORD).
!     LPRNT = TRUE IF STAGES ARE BEING PRINTED.
!    OSTRST = ARRAY HOLDING ALL THE VALUES IN THE STRUCTURE
!             STATISTICS OUTPUT.  STORED BY ELEMENT (33) AND BY
!             BEFORE OR AFTER THINNING (1 OR 2).  ATTRIBUTES SET
!             AND LISTED IN SSTAGE.F
!    PCTSMX = THE MINIMUM COVER PERCENT TO QUALIFY A POTENTIAL STRATUM.
!    SAWDBH = THE DBH BREAK BETWEEN POLE/SMALL+SAWTIMBER
!             AND LARGE SAWTIMBER.
!     SSDBH = THE DBH BREAK BETWEEN SEEDLING/SAPLINGS AND
!             POLE/SMALL+SAWTIMBER.
!    TPAMIN = THE MINIMUM TREES/ACRE THAT MUST BE EXCEEDED FOR A STAND
!             THAT HAS LESS THAN 5% COVER TO BE CLASSIFIED STAND INIT.
!
!-----END SEGMENT
