SUBROUTINE MBACAL
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!   THIS SUBROUTINE IDENTIFIES THE SITE SPECIES (IE SPECIES OF
!   MAXIMUM BASAL AREA IN THE STAND. THIS ROUTINE IS CALLED FROM
!   CRATET AND DENSE.
!----------
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'COEFFS.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
INCLUDE 'PLOT.f90'
!
!
INCLUDE 'HTCAL.f90'
!
!
INCLUDE 'PDEN.f90'
!
!
INCLUDE 'ESHAP.f90'
!
!
!OMMONS
!
INTEGER ISPC,II,I,MDX
REAL P,XMAX
!
!----------
!     FIND SITE SPECIES. IF SITE SPECIES HAS BEEN ENTERED ON KEYWORD
!     THEN BYPASS THE CALCULATION BASED ON THE TREELIST.
!---------
IF(LSITE) RETURN
IF (ITRN .LE. 0 .OR. ISISP .GT. 0) RETURN
DO 20 ISPC=1,MAXSP
IF (ISCT(ISPC,1).EQ.0) GOTO 20
DO 10 II=ISCT(ISPC,1),ISCT(ISPC,2)
I=IND1(II)
!----------
!     SKIP DEAD TREES.
!---------
IF (I.GE.IREC2) GOTO 10
P=PROB(I)
BARANK(ISPC)=.005454154*P*DBH(I)*DBH(I)+BARANK(ISPC)
10 CONTINUE
20 CONTINUE
MDX=0
XMAX=0.
DO 30 I=1,MAXSP
IF(XMAX .GT. BARANK(I)) GOTO 30
MDX=I
XMAX=BARANK(I)
30 CONTINUE
ISISP=MDX
RETURN
END
