INTEGER    MAXACT,MAXCOD,MAXEVA,MAXEVT,MAXPRM,MXCACT,MXEXPR, &
              MXLREG,MXPTDO, &
              MXTST1,MXTST2,MXTST3,MXTST4,MXTST5,MXXREG
!
PARAMETER (MAXACT=4000,MAXCOD=6000,MAXEVA=160,MAXEVT=150, &
              MAXPRM=7999,MXCACT=10000,MXEXPR=2000,MXLREG=400, &
              MXPTDO=500, &
              MXTST1=80,MXTST2=50,MXTST3=50,MXTST4=50,MXTST5=399, &
              MXXREG=400)
!
CHARACTER*250 SLSET,WKSTR1,WKSTR2,WKSTR3
!
!
CHARACTER*1 CACT(MXCACT)
CHARACTER*8 CTSTV5(MXTST5)
CHARACTER*250 AGLSET(MAXEVA)
!
CHARACTER   CEXPRS(MXEXPR)
!
COMMON / OPCHR / AGLSET,CACT,CEXPRS,CTSTV5,SLSET,WKSTR1,WKSTR2, &
                    WKSTR3
!
LOGICAL LBSETS,LEVUSE,LOPEVN
!
LOGICAL LREG(MXLREG),LTSTV4(MXTST4),LTSTV5(MXTST5)
!
INTEGER ICACT,ICOD,IEPT,IEVA,IEVT,ILGNUM,IMG1,IMG2,IMGL,IMPL, &
           IPHASE,ISEQDN,ITOPRM,ITST5,KTODO,LENSLS
!
INTEGER IACT(MAXACT,5),IDATE(MAXACT),IEVACT(MAXEVA,6), &
           IEVCOD(MAXCOD),IEVNTS(MAXEVT,3),IMGPTS(MAXCYC,2), &
           IOPCYC(MAXACT),IOPSRT(MAXACT),IPTODO(MXPTDO), &
           ISEQ(MAXACT),LENAGL(MAXEVA)
!
REAL    ACCFSP(MAXSP),BCCFSP(MAXSP),PARMS(MAXPRM),TSTV1(MXTST1), &
           TSTV2(MXTST2),TSTV3(MXTST3),TSTV4(MXTST4),TSTV5(MXTST5), &
           XREG(MXXREG)
!
COMMON / OPCOM / ACCFSP,BCCFSP,IACT,ICACT,ICOD,IDATE,IEPT,IEVA, &
                    IEVACT,IEVCOD,IEVNTS,IEVT,ILGNUM,IMG1,IMG2,IMGL, &
                    IMGPTS,IMPL,IOPCYC,IOPSRT,IPHASE,IPTODO,ISEQ, &
                    ISEQDN,ITOPRM,ITST5,KTODO,LBSETS,LENAGL,LENSLS, &
                    LEVUSE,LOPEVN,LREG,LTSTV4,LTSTV5,PARMS, &
                    TSTV1,TSTV2,TSTV3,TSTV4,TSTV5,XREG
!
!----------
!  VARIABLE DEFINITIONS:
!----------
!     ACCFSP= AFTER THIN CCF
!     AGLSET= THE ACTIVITY GROUP LABEL SET FOR EACH ACTIVITY GROUP
!     BCCFSP= BEFORE THIN CCF
!     CEXPRS= STRING HOLDING A EXPRESSION TO BE COMPILED.
!     CTSTV5= THE CHARACTER STRING REPRESENTATION OF THE VARIABLES.
!     IACT  = A TABLE CONTAINING THE ACTIVITIES, EACH ROW HOLDS ONE
!             ACTIVITY, THE COLUMNS ARE AS FOLLOWS:
!             1) CONTAINS THE ACTIVITIY NUMBER.
!             2) IF POSITIVE, THIS WORD POINTS TO THE FIRST ASSOCIATED
!                PARAMETER IN PARMS.  IF NEGATIVE, THE ABS OF THIS
!                WORD POINTS TO THE FIRST OPCODE IN IEVCOD NEEDED TO
!                EVALUATE THE PARAMETER EXPRESSION ASSOCIATED WITH
!                THE ACTIVITY.
!             3) POINTS TO THE LAST ASSOCIATED PARAMETER.
!             4) CONTAINS THE ACTIVITY STATUS CODE, WHERE:
!                0=ACTIVITY NOT DONE.
!                -1=ACTIVITY DELETED OR CANCELED.
!                >0=ACTIVITY COMPLETED ON THE DATE STORED IN THIS WORD.
!             5) POINTS TO THE FIRST CHARACTER IS THE CHARACTER VECTOR
!                ASSOCIATED WITH THE ACTIVITIY, ZERO IF NONE STORED.
!     ICACT = CURRENT AVAILABLE PLACE IN CACT
!     ICOD  = THE NEXT AVAILABLE LOCATION IN IEVCOD.
!     IDATE = THE SCHEDULED DATE.
!     IEPT  = POINTS TO THE NEXT AVAILABLE ROW IN IACT COUNTING FROM
!             THE BOTTOM UP; DECREMENTED WHEN LOPEVN IS TRUE.
!     IEVA  = NEXT AVAILABLE ROW IN IEVACT.
!     IEVACT= EACH ROW CORRESPONDS TO A GROUP OF ACTIVITIES ASSOCIATED
!             WITH ONE IF-THEN OR ALSOTRY AND ONE EVENT.  IN OTHERWORDS,
!             EACH EVENT MAY HAVE MORE THAN ONE ASSOCIATED GROUP OF
!             ACTIVITIES.  THE COLUMNS ARE DEFINED AS FOLLOWS:
!             1) THE INTERNAL EVENT NUMBER (= ROW NUMBER IN IEVNTS).
!             2) SCHEDULING STATUS CODE, WHERE:
!                -1= ATTEMPT TO SCHEDULE FAILED.
!                0= EVENT HAS NOT OCCURED, DO NOT SCHEDULE ACTIVITIES.
!                1= EVENT HAS OCCURED, BUT ACTIVITIES HAVE BEEN
!                   SCHEDULED (BLOCKED BY LABEL PROCESSING).
!                2= EVENT HAS OCCURED AND ACTIVITIES HAVE BEEN
!                   SCHEDULED.
!             3) BRANCH LEG NUMBER ASSOCIATED WITH THE NODE, APPLICABLE
!                ONLY WHEN ALSOTRY WAS SPECIFIED, USEFUL ONLY WITH
!                THE PARALLEL PROCESSING EXTENSION DECISION TREE
!                ANALYSIS AND BRANCHING FACILITY.
!             4) POINTS TO ROW IN BOTTOM OF IACT WHICH CONTAINS THE
!                FIRST ACTIVITIY TO BE SCHEDULED.
!             5) POINTS TO LAST ACTIVITIY TO BE SCHEDULED.
!             6) CONTROLS HOW THE BRANCH WEIGHTING FACTOR IN THE PPE
!                IS MODIFIED WHEN THE EVENT OCCURS (USEFUL FOR DECISION
!                TREE ANALYSIS).
!                0= NO MODIFICATION
!                >0=POINTS TO A CONSTANT MULT STORED IN PARMS.
!                <0=POINTS TO CODE IN IEVCOD FOR COMPUTING THE MULT.
!     IEVCOD= HOLDS THE OPERATION CODES...SEE ALGCMP FOR DESCRIPTION.
!     IEVNTS= EACH ROW HOLDS THE FOLLOWING:
!             1) POINTS TO THE FIRST POSITION IN IEVCOD APPLICABLE TO
!                THE EVENT.
!             2) STORES THE DATE THE ACTIVITY OCCURED, -1 OTHERWISE.
!             3) STORES THE USER-SPECIFIED WAITING TIME BEFORE THE
!                EVENT MAY BECOME TRUE AGAIN, -1 MEANS THAT THE EVENT
!                CAN NOT EVER BE EVALUATED AGAIN. (DELETE EVENT).
!     IEVT  = THE NEXT AVAILABLE ROW IN IEVNTS.
!     ILGNUM= THE LEG NUMBER COUNTER. INCREMENTED WHEN THEN OR ALSOTRY
!             IS SPECIFIED, SET TO ZERO IN GRINIT AND WHEN ENDIF IS
!             ENTERED.
!     IMG1  = POINTS TO THE FIRST POSITION IN IOPSRT WHERE POINTERS
!             TO ACTIVITIES SCHEDULED FOR THE CURRENT CYCLE RESIDE.
!     IMG2  = POINTS TO THE LAST POSITION IN IOPSRT, SEE IMG1.
!     IMGL  = POINTS TO THE NEXT AVAILABLE ROW IN IACT COUNTING FROM
!             THE TOP DOWN; INCREMENTED WHEN LOPEVN IS FALSE.
!     IMGPTS= DIMENSIONED CYCLE BY 2: THE FIRST ROW HOLDS
!             POINTERS TO ACTIVITIES SCHEDULED FOR THE FIRST CYCLE,
!             AND THE SUBSEQUENT ROWS HOLD POINTERS TO ACTIVITIES
!             SCHEDULED FOR SUBSEQUENT CYCLES.  THE VALUES OF IMG1 AND
!             IMG2 ARE LOADED FROM THE APPLICABLE ROW OF IMGPTS.
!             SEE IMG1 AND IMG2.
!     IMPL  = THE NEXT AVAILABLE LOCATION IN PARMS (BOTTOM TO TOP).
!     IMPL  = THE NEXT AVAILABLE LOCATION IN PARMS (TOP TO BOTTOM).
!     IOPCYC= POINTS TO ACTIVITIES THAT ARE ALL SCHEDULED FOR A
!             GIVEN CYCLE IN ASCENDING ORDER.
!     IOPSRT= ACTIVITY SORTING ARRAY USUALLY USED TO SORT ACTIVITIES
!             BY ASSENDING DATES.
!     IPTODO= POINTS TO ACTIVITIES THAT ARE ALL SCHEDULED FOR A
!             GIVEN CYCLE BY A GIVEN ROUTINE IN ASCENDING ORDER.
!     LOPEVN= TRUE IF THE ACTIVITIES BEING ENTERED BY THE USER
!             FOLLOW AN IF-THEN OR ALSOTRY; THUS THEY ARE BEING STORED
!             FOR SCHEDULING AFTER AN EVENT HAPPENS.
!     IPHASE= PROJECTION PROCESSING PHASE. 1=START OF CYCLE BEFORE
!             CUTS, 2=AFTER CUTS BUG MODELS COMPUTE PROB OF OUTBREAK
!     ISEQ  = THE ENTRY SEQUENCE NUMBER.
!     ISEQDN= SEQUENCE NUMBER ADDED TO THE IACT(.,4) TO POST THE
!             ACCOMPLISHMENT SEQUENCE.
!     ITOPRM=
!     ITST5 = THE NUMBER OF USER DEFINED VARIABLES.
!     KTODO =
!     LBSETS= TRUE IF LABEL PROCESSING IS ACTIVATED BY ENTERING AT LEAST
!             ONE AGPLABEL OR SPLABEL KEYWORD.
!     LENAGL= THE LENGTH OF EACH ACTIVITY GROUP LABEL SET.
!     LENSLS= THE LENGTH OF THE STAND LABEL SET.  IF LENGTH IS -1, NO
!             LABEL HAS BEEN SPECIFIED, 0=NULL SET, > 0 ACTUAL LENGTH.
!     LEVUSE= TRUE IF VARIABLES NEED TO BE SAVED.
!     LOPEVN=
!     LREG  = LOGICAL REG (STACK) USED FOR EVALUATING EXPRESSIONS.
!     LTSTV4= TRUE WHEN CORRESPONDING VARIABLE IS DEFINED, FALSE OTHER-
!             WISE.  INITIALIZED FALSE, CALL SUBROUTINE EVSET4 TO SET
!             A MEMBER OF TSTV4, AND EVUST4 TO UNSET A VALUE.
!     LTSTV5= TRUE WHEN CORRESPONDING VARIABLE IS DEFINED.
!     MAXACT= THE MAX NUMBER OF ACTIVITIES WHICH CAN BE STORED,
!             NUMBER OF ROWS IN IACT. (MAXIMUM NUMBER OF ACTIVITIES
!             IN THE WHOLE SIMULATION.  NO LIMIT.)
!     MAXCOD= THE MAX NUMBER OF OPERAND CODES WHICH CAN BE STORED
!             FOR THE EVENT MONITIOR, DIMENSION OF IEVCOD. (NO LIMIT).
!     MAXEVA= THE MAX NUMBER OF ACTIVITY GROUPS WHICH CAN BE STORED,
!             NUMBER OF ROWS IN IEVACT. (IF-THEN COUNTS AS ONE
!             ACTIVITY GROUP; ALSOTRY COUNTS AS 1 ACTIVITY GROUP.)
!     MAXEVT= THE MAX NUMBER OF EVENTS WHICH CAN BE STORED,
!             NUMBER OF ROWS IN IEVNTS. (MAXIMUM NUMBER OF "IF"
!             STATEMENTS.)
!     MAXPRM= THE MAX NUMBER OF PARAMETERS WHICH CAN BE STORED,
!             DIMENSION OF PARMS; CAN NOT BE OVER 5999.
!     MXEXPR= MAXIMUM NUMBER OF CHARACTERS WHICH CAN BE IN ANY
!             ONE EXPRESSION.
!     MXPTDO= MAX SIZE OF IPTODO. (MAXIMUM NUMBER OF ACTIVITIES THAT
!             CAN BE SCHEDULED IN ONE CYCLE.)
!     MXTST4= THE MAXIMUM NUMBER OF CATEGORY 4 VARIABLES ALLOWED.
!     MXTST5= THE MAXIMUM NUMBER OF USER DEFINED VARIABLES ALLOWED.
!             CURRENTLY THIS PARAMETER MAY NOT BE OVER 199.
!             (MAXIMUM NUMBER OF COMPUTE VARIABLES, I.E. LEFT HAND
!             SIDES.)
!     MXXREG= SIZE OF XREG. (NOTE: MXXREG AND MXLREG NEED
!     MXLREG= SIZE OF LREG.        TO BE THE SAME SIZE!)
!     PARMS = THE PARAMETERS ASSOCIATED WITH ALL ACTIVITIES.
!     SLSET = THE STAND LABEL SET
!     TSTV1 = GROUP 1 TEST VALUES, KNOWN FROM PHASE I, CYCLE 1 FORWARD.
!     TSTV2 = GROUP 2 TEST VALUES, KNOWN FROM PHASE II OF EACH CYCLE.
!     TSTV3 = GROUP 3 TEST VALUES, KNOWN FROM PHASE I, CYCLE 2 FORWARD.
!     TSTV4 = GROUP 4 TEST VALUES, KNOWN ONLY WHEN CORRESPONDING
!             FLAG (LTSTV4), IS TRUE.
!     TSTV5 = GROUP 5 TEST VALUES ARE USER DEFINED.
!     WKSTR1= A WORK STRING 1
!     WKSRT2= A WORK STRING 2
!     WKSRT3= A WORK STRING 3
!     XREG  = X REGISTER (STACK) USED FOR EVALUATING EXPRESSIONS.
!
!-----END SEGMENT
