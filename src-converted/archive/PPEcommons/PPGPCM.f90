LOGICAL LMOPEN(300)
INTEGER MIDATE(300),MIACT(6,300),MLPARM,MXGPRM,MIMGL,MGPMAX, &
           MGPCYC(300),MGPCUR,IGPSEQ(300)
REAL    GPARMS(300)
COMMON /PPGPCM/ MIDATE,MIACT,LMOPEN,GPARMS,MLPARM,MXGPRM,MIMGL, &
                   MGPMAX,MGPCYC,MGPCUR,IGPSEQ
!
!     MIACT = GLOBAL ACTIVITY KEY ARRAY
!             (1,.) ACTIVITY CODE
!             (2,.) POINTER TO FIRST PARAMETER (GPARMS).
!             (3,.) POINTER TO LAST PARAMETER (GPARMS).
!             (4,.) BEGINNING OF STAND LIST (LIST OF STANDS THAT THIS
!                   ACTIVITY APPLIES TO).
!             (5,.) END OF STAND LIST.
!             (6,.) STATUS CODE 0=NOT DONE, -1=DELETED, >0, COMPLETED
!                   IN THE YEAR STORIED IN THIS WORD.
!     LMOPEN= IF TRUE, ADD INCOMING STANDS TO LIST OF STANDS THIS
!             OPTION APPLIES TO.
!     GPARMS= ARRAY OF PARAMETERS.
!     MIMGL = POINTS TO LAST USED ROW IN MIACT
!     MXGPRM= MAX NUMBER OF PARMS, DIMENSION OF GPARMS.
!     MLPARM= POINTS TO THE LAST USED ROW IN GPARMS.
!     MGPCYC= POINTS TO ACTITITIES THAT ARE SCHEDULED FOR THE CURRENT
!             MASTER CYCLE IN ASCENDING ORDER OF SPECIFICATION.
!     MGPCUR= POINTS TO THE END OF MGPCYC.
!     IGPSEQ= THE ENTRY SEQUENCE NUMBER FOR THE ACTIVITY.
!
!     CURRENTLY DEFINED GLOBAL ACTIVITIY CODES
!     100 PPE  INTERACT (MANUAL INTERACT OPTION)
!     101 WSBW FLYUNIT  (BUDWORM ADULT DISPERSAL)
!     102 WSBW SPRYUNIT (BUDWORM SPRAY AREA)
!     201 MPB  MPBDISP  (MOUNTAIN PINE BEETLE DISPERSAL)
!     301 MPB/WWPB/I    (DISPERSAL)
!     302 MPB/WWPB/I    (REPELLING PHEROMONE)
!     303 MPB/WWPB/I    (ATTRACTING PHEROMONE)
!     304 MPB/WWPB/I    (BUG SPRAY)
!     305 MPB/WWPB/I    (WINDTHROW - LANDSCAPE)
!     306 MPB/WWPB/I    (LIGHTNING - LANDSCAPE)
!     307 MPB/WWPB/I    (TYPE 3 BARK BEETLES)
!     308 MPB/WWPB/I    (OTHER DEFOLIATORS)
!     309 MPB/WWPB/I    (DROUGHT 1 - LANDSCAPE)
!     310 MPB/WWPB/I    (DROUGHT 2 - LANDSCAPE)
!
