SUBROUTINE HTCALC (SINDX,ISPC,AG,HGUESS,JOSTND,DEBUG)
IMPLICIT NONE
!----------
! EC $Id$
!----------
! THIS ROUTINE CALCULATES A POTENTIAL HT GIVEN A SPECIES SITE AND AGE
! IT IS USED TO CAL POTHTG AND SITE.
! GO TO A DIFFERENT POTENTIAL HEIGHT CURVE DEPENDING ON THE SPECIES
!----------
LOGICAL DEBUG
INTEGER ISPC,JOSTND
REAL HGUESS,AG,SINDX
REAL X1,X2,X3,Z
!----------
!  SPECIES LIST FOR EAST CASCADES VARIANT.
!
!   1 = WESTERN WHITE PINE      (WP)    PINUS MONTICOLA
!   2 = WESTERN LARCH           (WL)    LARIX OCCIDENTALIS
!   3 = DOUGLAS-FIR             (DF)    PSEUDOTSUGA MENZIESII
!   4 = PACIFIC SILVER FIR      (SF)    ABIES AMABILIS
!   5 = WESTERN REDCEDAR        (RC)    THUJA PLICATA
!   6 = GRAND FIR               (GF)    ABIES GRANDIS
!   7 = LODGEPOLE PINE          (LP)    PINUS CONTORTA
!   8 = ENGELMANN SPRUCE        (ES)    PICEA ENGELMANNII
!   9 = SUBALPINE FIR           (AF)    ABIES LASIOCARPA
!  10 = PONDEROSA PINE          (PP)    PINUS PONDEROSA
!  11 = WESTERN HEMLOCK         (WH)    TSUGA HETEROPHYLLA
!  12 = MOUNTAIN HEMLOCK        (MH)    TSUGA MERTENSIANA
!  13 = PACIFIC YEW             (PY)    TAXUS BREVIFOLIA
!  14 = WHITEBARK PINE          (WB)    PINUS ALBICAULIS
!  15 = NOBLE FIR               (NF)    ABIES PROCERA
!  16 = WHITE FIR               (WF)    ABIES CONCOLOR
!  17 = SUBALPINE LARCH         (LL)    LARIX LYALLII
!  18 = ALASKA CEDAR            (YC)    CALLITROPSIS NOOTKATENSIS
!  19 = WESTERN JUNIPER         (WJ)    JUNIPERUS OCCIDENTALIS
!  20 = BIGLEAF MAPLE           (BM)    ACER MACROPHYLLUM
!  21 = VINE MAPLE              (VN)    ACER CIRCINATUM
!  22 = RED ALDER               (RA)    ALNUS RUBRA
!  23 = PAPER BIRCH             (PB)    BETULA PAPYRIFERA
!  24 = GIANT CHINQUAPIN        (GC)    CHRYSOLEPIS CHRYSOPHYLLA
!  25 = PACIFIC DOGWOOD         (DG)    CORNUS NUTTALLII
!  26 = QUAKING ASPEN           (AS)    POPULUS TREMULOIDES
!  27 = BLACK COTTONWOOD        (CW)    POPULUS BALSAMIFERA var. TRICHOCARPA
!  28 = OREGON WHITE OAK        (WO)    QUERCUS GARRYANA
!  29 = CHERRY AND PLUM SPECIES (PL)    PRUNUS sp.
!  30 = WILLOW SPECIES          (WI)    SALIX sp.
!  31 = OTHER SOFTWOODS         (OS)
!  32 = OTHER HARDWOODS         (OH)
!
!  SURROGATE EQUATION ASSIGNMENT:
!
!  FROM THE EC VARIANT:
!      USE 6(GF) FOR 16(WF)
!      USE OLD 11(OT) FOR NEW 12(MH) AND 31(OS)
!
!  FROM THE WC VARIANT:
!      USE 19(WH) FOR 11(WH)
!      USE 33(PY) FOR 13(PY)
!      USE 31(WB) FOR 14(WB)
!      USE  7(NF) FOR 15(NF)
!      USE 30(LL) FOR 17(LL)
!      USE  8(YC) FOR 18(YC)
!      USE 29(WJ) FOR 19(WJ)
!      USE 21(BM) FOR 20(BM) AND 21(VN)
!      USE 22(RA) FOR 22(RA)
!      USE 24(PB) FOR 23(PB)
!      USE 25(GC) FOR 24(GC)
!      USE 34(DG) FOR 25(DG)
!      USE 26(AS) FOR 26(AS) AND 32(OH)
!      USE 27(CW) FOR 27(CW)
!      USE 28(WO) FOR 28(WO)
!      USE 36(CH) FOR 29(PL)
!      USE 37(WI) FOR 30(WI)
!----------
!  SEE IF WE NEED TO DO SOME DEBUG.
!----------
IF(DEBUG) WRITE(JOSTND,10)SINDX,ISPC,AG
10 FORMAT(' ENTERING HTCALC, SINDX,ISPC,AG= ',F7.2,I5,F7.2)
!
SELECT CASE (ISPC)
!----------
! WHITE PINE USE BRICKELL, INT-75
! SPECIES: WP
!----------
CASE(1)
  HGUESS=SINDX/(0.37504453* &
            (1.0-0.92503*EXP(-0.0207959*AG))**(-2.4881068))
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC WP HGUESS = ',HGUESS
!----------
! WESTERN LARCH USE COCHRAN, PNW 424
! SPECIES: WL, LL
!----------
CASE(2,17)
  HGUESS=4.5 + 1.46897*AG + 0.0092466*AG*AG - 0.00023957*AG**3 &
            + 1.1122E-6*AG**4 + (SINDX -4.5)*(-0.12528 + 0.039636*AG &
            - 0.0004278*AG*AG + 1.7039E-6*AG**3) &
            - 73.57*(-0.12528 + 0.039636*AG - 0.0004278*AG*AG &
            + 1.7039E-6*AG**3)
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC WL/LL HGUESS = ',HGUESS
!----------
! DOUGLAS FIR USE COCHRAN, PNW 251
! SPECIES: DF
!----------
CASE(3)
  HGUESS=4.5 + EXP(-0.37496 + 1.36164*ALOG(AG) - 0.00243434* &
            (ALOG(AG))**4) - 79.97*(-0.2828 + 1.87947*(1.0 - &
            EXP(-0.022399*AG))**0.966998) + (SINDX - 4.5)* &
            (-0.2828 + 1.87947*(1.0 - EXP(-0.022399*AG)**0.966998))
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC DF HGUESS = ',HGUESS
!----------
! SILVER FIR, SURROGATE IS WHITE FIR USE COCHRAN PNW 252
! GRAND FIR ---- USE SILVER FIR
! SPECIES: SF, GF, WF
!----------
CASE(4,6,16)
  X2= -0.30935 + 1.2383*ALOG(AG) + 0.001762*(ALOG(AG))**4 &
         - 5.4E-6*(ALOG(AG))**9 + 2.046E-7*(ALOG(AG))**11 &
         - 4.04E-13*(ALOG(AG))**18
  X3= -6.2056 + 2.097*ALOG(AG) - 0.09411*(ALOG(AG))**2 &
         - 0.00004382*(ALOG(AG))**7 + 2.007E-11*(ALOG(AG))**16 &
         - 2.054E-17*(ALOG(AG))**24
  HGUESS=EXP(X2) - 84.73*EXP(X3) + (SINDX - 4.5)*EXP(X3) &
          + 4.5
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC WP X2,X3,HGUESS = ', &
     X2,X3,HGUESS
!----------
! RED CEDAR ---- USE HEGYI, JELINEK, VISZLAI,
! & CARPENTER 1981 FOR SITE REFERENCE+
! SPECIES: RC
!----------
CASE(5)
  HGUESS = 1.3283*SINDX * ((1.0 - EXP(-0.0174*AG))**1.4711)
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC RC HGUESS = ',HGUESS
!----------
! LODGEPOLE PINE USE ALEXANDER TACKLE AND DAHMS FOR CCF 100, RM-29
! SPECIES: LP
!----------
CASE(7)
  HGUESS = 9.89331 - 0.19177*AG + 0.00124*AG*AG &
            - 0.00082*0.0*SINDX &
            + 0.01387*AG*SINDX - 0.0000455*AG*AG*SINDX
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC LP HGUESS = ',HGUESS
!----------
! ENGLEMANN SPRUCE USE ALEXANDER, RM-32
! SPECIES: ES
!----------
CASE(8)
  HGUESS = 4.5+((2.75780*SINDX**0.83312)* &
            (1.0-EXP(-0.015701*AG))**(22.71944*SINDX**(-0.63557)))
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC ES HGUESS = ',HGUESS
!----------
! SUB ALPINE FIR AND NOBLE FIR USE JOHNSON'S EQUIV OF DEMARS, PNW-119
! SPECIES: AF
!----------
CASE(9)
  HGUESS=SINDX*(-0.07831 + 0.0149*AG - 4.0818E-5*AG*AG)
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC AF HGUESS = ',HGUESS
!----------
! PONDEROSA PINE USE BARRETT, PNW-232
! SPECIES: PP
!----------
CASE(10)
  HGUESS =( 128.8952205*(1.0 -EXP(-0.016959*AG))**1.23114) &
              - ((-0.7864 + 2.49717* &
              (1.0-EXP(-0.004504*AG))**0.33022)*100.43) &
              + ((-0.7864 + 2.49717*(1.0-EXP(-0.004504*AG))**0.33022) &
              *(SINDX-4.5)) + 4.5
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC PP HGUESS = ',HGUESS
!----------
! WESTERN HEMLOCK - WILEY, 1978
! SPECIES: WH
!----------
CASE(11)
  Z = 2500./(SINDX-4.5)
  HGUESS = (AG*AG/(-1.7307 + 0.1394*Z + (-0.0616 + 0.0137*Z)*AG + &
              (0.00192 + 0.00007*Z)*(AG*AG))) + 4.5
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC WH Z,HGUESS = ',Z,HGUESS
!----------
! MOUNTAIN HEMLOCK AND OTHER SOFTWOODS. USE MEANS (UNPUBL) METRIC
! SINCE MEANS USES HEIGHT ABOVE B.H., ADD IN 1.37 METERS BEFORE
! CONVERTING TO ENGLISH UNITS.
! SPECIES: MH, OS
!----------
CASE(12,31)
  HGUESS = (22.8741 + 0.950234*SINDX)* &
             (1.0 - EXP(-0.00206465*SQRT(SINDX)*AG)) &
             ** (1.365566 + 2.045963/SINDX)
  HGUESS=(HGUESS+1.37)*3.281
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC MH/OS HGUESS = ',HGUESS
!----------
! PACIFIC YEW, WHITEBARK PINE, ALASKA CEDAR, WESTERN JUNIPER
! BIGLEAF MAPLE, VINE MAPLE, PAPER BIRCH, GIANT CHINQUAPIN,
! PACIFIC DOGWOOD, QUAKING ASPEN, BLACK COTTONWOOD,
! CHERRY AND PLUM SPECIES, WILLOW SPECIES, OTHER HARDWOODS
!
! USE CURTIS, FOR. SCI. 20:307-316.
! CURTIS CURVES ARE PRESENTED IN METRIC (3.2808 ?)
! BECAUSE OF EXCESSIVE HT GROWTH -- APPROX 30-40 FT/CYCLE, TOOK OUT
! THE METRIC MULTIPLIER DIXON 11-05-92
! SPECIES: PY, WB, YC, WJ, BM, VN, PB, GC, DG, AS, CW, PL, WI, OH
!----------
CASE(13,14,18:21,23:27,29:30,32)
  HGUESS = (SINDX - 4.5) / (0.6192 - 5.3394/(SINDX - 4.5) &
              + 240.29*AG**(-1.4) +(3368.9/(SINDX-4.5))*AG**(-1.4))
  HGUESS = HGUESS + 4.5
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC PY/OTHERS HGUESS = ',HGUESS
!----------
! NOBLE FIR - HERMAN, PNW-243
! SPECIES: NF
!----------
CASE(15)
  X1 = -564.38 + 22.25*(SINDX - 4.5) - 0.04995*(SINDX - 4.5)**2
  X2 = 6.80 + 2843.21*(SINDX - 4.5)**(-1) &
          + 34735.54*(SINDX - 4.5)**(-2)
  HGUESS = 4.5 + (SINDX - 4.5)/(X1*(1.0/AG)**2 + &
      X2*(1.0/AG) + 1.0 - 0.0001*X1 - 0.01*X2)
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC NF X1,X2,HGUESS= ',X1,X2,HGUESS
!----------
! RED ALDER - HARRINGTON, PNW-358
! SPECIES: RA
!----------
CASE(22)
  HGUESS = SINDX &
              + (59.5864 + 0.7953*SINDX)*(1.0-EXP((0.00194 &
              - 0.00074*SINDX)*AG))**0.9198 &
              - (59.5864 + 0.7953*SINDX)*(1.0-EXP((0.00194 &
              - 0.00074*SINDX)*20.0))**0.9198
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC RA HGUESS = ',HGUESS
!----------
! OREGON WHITE OAK - KING, DF, WEYERHAEUSER FORESTRY PAPER NO. 8
! SPECIES: WO
!----------
CASE(28)
  Z = 2500./(SINDX-4.5)
  HGUESS = (AG*AG/(-0.954038 + 0.109757*Z + (5.58178E-2 + &
              7.92236E-3*Z)*AG + (-7.33819E-4 + 1.97693E-4*Z)* &
              (AG*AG))) + 4.5
  IF(DEBUG)WRITE(JOSTND,*)' HTCALC WO HGUESS = ',HGUESS
!----------
! FUTURE SPECIES
!----------
CASE DEFAULT
  HGUESS = 0.
!
END SELECT
!
RETURN
END
