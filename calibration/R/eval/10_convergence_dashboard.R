# --- Libraries ---------------------------------------------------------------
library(tidyverse)
library(ggsci)
library(patchwork)

# --- Paths -------------------------------------------------------------------
PROJ_ROOT <- "/users/PUOM0008/crsfaaron/fvs-modern"
OUT_DIR   <- file.path(PROJ_ROOT, "calibration/output/evaluation")
dir.create(OUT_DIR, recursive = TRUE, showWarnings = FALSE)

# Map of base model identifier to summary CSV path. NA entries are skipped
# at runtime with a warning, so the script works as more fits land.
# Unified eval summaries (regenerated by 00_regenerate_summaries.R)
fit_paths <- tribble(
  ~model,        ~variant,            ~path,
  "HG",          "B2 species-aware",  "calibration/output/evaluation/summaries/hg_b2_eval_summary.csv",
  "HG",          "B1 species-free",   "calibration/output/evaluation/summaries/hg_b1_eval_summary.csv",
  "HT-DBH",      "B2 species-aware",  "calibration/output/evaluation/summaries/htdbh_b2_eval_summary.csv",
  "CR",          "B2 species-aware",  "calibration/output/evaluation/summaries/cr_b2_eval_summary.csv",
  "HCB",         "B2 species-aware",  "calibration/output/evaluation/summaries/hcb_b2_eval_summary.csv",
  "Mortality",   "B2 species-aware",  "calibration/output/evaluation/summaries/mort_b2_eval_summary.csv",
  "DG Kuehne",   "B2 species-aware",  "calibration/output/evaluation/summaries/dg_kue_b2_eval_summary.csv",
  "DG Kuehne",   "B1 species-free",   "calibration/output/evaluation/summaries/dg_kue_b1_eval_summary.csv"
) %>%
  mutate(full_path = file.path(PROJ_ROOT, path))

# --- Read and classify parameters -------------------------------------------

classify_param <- function(v) {
  case_when(
    grepl("^(a|b)[0-9]$", v)              ~ "Fixed-effect coefs (a, b)",
    grepl("^gamma\\[", v)                 ~ "Trait coefs (gamma)",
    grepl("^trait_effect\\[", v)          ~ "Species trait effects",
    grepl("^z_(sp|L1|L2|L3)\\[", v)       ~ "Random-effect deviations",
    grepl("^sigma_(sp|L1|L2|L3)$", v)     ~ "Random-effect scales (sigma_X)",
    grepl("^sigma$", v)                   ~ "Residual sigma",
    TRUE                                  ~ "Other"
  )
}

read_one <- function(model, variant, full_path) {
  if (!file.exists(full_path)) {
    message(sprintf("  skip: %s | %s (file not yet present)", model, variant))
    return(NULL)
  }
  message(sprintf("  read: %s | %s", model, variant))
  df <- read_csv(full_path, show_col_types = FALSE)
  df %>%
    mutate(
      model     = model,
      variant   = variant,
      param_grp = classify_param(variable)
    ) %>%
    filter(param_grp != "Species trait effects",
           param_grp != "Random-effect deviations")
}

all_params <- fit_paths %>%
  pmap_dfr(~read_one(..1, ..2, ..4))

# --- Summary table -----------------------------------------------------------

dashboard <- all_params %>%
  group_by(model, variant, param_grp) %>%
  summarise(
    n_params      = n(),
    rhat_max      = max(rhat, na.rm = TRUE),
    rhat_median   = median(rhat, na.rm = TRUE),
    rhat_q95      = quantile(rhat, 0.95, na.rm = TRUE),
    ess_bulk_min  = min(ess_bulk, na.rm = TRUE),
    ess_bulk_med  = median(ess_bulk, na.rm = TRUE),
    pct_rhat_le_105 = mean(rhat <= 1.05, na.rm = TRUE) * 100,
    .groups = "drop"
  ) %>%
  mutate(across(where(is.numeric), ~round(., 3)))

dashboard_csv <- file.path(OUT_DIR, "convergence_dashboard.csv")
write_csv(dashboard, dashboard_csv)
cat("Wrote", dashboard_csv, "\n")

# --- Figure: rhat distributions by group ------------------------------------

theme_pub <- theme_classic(base_size = 12) +
  theme(
    panel.grid.major.y = element_line(color = "grey92"),
    panel.grid.minor   = element_blank(),
    legend.position    = "bottom",
    strip.text         = element_text(face = "bold")
  )

p_rhat <- all_params %>%
  filter(param_grp %in% c("Fixed-effect coefs (a, b)",
                          "Trait coefs (gamma)",
                          "Random-effect scales (sigma_X)",
                          "Residual sigma")) %>%
  mutate(model_variant = paste(model, variant, sep = " | ")) %>%
  ggplot(aes(rhat, model_variant, color = variant)) +
  geom_vline(xintercept = c(1.01, 1.05), linetype = "dashed",
             color = c("grey30", "grey60"), linewidth = 0.4) +
  geom_point(alpha = 0.5, size = 1.4) +
  facet_wrap(~param_grp, scales = "free_x", ncol = 2) +
  scale_color_aaas() +
  labs(
    x = "rhat", y = NULL, color = "Variant",
    title = "Convergence by base model and parameter group",
    subtitle = "Dashed lines at rhat = 1.01 and 1.05"
  ) +
  theme_pub

p_ess <- all_params %>%
  filter(param_grp %in% c("Fixed-effect coefs (a, b)",
                          "Trait coefs (gamma)",
                          "Random-effect scales (sigma_X)",
                          "Residual sigma")) %>%
  mutate(model_variant = paste(model, variant, sep = " | ")) %>%
  ggplot(aes(ess_bulk, model_variant, color = variant)) +
  geom_vline(xintercept = c(400, 1000), linetype = "dashed",
             color = c("grey30", "grey60"), linewidth = 0.4) +
  geom_point(alpha = 0.5, size = 1.4) +
  scale_x_log10() +
  facet_wrap(~param_grp, scales = "free_x", ncol = 2) +
  scale_color_aaas() +
  labs(
    x = "ESS bulk (log scale)", y = NULL, color = "Variant",
    title = "Effective sample size by base model and parameter group",
    subtitle = "Dashed lines at ESS = 400 and 1000"
  ) +
  theme_pub

dashboard_fig <- p_rhat / p_ess +
  plot_layout(guides = "collect") +
  plot_annotation(tag_levels = "A") &
  theme(legend.position = "bottom")

# --- Save outputs ------------------------------------------------------------

fig_png <- file.path(OUT_DIR, "convergence_dashboard.png")
fig_pdf <- file.path(OUT_DIR, "convergence_dashboard.pdf")
ggsave(fig_png, dashboard_fig,
       width = 22, height = 22, units = "cm", dpi = 300, bg = "white")
ggsave(fig_pdf, dashboard_fig,
       width = 22, height = 22, units = "cm")
cat("Wrote", fig_png, "\n")
cat("Wrote", fig_pdf, "\n")

# --- Print summary ----------------------------------------------------------

cat("\n=== Convergence dashboard summary ===\n")
print(dashboard, n = nrow(dashboard))
cat("\nDone.\n")
