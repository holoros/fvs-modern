RETURN
!----------
! ENTRY ECNGET CALLED FROM GETSTD
!----------
ENTRY ECNGET (ARRAY,I1,I2)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  RDANUW = ARRAY(1)
  IDANUW = I1
  IDANUW = I2
RETURN
!----------
! ENTRY ECNPUT CALLED FROM PUTSTD
!----------
ENTRY ECNPUT (ARRAY,I1,I2)
