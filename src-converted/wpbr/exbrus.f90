RETURN

!     CALLED FROM GRADD
!
ENTRY BRTREG
RETURN

!     CALLED FROM INITRE
!
ENTRY BRINIT
RETURN

ENTRY BRIN (KEYWRD,ARRAY,LNOTBK)
