INTEGER ISTEML(IRRTRE), ISTEMI(IRRTRE)

REAL    FPROB(IRRTRE), PROBI(IRRTRE,41,2), PROBIT(IRRTRE), &
           PROBIU(IRRTRE), PROBL(IRRTRE), PROP1(82), &
           PROPI(IRRTRE,41,2), ROOTL(IRRTRE), RRKILL(IRRTRE), &
           TXP8(82), WINDWL(IRRTRE), WINDWI(IRRTRE)

COMMON /RDARRY/ FPROB, ISTEML, ISTEMI, PROBI, PROBIT, PROBIU, &
                   PROBL, PROP1, PROPI, ROOTL, RRKILL, &
                   WINDWL, WINDWI, TXP8
!
!     PROBI   -- (TREE,CYCLE,IN/EXP)INFECTED TPA OF RECORD (IN DISEASE
!                CENTER) BY CYCLE OF INFECTION AND WHETHER INFECTED
!                WITHIN CENTER (,,1) OR BY CENTER EXPANSION (,,2).
!                INITIALIZED FROM TREEDATA IN RDPRIN.F.
!     PROBIU  -- UNINFECTED TPA OF RECORD (IN DISEASE CENTER).
!                INITIALIZED FROM TREEDATA IN RDPRIN.F.
!     FPROB   -- UNINFECTED TPA OF RECORD (NOT IN DISEASE CENTER).
!                INITIALIZED FROM TREEDATA IN RDPRIN.F.
!     PROBL   -- LIVE TPA OF RECORD LOADED DIRECTLY FROM FVS PROB
!                ARRAY IN RDSETP.F AND RDTREG.F.
!     PROBIT  -- THE NUMBER OF TREES IN EACH TREE RECORD THAT
!                ARE INFECTED. (THE SUM OF PROBI ACROSS THE CYCLES).
!     ROOTL   -- THE ROOT RADIUS (FEET) FOR EACH TREE RECORD.
!     RRKILL  -- NUMBER OF INFECTED TREES KILLED BY ROOT
!                DISEASE AND OTHER AGENTS IN THE CURRENT CYCLE FOR
!                EACH TREE RECORD.
!     ISTEMI  -- INDEX ARRAY USED TO INDEX RECORDS THAT ARE
!                INFECTED AND SUSCEPTIBLE TO WINDTHROW.
!     ISTEML  -- INDEX ARRAY USED TO INDEX RECORDS THAT ARE
!                UNINFECTED AND SUSCEPTIBLE TO WINDTHROW.
!     WINDWL  -- CONTAINS THE TREES/ACRE MULTIPLIED BY THE
!                CROWN RATIO FOR EACH TREE RECORD.  USED IN THE
!                WINDTHROW MODEL.
!     WINDWI  --
!     PROPI   -- (TREES,CYCLE,IN/EXP) HOLDS THE PROPORTION OF ROOTS
!                INFECTED ON THE TREES IN PROBI BY THE CYCLE IN
!                WHICH THE TREE RECORD WAS INFECTED AND IF IT WAS
!                INFECTED WITHIN A CENTER (,,1) OR BY CENTER
!                EXPANSION (,,2).
!     TXP8    --
!     PROP1   --
!
