! WHEREBY GRASS LEAVES WERE TURNED OFF FOR DAYS 120-210 (PREVIOUSLY THEY WERE
! ON DURING THIS PERIOD)
!
! REVISING LOGIC AGAIN AJM 11/18/03.
! COMMENTING OUT SECTION DEALING WITH REALLOCATING EACH ENTITY'S BUCKET RATIO
! WHICH IS (WAS) ALTERED AS GRASSES TURNED ON OR OFF.  TREE'S RATIO WON'T
! SIGNIFICANTLY CHANGE BY OMITTING THIS TEST.  WHAT WILL CHANGE BY LEAVING THIS
! OUT IS THAT EACH TREE WILL GET A VERY SLIGHT BIT LESS WHEN/IF GRASSES ARE "OFF".
! HOWEVER, FOR THIS VERSION, I'M TURNING GRASSES ON ALL YEAR ANYWAY (EXCEPT
! WILL TEST A NEW PROCEDURE WHEREBY GRASS LEAVES WILL BE OFF IF SNOW IS DEEP.
! [BUT GRASS ROOTS WILL STILL BE ON TO RESPIRE]).
!
! WITHOUT THIS CHANGE, TREES' BUCKETS GET BIGGER WHEN GRASS ROOTS OFF (BY A
! SLIGHT AMOUNT, WITH SUM TOTAL OF EACH TREES BUCKET-INCREMENT BEING WHAT
! GRASSES HAD IF THEY WERE ON.  WITH THIS CHANGE, THEY WILL HAVE SAME BUCKET
! SIZE.  EACH TREE'S BUCKET SIZE RELATIVE TO ALL OTHERS WILL BE IDENTICAL
! (UNDER EITHER SCENARIO).
! NOTE THAT BY REMOVING THE TEST BELOW, GRASSES STILL "OCCUPY" THE SITE WHEN
! "OFF" HENCE STILL HAVE A BUCKET WHEN OFF.  THEY JUST DONT "DO" ANTHING WHEN
! OFF EXCEPT ROOT RESPIRATION) IN BGROWN.
! SO, ALTHOUGH TREES WILL HAVE A SLIGHTLY SMALLER BUCKET THIS WAY, THERE MAY
! BE REMAINING WATER ON SITE THE DAY AFTER TREES TRANSPIRE FROM THE WATER THAT
! DIDN'T GET INTO ANY TREE'S BUCKET THE PREVIOUS DAY (BECAUSE THEIR BUCKET WAS
! A BIT SMALLER).  THIS ON-SITE WATER WILL GET 'REDISTRIBUTED" ACROSS THE SITE
! THE NEXT MORNING, AND AVAILABLE TO FILL BUCKETS.
!
! THIS 11/18/03 CHANGE BEING SAVED AS "SITOCN3"
!
!-------------------------
