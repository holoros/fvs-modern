SUBROUTINE MPBTAB(CFTVOL)
IMPLICIT NONE
!----------
! LPMPB $Id$
!----------
!
!  PRINTS OUT THE SUMMARY OF MORTALITY BY MPB.  THIS TABLE IS PRINTED
!  TO THE MACHINE READABLE FILE THAT IS DEFINED BY KEYWORD MPBECHO.
!  THIS IS THE SUMMARY THAT SHOWS TOTALS FOR MORTALITY BY DBH CLASS.
!  THIS IS THE SECOND TABLE THAT IS PRINTED IN THE MACHINE READABLE
!  FILE.
!
!  CALLED BY :
!     COLMRT  [MPB]
!
!  CALLS :
!     COLIND  (SUBROUTINE)  [MPB]
!
!  PARAMETERS :
!     CFTVOL - ARRAY THAT HOLDS THE CUBIC FOOT VOLUME LOST DUE TO MPB
!              FOR EACH DBH CLASS.
!
!  LOCAL VARIABLES :
!     DCLASS - ARRAY THAT HOLDS THE DIAMETER CLASS HEADINGS THAT ARE
!              TO BE PRINTED IN THE SUMMARY TABLE.
!     DEADLP - ARRAY THAT HOLDS THE TOTAL NUMBER OF DEAD LODGEPOLE
!              PINE TREES/ACRE AT THE END OF THE OUTBREAK BY DBH CLASS.
!     I, J   - INDEX COUNTERS.
!     INDEX  - THE DBH CLASS OF THE CURRENT TREE RECORD.
!     LIVELP - ARRAY THAT HOLDS THE TOTAL NUMBER OF LIVE LODGEPOLE PINE
!              TREES/ACRE THAT EXIST AFTER THE OUTBREAK BY DBH CLASS.
!     NUMYR  - NUMBER OF YEARS IN THE OUTBREAK.
!     TOTGRN - ARRAY THAT HOLDS THE TOTAL LIVE TREES/ACRE FOR EACH DBH
!              CLASS IN THE STAND.
!     TTDEAD - HOLDS THE TOTAL NUMBER OF LODGEPOLE PINE THAT ARE KILLED
!              BY MPB (TREES/ACRE).
!     TTLIVE - HOLDS THE TOTAL NUMBER OF LIVE LODGEPOLE PINE
!              (TREES/ACRE).
!     TTTOTL - HOLDS THE TOTAL NUMBER OF LIVE TREES (TREES/ACRE).
!     TTVOL  - HOLDS THE TOTAL CUBIC FOOT VOLUME LOST DUE TO THE MPB.
!     YRTEST - USED TO TEST FOR THE NUMBER OF YEARS IN THE OUTBREAK.
!
!  COMMON BLOCK VARIABLES USED :
!     DBH    - (ARRAYS)  INPUT
!     DEAD   - (COLCOM)  INPUT
!     GREEN  - (COLCOM)  INPUT
!     ICYC   - (CONTRL)  INPUT
!     ITRN   - (CONTRL)  INPUT
!     IY     - (CONTRL)  INPUT
!     JOMPBX - (MPBCOM)  INPUT
!     PROB   - (ARRAYS)  INPUT
!
! Revision History
!   03/26/91 Last noted revision date.
!   07/02/10 Lance R. David (FMSC)
!     Added IMPLICIT NONE.
!   08/22/14 Lance R. David (FMSC)
!     Function name was used as variable name.
!     changed variable INDEX to INDX
!----------
!
!OMMONS
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
INCLUDE 'MPBCOM.f90'
!
!
INCLUDE 'COLCOM.f90'
!
!OMMONS
!

INTEGER I, J, INDX, NUMYR

REAL    TOTGRN(10), CFTVOL(10), LIVELP(10), DEADLP(10)
REAL    TTLIVE, TTDEAD, TTTOTL, TTVOL, YRTEST

CHARACTER*8 DCLASS(10)

DATA DCLASS / ' 1 -  3',' 3 -  5',' 5 -  7',' 7 -  9',' 9 - 11', &
                 '11 - 13','13 - 15','15 - 17','17 - 19','19 - 20+' /

!
!     TEST TO SEE IF MACHINE READABLE OUTPUT IS BEING PRINTED.
!     IF IT IS NOT THEN DO NOT PRINT THIS TABLE.
!
IF (JOMPBX .EQ. 0) GOTO 1000

!
!     INITIALIZE TOTGRN, LIVELP, DEADLP.
!     FIND THE NUMBER OF YEARS IN THE OUTBREAK BY TESTING TO SEE WHEN
!     GREEN EQUALS ZERO FOR ALL SIZE CLASSES.
!
NUMYR = 0
DO 100 I = 1,10
   TOTGRN(I) = 0.0
   LIVELP(I) = 0.0
   DEADLP(I) = 0.0

   YRTEST = 0.0
   DO 90 J = 1,10
      YRTEST = YRTEST + GREEN(I,J)
90    CONTINUE
   IF (YRTEST .GT. 0.0) NUMYR = NUMYR + 1
100 CONTINUE

!
!     BREAK PROGNOSIS PROB ARRAY INTO DBH CLASSES.
!
DO 200 I = 1, ITRN
   CALL COLIND (DBH(I), INDX)
   TOTGRN(INDX) = TOTGRN(INDX) + PROB(I)
200 CONTINUE

!
!     PRINT FIRST LINE SO THAT THIS TABLE CAN BE DISTINGUISHED FROM
!     THE OTHER MACHINE READABLE TABLE.  ALSO PRINT THE CYCLE YEAR.
!
WRITE(JOMPBX,*)
WRITE(JOMPBX,*) 'SUMMARY BY DBH'

WRITE (JOMPBX,300) IY(ICYC)
300 FORMAT (I4)

!
!     CALCULATE THE TOTALS FOR LIVE AND DEAD LODGEPOLE FOR EACH DBH
!     CLASS.
!
DO 500 I = 1,10
   DO 400 J = 1,10
      DEADLP(I) = DEADLP(I) + DEAD(J,I)
400    CONTINUE
   LIVELP(I) = LIVELP(I) + GREEN(NUMYR,I)
500 CONTINUE

!
!     INITIALIZE THE VARIABLES THAT KEEP THE TOTALS.
!
TTLIVE = 0.0
TTDEAD = 0.0
TTTOTL = 0.0
TTVOL  = 0.0

!
!     PRINT OUT SUMMARY TABLE INFORMATION AND CALCULATE TOTALS.
!
DO 600 I = 1,10
   TOTGRN(I) = TOTGRN(I) - DEADLP(I)
   WRITE (JOMPBX,700) DCLASS(I), TOTGRN(I), LIVELP(I), &
                         DEADLP(I), CFTVOL(I)

   TTLIVE = TTLIVE + LIVELP(I)
   TTDEAD = TTDEAD + DEADLP(I)
   TTTOTL = TTTOTL + TOTGRN(I)
   TTVOL  = TTVOL  + CFTVOL(I)
600 CONTINUE

!
!     PRINT OUT THE SUMMARY TABLE TOTALS.
!
WRITE (JOMPBX,710)
WRITE (JOMPBX,720) TTTOTL, TTLIVE, TTDEAD, TTVOL

700 FORMAT (2X,A8,2X,F7.1,5X,F7.1,5X,F7.1,4X,F8.1)
710 FORMAT (1X,'           ----------  ----------  ---------- ', &
           ' ----------')
720 FORMAT (12X,F7.1,5X,F7.1,5X,F7.1,4X,F8.1)

1000 CONTINUE
RETURN
END
