INTEGER    MXCMRW
PARAMETER (MXCMRW=MAXCYC*2)
INTEGER YEAR(MXCMRW),INDEX(MXCMRW),LEN
REAL    ALL(17,MXCMRW),PRBSUM(MXCMRW),HRVSUM(MXCMRW)
COMMON /PPCMAD/ ALL,YEAR,PRBSUM,HRVSUM,INDEX,LEN
INTEGER IALL(17,MXCMRW)
EQUIVALENCE (ALL,IALL)
!
!     USED IN BUILDING COMPOSITE YIELD TABLES.
!     ALL   = HOLDS THE FLOATING POINT VERSION OF THE TOTALS.
!     IALL  = CONVERSION TO INTEGERS...SHARES MEMORY WITH ALL.
!     YEAR  = ALL OF THE UNIQUE YEARS.
!     PRBSUM= THE SUM OF STAND WEIGHTING FACTORS FOR EACH ROW.
!     HRVSUM= THE SUM OF STAND WEIGHTING FACTORS OF STANDS THAT HAVE
!             HARVESTED.
!     INDEX = AN INDEX POINTER ARRAY, USED IN SPECIFYING THE SORTED
!             ORDER OF THE OUTPUT.
!     LEN   = THE NUMBER OF POSITIONS IN THE ARRAYS THAT ARE DEFINED.
!
!-----END SEGMENT
