# =============================================================================
# Title: Posterior predictive checks (PPC) for a fitted base model
# Author: A. Weiskittel
# Date: 2026-05-11
# Description: Generates posterior predictive samples from a banked fit and
#              compares them to observed data via density overlay, grouped
#              means, and quantile coverage. Designed to support manuscript
#              Methods sentence on model adequacy.
#
# Run on Cardinal:
#   sbatch calibration/slurm/eval_ppc.sh
#     # which calls:
#     # Rscript --vanilla calibration/R/eval/50_posterior_predictive.R --model=hg --variant=b2
#
# CLI flags:
#   --model={hg,htdbh,cr,hcb,mort,dg_kue}
#   --variant={b1,b2}
#   --n_draws=N       number of posterior predictive draws to retain (default: 100)
#   --subsample=N     compute PPC on a random N-row subsample (default: 5000)
#
# The PPC samples are generated by sampling parameters from the posterior
# (or using the stored mu_a / eta_gq plus the residual sigma) and adding
# the appropriate likelihood noise. Lognormal for HG and DG, Beta for CR
# and HCB, Bernoulli for Mortality.
# =============================================================================

# --- Libraries ---------------------------------------------------------------
library(tidyverse)
library(ggsci)
library(posterior)
library(patchwork)

# --- CLI parsing -------------------------------------------------------------
args <- commandArgs(trailingOnly = TRUE)
get_arg <- function(name, default = NULL) {
  m <- grep(paste0("^--", name, "="), args, value = TRUE)
  if (length(m) == 0) return(default)
  sub(paste0("^--", name, "="), "", m[1])
}
MODEL      <- get_arg("model", "hg")
VARIANT    <- get_arg("variant", "b2")
N_DRAWS    <- as.integer(get_arg("n_draws", "100"))
SUBSAMPLE  <- as.integer(get_arg("subsample", "5000"))

cat(sprintf("PPC: model=%s variant=%s n_draws=%d subsample=%d\n",
            MODEL, VARIANT, N_DRAWS, SUBSAMPLE))

# --- Paths and likelihood lookup --------------------------------------------
PROJ_ROOT <- "/users/PUOM0008/crsfaaron/fvs-modern"
OUT_DIR <- file.path(PROJ_ROOT, "calibration/output/evaluation/ppc",
                     paste(MODEL, VARIANT, sep = "_"))
dir.create(OUT_DIR, recursive = TRUE, showWarnings = FALSE)

paths_index <- tribble(
  ~model,  ~variant, ~fit_path, ~likelihood,
  "hg",    "b2",     "calibration/output/conus/hg/hg_organon_fixedK_cspi_traits1_fit.rds",                       "lognormal_scaled",
  "hg",    "b1",     "calibration/output/conus/hg/speciesfree_pilot/hg_organon_fixedK_cspi_traits1_fit.rds",     "lognormal_scaled",
  "dg_kue","b2",     "calibration/output/conus/dg/dg_kuehne_cspi_traits1_fit.rds",                                "lognormal_scaled",
  "dg_kue","b1",     "calibration/output/conus/dg/speciesfree_pilot/dg_kuehne_cspi_traits1_b1_fit.rds",          "lognormal_scaled",
  "htdbh", "b2",     "calibration/output/conus/ht_dbh/htdbh_wykoff_lognormal_cspi_traits1_fit.rds",              "lognormal",
  "cr",    "b2",     "calibration/output/conus/crown_recession/cr_recession_cspi_traits1_fit.rds",               "beta",
  "hcb",   "b2",     "calibration/output/conus/hcb/hcb_organon_cspi_traits1_fit.rds",                            "beta",
  "mort",  "b2",     "calibration/output/conus/mortality/mort_logit_simple_cspi_traits1_fit.rds",                "bernoulli"
)
lookup <- paths_index %>% filter(model == MODEL, variant == VARIANT)
stopifnot(nrow(lookup) == 1)
FIT_PATH <- file.path(PROJ_ROOT, lookup$fit_path)
LIK      <- lookup$likelihood

# --- Load fit ---------------------------------------------------------------
cat("Loading fit:", FIT_PATH, "\n"); flush.console()
fit <- readRDS(FIT_PATH)

# --- Extract mu_a or eta_gq, sigma, and (optionally) sqrt_years ------------
cat("Extracting mu_a/eta_gq draws ..."); flush.console()
mu_draws <- tryCatch({
  fit$draws(variables = "mu_a", format = "draws_matrix")
}, error = function(e) {
  fit$draws(variables = "eta_gq", format = "draws_matrix")
})
cat(" done. Dim:", paste(dim(mu_draws), collapse = " x "), "\n")

n_obs_total <- ncol(mu_draws)
n_draws_total <- nrow(mu_draws)

# Subset draws and observations
set.seed(42)
draw_idx <- sample.int(n_draws_total, min(N_DRAWS, n_draws_total))
obs_idx  <- sort(sample.int(n_obs_total, min(SUBSAMPLE, n_obs_total)))

mu_sub <- mu_draws[draw_idx, obs_idx, drop = FALSE]
rm(mu_draws); gc()

# Sigma draws
sigma_vec <- tryCatch({
  as.numeric(fit$draws("sigma", format = "draws_matrix"))[draw_idx]
}, error = function(e) NULL)

# sqrt_years for scaled likelihoods
stan_data_path <- sub("_fit\\.rds$", "_standata.rds", FIT_PATH)
sqrt_years_vec <- NULL
obs_vec <- NULL
if (file.exists(stan_data_path)) {
  sd_list <- readRDS(stan_data_path)
  if ("sqrt_years" %in% names(sd_list)) sqrt_years_vec <- sd_list$sqrt_years[obs_idx]
  if ("dg_obs_a" %in% names(sd_list))   obs_vec        <- sd_list$dg_obs_a[obs_idx]
  if (is.null(obs_vec) && "y" %in% names(sd_list)) obs_vec <- sd_list$y[obs_idx]
}

rm(fit); gc()

# --- Posterior predictive draws ---------------------------------------------
cat("Generating posterior predictive samples ..."); flush.console()

ppc_mat <- switch(LIK,
  "lognormal_scaled" = {
    stopifnot(!is.null(sigma_vec), !is.null(sqrt_years_vec))
    sapply(seq_len(N_DRAWS), function(d) {
      rlnorm(length(obs_idx),
             meanlog = mu_sub[d, ],
             sdlog   = sigma_vec[d] / sqrt_years_vec)
    })
  },
  "lognormal" = {
    stopifnot(!is.null(sigma_vec))
    sapply(seq_len(N_DRAWS), function(d) {
      rlnorm(length(obs_idx), meanlog = mu_sub[d, ], sdlog = sigma_vec[d])
    })
  },
  "beta" = {
    # Beta likelihood: phi precision parameter from the fit
    phi_vec <- tryCatch({
      as.numeric(fit$draws("phi", format = "draws_matrix"))[draw_idx]
    }, error = function(e) rep(50, N_DRAWS))
    sapply(seq_len(N_DRAWS), function(d) {
      p <- plogis(mu_sub[d, ])
      rbeta(length(obs_idx), p * phi_vec[d], (1 - p) * phi_vec[d])
    })
  },
  "bernoulli" = {
    sapply(seq_len(N_DRAWS), function(d) {
      rbinom(length(obs_idx), 1, plogis(mu_sub[d, ]))
    })
  }
)
cat(" done. ppc_mat dim:", paste(dim(ppc_mat), collapse = " x "), "\n")

# --- Figures ----------------------------------------------------------------
theme_pub <- theme_classic(base_size = 12) +
  theme(
    panel.grid.major.y = element_line(color = "grey92"),
    panel.grid.minor   = element_blank(),
    legend.position    = "bottom",
    strip.text         = element_text(face = "bold"),
    plot.title         = element_text(face = "bold")
  )

# Panel A: density overlay (observed vs replicates)
if (!is.null(obs_vec)) {
  ppc_long <- as_tibble(ppc_mat) %>%
    setNames(paste0("rep_", seq_len(ncol(ppc_mat)))) %>%
    mutate(obs_idx = seq_along(obs_vec)) %>%
    pivot_longer(-obs_idx, names_to = "rep", values_to = "value")
  obs_df <- tibble(value = obs_vec, type = "Observed")

  p_density <- ggplot() +
    geom_density(data = ppc_long, aes(value, group = rep),
                 color = "#3B499222", linewidth = 0.3) +
    geom_density(data = obs_df, aes(value),
                 color = "#EE0000", linewidth = 0.8) +
    coord_cartesian(xlim = quantile(obs_vec, c(0.01, 0.99), na.rm = TRUE)) +
    labs(x = "Response value", y = "Density",
         title = sprintf("A) Density overlay: %s %s", MODEL, VARIANT),
         subtitle = "Light lines: posterior predictive replicates. Red line: observed.") +
    theme_pub
} else {
  p_density <- ggplot() + theme_void() + labs(title = "A) (observations not available)")
}

# Panel B: posterior predictive interval coverage
if (!is.null(obs_vec)) {
  pp_q5  <- apply(ppc_mat, 1, quantile, 0.05, na.rm = TRUE)
  pp_q25 <- apply(ppc_mat, 1, quantile, 0.25, na.rm = TRUE)
  pp_q50 <- apply(ppc_mat, 1, quantile, 0.50, na.rm = TRUE)
  pp_q75 <- apply(ppc_mat, 1, quantile, 0.75, na.rm = TRUE)
  pp_q95 <- apply(ppc_mat, 1, quantile, 0.95, na.rm = TRUE)
  cover_90 <- mean(obs_vec >= pp_q5 & obs_vec <= pp_q95, na.rm = TRUE)
  cover_50 <- mean(obs_vec >= pp_q25 & obs_vec <= pp_q75, na.rm = TRUE)

  cov_df <- tibble(
    obs = obs_vec,
    pred_q5 = pp_q5, pred_q25 = pp_q25, pred_q50 = pp_q50,
    pred_q75 = pp_q75, pred_q95 = pp_q95
  ) %>% arrange(pred_q50) %>%
    mutate(rank = row_number())

  p_coverage <- ggplot(cov_df) +
    geom_ribbon(aes(rank, ymin = pred_q5, ymax = pred_q95),
                fill = "#3B4992", alpha = 0.2) +
    geom_ribbon(aes(rank, ymin = pred_q25, ymax = pred_q75),
                fill = "#3B4992", alpha = 0.4) +
    geom_line(aes(rank, pred_q50), color = "#3B4992", linewidth = 0.4) +
    geom_point(aes(rank, obs), color = "#EE0000", alpha = 0.3, size = 0.4) +
    labs(x = "Observation rank (sorted by predicted median)", y = "Response value",
         title = "B) Posterior predictive intervals",
         subtitle = sprintf("Coverage: 90%% PI = %.1f%% (target 90); 50%% PI = %.1f%% (target 50)",
                            100*cover_90, 100*cover_50)) +
    theme_pub
} else {
  p_coverage <- ggplot() + theme_void() + labs(title = "B) (observations not available)")
}

# Combine
ppc_fig <- p_density / p_coverage +
  plot_annotation(title = sprintf("Posterior predictive check: %s %s",
                                  MODEL, VARIANT))

fig_png <- file.path(OUT_DIR, "ppc.png")
fig_pdf <- file.path(OUT_DIR, "ppc.pdf")
ggsave(fig_png, ppc_fig, width = 20, height = 22, units = "cm",
       dpi = 300, bg = "white")
ggsave(fig_pdf, ppc_fig, width = 20, height = 22, units = "cm")
cat("Wrote", fig_png, "\n")
cat("Wrote", fig_pdf, "\n")

# --- Summary CSV ------------------------------------------------------------
if (!is.null(obs_vec)) {
  ppc_summary <- tibble(
    model       = MODEL,
    variant     = VARIANT,
    n_obs       = length(obs_vec),
    n_draws     = N_DRAWS,
    obs_mean    = mean(obs_vec, na.rm = TRUE),
    rep_mean    = mean(ppc_mat, na.rm = TRUE),
    obs_sd      = sd(obs_vec, na.rm = TRUE),
    rep_sd_mean = mean(apply(ppc_mat, 2, sd, na.rm = TRUE)),
    cover_90    = cover_90,
    cover_50    = cover_50
  ) %>% mutate(across(where(is.numeric), ~round(., 4)))
  write_csv(ppc_summary, file.path(OUT_DIR, "ppc_summary.csv"))
  cat("\n=== PPC summary ===\n"); print(ppc_summary)
}

cat("\nDone.\n")
