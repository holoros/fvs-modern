SUBROUTINE EXMPB
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     EXTRA EXTERNAL REFERENCES FOR MPB CALLS
!
LOGICAL L,LNOTBK,LKECHO
CHARACTER*8 KEYWRD,NOMPB
CHARACTER*26 NPLT
DIMENSION LNOTBK(12)
INTEGER ICODES(6)
REAL ARRAY(12)
INTEGER MGMID,II,KEY
REAL RDANUW
INTEGER IDANUW
CHARACTER*8 CDANUW
LOGICAL LDANUW
!
DATA NOMPB/'*NO MPB'/
!
!----------
!  ENTRY MPBINT
!----------
ENTRY MPBINT
RETURN
!
!----------
!  ENTRY MPBHED
!----------
ENTRY MPBHED (NPLT,MGMID)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  CDANUW(1:8) = NPLT(1:8)
  IDANUW = MGMID
RETURN
!
!----------
!  ENTRY MPBIN
!----------
ENTRY MPBIN (KEYWRD,ARRAY,LNOTBK,LKECHO)
  CALL ERRGRO (.TRUE.,11)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  CDANUW(1:8) = KEYWRD(1:8)
  RDANUW = ARRAY(1)
  LDANUW = LNOTBK(1)
  LDANUW = LKECHO
RETURN
!
!----------
!  ENTRY MPBDAM
!----------
ENTRY MPBDAM (II,ICODES)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  IDANUW = II
  IDANUW = ICODES(1)
RETURN
!
!----------
!  ENTRY MPBOPS
!----------
ENTRY MPBOPS
RETURN
!
!----------
!  ENTRY MPBCUP
!----------
ENTRY MPBCUP
RETURN
!
!----------
!  ENTRY MPBGO
!----------
ENTRY MPBGO (L)
  L = .FALSE.
RETURN
!
!----------
!  ENTRY MPBOUT
!----------
ENTRY MPBOUT
RETURN
!
!----------
!  ENTRY MPSDLP
!----------
ENTRY MPSDLP
RETURN
!
!----------
!  ENTRY MPKEY
!----------
ENTRY MPKEY(KEY,KEYWRD)
  KEYWRD=NOMPB
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  IDANUW = KEY
RETURN
!
END
