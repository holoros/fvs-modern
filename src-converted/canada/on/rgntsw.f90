      CALL ONSTHG(HTG2, ISPC, D, H1, SITEAR(ISPC), BAL)

      HTG2 = LOG(HTG2 * REGYR)
      H2 = H1 + EXP(HTG2)*(FLOAT(KPER(J))/REGYR)*XRHGRO
      WK3(I)=H2

