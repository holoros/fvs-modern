SUBROUTINE DBALL (ICYC)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'DBSTK.f90'
!
!
!OMMONS
!
INTEGER ICYC,IRC
!
IF ((ICYC.LT.1).OR.(ICYC.GT.MAXCYC)) THEN
  CALL DBINIT
  CALL DBADD (ALLSUB,6,0,IRC)
ELSE
  CALL DBADD (ALLSUB,6,ICYC,IRC)
ENDIF
RETURN
END
