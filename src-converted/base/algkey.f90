  IF (CTOK.EQ.CTAB1(I)) THEN
    NUM=IOPT1(I)
    GOTO 1000
  END IF
END DO
!
CASE (2)
!     THE CTAB2 TABLE IS CHECKED.
!
DO I=1,N2
  IF (CTOK.EQ.CTAB2(I)) THEN
    NUM=IOPT2(I)
    GOTO 1000
  END IF
END DO
!
CASE (3)
!     THE CTAB3 TABLE IS CHECKED.
!
DO I=1,N3
  IF (CTOK.EQ.CTAB3(I)) THEN
    NUM=IOPT3(I)
    GOTO 1000
  END IF
END DO
!
CASE (4)
!     THE CTAB4 TABLE IS CHECKED.
!
DO I=1,N4
  IF (CTOK.EQ.CTAB4(I)) THEN
    NUM=IOPT4(I)
    GOTO 1000
  END IF
END DO
!
CASE (5)
!     THE CTAB5 TABLE IS CHECKED.
!
DO I=1,N5
  IF (CTOK.EQ.CTAB5(I)) THEN
    NUM=IOPT5(I)
    GOTO 1000
  END IF
END DO
!
