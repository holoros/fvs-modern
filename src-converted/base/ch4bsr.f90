SUBROUTINE CH4BSR(N,A,IORD,F,IP)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     CHARACTER*4 KEYED BINARY SEARCH
!
!     PART OF THE MOUNTAIN PINE BEETLE DISPERSAL MODEL.
!     ADAPTED FROM INTEGER SEARCH - NL CROOKSTON - JAN 1981 - MOSCOW
!
!     DESCRIPTION :
!
!     BINARY SEARCH ROUTINE:  FINDS THE SUBSCRIPT LOCATION OF F IN
!     ARRAY A, IF IORD IS FILLED WITH THE ASCENDING ORDER INDICES
!     INTO ARRAY A, RATHER THAN THE DESCENDING ORDER INDICES.
!     ARRAY A NEED NOT BE IN ASCENDING ORDER, AS LONG AS THE
!     KEYS IN IORD ARE IN ASCENDING ORDER OVER A.
!
!     N    = THE LENGTH OF A.
!     A    = A LIST OF CHARACTER*4 STRINGS.
!     IORD = AN ARRAY OF KEYS SORTED IN ASCENDING ORDER OVER A.
!     F    = A CHARACTER*4 STRING WHICH YOU WISH TO FIND IN A.
!     IP   = THE POSITION IN A WHERE F WAS FOUND; OR
!             0 WHEN F IS NOT A MEMBER OF A.
!
CHARACTER*4 A(*),F
INTEGER IORD(*),ITOP,IBOT,IMID,IP
INTEGER N,I1,IN,IM,IB,IT
!
IMID=1
I1=IORD(1)
!
!     IF THE VALUE WE SEARCH FOR IS LESS THAN THE SMALLEST VALUE IN THE
!     ARRAY, TERMINATE THE SEARCH.
!
IF (F.LE.A(I1)) GOTO 40
IMID=N
IN=IORD(N)
!
!     IF THE VALUE WE SEARCH FOR IS GREATER THAN THE LARGEST VALUE IN
!     THE ARRAY, TERMINATE THE SEARCH.
!
IF (F.GE.A(IN)) GOTO 40
!
!     INITIALIZE THE TOP AND BOTTOM OF THE SEARCH PARTITION TO THE TOP
!     BOTTOM OF THE ARRAY.
!
ITOP=1
IBOT=N
!
!     TEST THE MIDDLE VALUE OF THE PARTITION
!
20 CONTINUE
IMID=(IBOT+ITOP)/2
IM=IORD(IMID)
!
!     IF THE MIDDLE VALUE IS GREATER THAN OR EQUAL TO THE SEARCH VALUE,
!     SET THE TOP OF THE PARTITION TO THE MIDDLE VALUE PLUS ONE.
!
IF (F.GT.A(IM)) GOTO 30
!
!     IF THE MIDDLE VALUE IS LESS THAN WHAT WE SEARCH FOR, SET THE
!     BOTTOM OF THE PARTITION TO THE MIDDLE VALUE MINUS ONE.
!
IBOT=IMID-1
IB=IORD(IBOT)
!
!     IF THE VALUE OF THE BOTTOM OF THE PARTITION IS GREATER THAN OR
!     EQUAL TO THE SEARCH VALUE, TERMINATE.
!
IF (F.GT.A(IB)) GOTO 40
!
!     RESEARCH THE PARTITION.
!
GOTO 20
!
!     SET THE TOP OF THE PARTITION TO THE MIDDLE VALUE PLUS ONE.
!
30 CONTINUE
ITOP=IMID+1
IT=IORD(ITOP)
!
!     IF THE VALUE OF THE TOP OF THE PARTITION IS LESS THAN OR EQUAL
!     TO THE SEARCH VALUE, TERMINATE.
!
IF (F.LT.A(IT)) GOTO 40
!
!     RESEARCH THE PARTITION.
!
GOTO 20
!
!     THIS IS THE EXIT POINT FOR THE ROUTINE.  IF WE GOT HERE, WE
!     EITHER FOUND THE SEARCH VALUE AS THE MIDDLE OF A PARTITION,
!     OR PARTITIONED TO THE POINT WHERE THE SEARCH VALUE COULD
!     NOT BE CONTAINED IN THE PARTITION.  AT THIS POINT, WE DONT KNOW
!     WHICH SO WE MAKE A FINAL CHECK, AND RETURN ZERO IF NOT FOUND.
!
40 CONTINUE
IP=IORD(IMID)
IF (F.NE.A(IP)) IP=0
RETURN
END
