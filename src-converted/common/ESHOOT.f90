!ODE SEGMENT ESHOOT
!----------
! COMMON $Id$
!----------
!  BE SURE TO INCLUDE ESPARM BEFORE ESHOOT.
!  ESHOOT IS FOUND IN ESTUMP AND ESUCKR.
!----------
!
INTEGER*4 ISHOOT(MAXTRE)
!
INTEGER   JSHAGE(MAXTRE)
!
REAL      ASBAR,ASTPAR
!
REAL      DSTUMP(MAXTRE),PRBREM(MAXTRE)

COMMON /ESHOOT/ ASBAR,ASTPAR,DSTUMP,ISHOOT,JSHAGE,PRBREM
!
!----------
!  VARIABLE DEFINITIONS:
!----------
!     ASBAR  -- ASPEN BASAL AREA REMOVED FOR SPROUTING EQNS
!     ASTPAR -- ASPEN TREES-PER-ACRE REMOVED FOR SPROUTING EQNS
!     DSTUMP -- DBH OF CUT OR KILLED TREE
!     ISHOOT -- A CONCATINATED 10-DIGIT NUMBER HOLDING INFORMATION ON
!               STUMPS THAT WILL SPROUT. THE FIRST 3 DIGITS IDENTIFY
!               THE DIAMETER CLASS.  THE NEXT 3 DIGITS IDENTIFY THE
!               SPECIES.  THE LAST 4 DIGITS IDENTIFY THE PLOT NUMBER.
!     JSHAGE -- HOLDS THE AGE OF SPROUTS AT THE END OF THE CYCLE
!               IN WHICH THEY WERE CREATED,(CALCULATED AS ISHAG IN
!               CUTS AND FMKILL), LOADED FROM ISHAG IN ESTUMP
!               AND USED IN ESUCKR TO CALCULATE HEIGHT
!     PRBREM -- HOLDS THE AMOUNT OF PROB REMOVED IN CUTTING THE TREE
!               RECORD.
!
!-----END SEGMENT
