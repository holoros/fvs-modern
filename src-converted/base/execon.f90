RETURN
!----------
! ENTRY ECSETP CALLED FROM GRINCR
!----------
ENTRY ECSTATUS(I3, I2, IARRAY, I1)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  IDANUW = I3
  IDANUW = I2
  IDANUW = IARRAY(1)
  IDANUW = I1
RETURN
!----------
! ENTRY ECHARV CALLED FROM CUTS
!----------
