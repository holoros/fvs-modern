CHARACTER*4   MGMIDS(MXSTND),MGM1,MGM2,NSET,CMPID,CAUTID,CLBWID
CHARACTER*26  STDIDS(MXSTND),SID1,SID2,NOTSET
CHARACTER*11  CISNUM(MXSTND),CSISN,CLISN
CHARACTER*40  SLHPLB
CHARACTER*250 SLSETS(MXSTND),SLSPLB
COMMON /PPCHAR/ CISNUM,STDIDS,MGMIDS,SLSETS,SID1,SID2,NOTSET, &
                   MGM1,MGM2,NSET,SLSPLB,SLHPLB,CSISN,CLISN,CLBWID, &
                   CMPID,CAUTID
LOGICAL LIN1ST,LASTIN,LEORUN,PDEBUG,LRESTR,LAREAS, &
           LMCYC,LSTYR,LMINT,LCMPS,LACTS,LSUM,LHIST,LBWTS, &
           LSNDCP,LSMPT,LPAUSE(MAXCYC),LHVALL,LTMPAC,LTMPAD
INTEGER ISNKEY(MXSTND,4),ISNSRT(MXSTND),IPMODE(MXSTND), &
           LNSETS(MXSTND),ISDWK1(MXSTND),INEXR(NDAREC),MIY(MAXCY1), &
           MNCYC,MICYC,MAXBUF,ISTND,NOSTND,IAVAIL,IENDFR, &
           IDAIO(MXDAFL),NDAFL,IWTREC,IRDREC,JLBWTS,JEXOPT, &
           JOHIST,INCYC,IDAPRT,NPTREC,NAVPRT,IASVPT,IPTELS,NMPRTD, &
           JCPCMP,JCPSUM,JOCOPY,IPMODI,JOPPRT,LNSSPL,LNSHPL, &
           NBYGRP,IOSTRC,NAUTID,JADFIL,IMSREC,IMSEND, &
           NDACUM(MXDAFL),NAURPS
REAL    BRCHWT(MXSTND),AREAS(MXSTND),SAMWTS(MXSTND),DCTOP,DCTPA
COMMON /PPCNTL/ ISNKEY,BRCHWT,AREAS, &
         ISNSRT,IPMODE,LNSETS,ISDWK1, &
         SAMWTS,INEXR,LPAUSE,MIY,JEXOPT, &
         MNCYC,MICYC,MAXBUF,LMINT,ISTND,NOSTND,LMCYC,LSTYR,IAVAIL, &
         IENDFR,IDAIO,NDAFL,IWTREC,IRDREC,JLBWTS,LBWTS, &
         JOHIST,INCYC,LASTIN,LEORUN,PDEBUG,LIN1ST,IDAPRT,NPTREC, &
         NAVPRT,IASVPT,LHVALL,IPTELS,NMPRTD,LCMPS,LACTS,LSUM, &
         JCPCMP,JCPSUM,JOCOPY,IPMODI,LHIST,LSNDCP,DCTOP,DCTPA, &
         LSMPT,JOPPRT,LNSSPL,LNSHPL,NBYGRP,IOSTRC,NAUTID,JADFIL, &
         LTMPAC,LTMPAD,IMSREC,IMSEND,LAREAS,LRESTR,NDACUM, &
         NAURPS
!
!     STDIDS= STAND IDENTS
!     MGMIDS= MANAGEMENT IDENTS
!     SLSETS= STAND LABEL SETS FOR ALL STANDS
!     LNSETS= LENGHT OF EACH STAND LABEL SET
!     CISNUM= INTERNAL STAND NUMBER IN FIRST 4 COLS, BRANCHING IN REST
!     ISNKEY= (I,1) ALL-DATA VERSION WHERE:
!                   UNITS PLACE
!                   1= BASE MODEL ALL-DATA
!                   2= BASE MODEL SUMMARY ARRAYS ONLY
!                   3= BASE MODEL ALL-DATA ON TEMPORARY SPACE
!                   TENS PLACE
!                   0= ESTABLISHMENT SUBSYSTEM NOT ACTIVE
!                   1= ESTABLISHMENT SUBSYSTEM ACTIVE
!                   HUNDREDS
!                   0= COVER/BROWSE SUBSYSTEM NOT ACTIVE
!                   1= COVER/BROWSE SUBSYSTEM ACTIVE
!             (I,2) DA RECORD MARKING START OF ALL-DATA LIST.
!             (I,3) DA RECORD MARKING END OF ALL DATA LIST.
!             (I,4) PROCESSING PHASE WHEN BRANCH NODE WAS CREATED.
!     BRCHWT= WEIGHTING FACTOR ASSOCIATED WITH THE STAND.
!     AREAS = STAND AREAS AS READ BY THE AREALOCS KEYWORD, OR 1.0
!     SAMWTS= STAND SAMWT FROM THE DESIGN RECORDS.
!     LAREAS= TRUE IF AREAS IS DEFINED, FALSE IF NOT OR WHEN AREALOCS
!             KEYWORD HAS BEEN SPECIFIED.
!     LRESTR= TRUE WHEN PAUSE WAS EXECUTED AFTER PROJECTION HAS
!             STARTED (ALL STANDS HAVE BEEN ENTERED), AND FALSE
!             OTHER TIMES (WHEN PAUSE WAS EXECUTED DURING THE ENTERING
!             OF STANDS).
!     NAUTID= CONTROLS AUTOMATIC GENERATION OF MGMTID'S FOR OUTPUT.
!             0=OPTION OFF, 1=OPTION ON AND PPAUID USES THE VALUE
!             OF CAUTID FOR THE FIRST AUTOID, 2=OPTION ON AND PPAUID
!             INCREMENTS CAUTID FOR THE AUTOID (PPAUID CHANGES NAUTID
!             FROM 1 TO 2 ON THE FIRST CALL).
!     CAUTID= THE FIRST AUUTID IN THE SEQUENCE (CAN BE SET BY KEYWORD)
!     IOSTRC= A ALL-DATA RECORD POINTER TO THE LAST RECORD USED TO
!             HOLD DATA FOR THE CURRENT STAND.
!     LPAUSE= TRUE IF A PROCESSING PAUSE IS SCHEDULED (INDEXED
!             BY MASTER CYCLE NUMBER).
!     ISNSRT= AN INDEXING ARRAY USED TO SORT ISNKEY.
!     ISTND = SUBSCRIPT TO ISNKEY OF CURRENT STAND.
!     NOSTND= NUMBER OF ENTERIES IN ISNKEY
!     LMCYC = TRUE IF MNCYC HAS BEEN DEFINED
!     LSTYR = TRUE IF MASTER STARTING YEAR (MIY(1)) HAS BEEN
!             DEFINED.
!     LMINT = TRUE IF MASTER TIME INTERIVALS HAVE BEEN DEFINED.
!     MIY   = MASTER VERSION OF SPS ARRAY IY
!     MNCYC = MASTER VERSION OF SPS VARIABLE NCYC
!     MICYC = MASTER VERSION OF SPS VARIABLE ICYC
!     IDAIO = ALL-DATA DA DATA SET REFERENCE NUMBERS.
!     NDAFL = NUMBER OF ALL-DATA DA FILES.
!     IWTREC= RECORD NUMBER TO BEGIN WRITING ALL-DATA.
!     IRDREC= RECORD NUMBER TO BEGIN READING ALL-DATA.
!     JOHIST= PROJECTION HISTORY DATA SET REF. NUM.
!     INCYC = THE CYCLE NUMBER OF THE CURRENT STAND WHICH
!             POINTS TO THE YEAR THE CURRENT MASTER CYCLE ENDS.
!     LASTIN= TRUE IF THE LAST STAND HAS BEEN INITIATED.
!     LEORUN= TRUE WHEN THE MASTER CLOCK SIGNALS THE LAST CYCLE.
!     PDEBUG= TRUE WHEN DEBUGGING THE PARALLEL PROCESSOR.
!     LIN1ST= TRUE FOR FIRST CALL TO PPIN FOR PROJECTION.
!     IDAPRT= DA PRINT-DATA DATA SET REFERENCE NUMBER.
!     NPTREC= NUMBER OF PRINT-DATA DA RECORDS AVAILABLE IN THE FILE.
!     NAVPRT= NEXT AVAILABLE RECORD TO WRITE PRINT-DATA ON DA FILE.
!     IASVPT= POINTS TO PRINT-DATA RECORD BEING WRITTEN.
!     LHVALL= TRUE IF MULTISTAND TREATMENT SCHEDULING IS ACTIVE.
!             (FORMALLY CALLED HARVEST SIMULATION).
!     IPTELS= PRINT-DATA DATA ELEMENT DISCRIPTION WORD WHERE:
!             UNITS PLACE
!             0= INITIAL CYCLE
!             1= NORMAL CYCLE
!             2= NORMAL CYCLE WITH REMOVAL
!             3= LAST CYCLE FOR STAND
!             TENS PLACE
!             0= NO CHEAPO OUTPUT
!             1= WITH CHEAPO OUTPUT
!             IPTELS DISCRIBES THE DATA ELEMENTS THAT ARE WRITTEN
!             ON THE PRINT-DATA DA FILE FOR A GIVEN STAND AND CYCLE.
!     MXPRTD= THE MAXIMUM NUMBER OF HISTORY RECORDS THAT CAN BE
!             READ AT ONE TIME...IN OTHER WORDS THE MAX NUMBER OF
!             CYCLES*STANDS WHICH CAN BE RETREIVED AT ONCE FOR
!             PRINT-DATA PROCESSING.
!     NMPRTD= THE NUMBER OF TYPE 2 HISTORY RECORDS READ AND STORED.
!     JOPPRT= PRINTER DSRN FOR PARALLEL PROCESSOR OUTPUT.
!     JADFIL= USED IN CONJUNCTION WITH ICL6 TO SUPPORT ADDFILE.
!     LCMPS = TRUE IF COMPOSITE YIELD TABLES ARE REQUESTED
!     LACTS = TRUE IF ACTIVITY SUMMARY WILL BE PRINTED.
!     LSUM  = TRUE IF SUMMARY DATA ARE TO BE PROCESSED FOR OUTPUT.
!     LSCOPY= TRUE IF SUMMARY DATA WHICH IS SELECTED FOR PROCESSING
!             IS ALSO COPIED TO FILE JOCOPY.
!     JOCOPY= DSRN FOR SUMMARY DATA OUTPUT FILE, WRITTEN WITHOUT
!             HEADINGS.
!     IPMODE= THE PROCESSING MODE FOR EACH STAND IN THE CURRENT CYCLE:
!             1 IF THE STAND WILL BE INDEPENTENTLY PROCESSED UP TO
!             THE END OF THE MASTER CYCLE.
!             2 IF THE STAND WILL BE ALLOWED TO INTERACT ON A ANNUAL
!             OR MORE FREQUENT BASIS.  NOTE THAT STANDS WITH A
!             PROCESSING MODE OF 2 CYCLE ONLY ONCE DURING
!             THE MASTER CYCLE.
!     IPMODI= THE PROCESSING MODE FOR THE CURRENT STAND.
!     LHIST = TRUE IF HISTORY FILE IS OPEN FOR INPUT, AND FALSE IF IS
!             OPEN FOR OUTPUT.
!     LTMPAD= CONTROLS STAND TEMPLATING
!     IMSREC= THE FIRST RECORD WHERE THE TEMPLATE IS STORED.
!     IMSEND= THE LAST RECORD WHERE THE TEMPLATE IS STORED.
!     LSNDCP= TRUE IF STAND COMPOSTION TABLE WILL BE PRINTED.
!     LSMPT = TRUE IF SAMPLE TREE AND STAND ATTRIBUTE TABLES WILL BE
!             PRINTED.
!     MGM1  = THE MANAGEMENT ID IN THE FIRST FIELD OF SELMGMID OPTION.
!     MGM2  = THE MANAGEMENT ID IN THE SELCOND FIELD OF SELMGMID.
!     SID1  = THE STAND IDENT. IN THE FIRST FIELD OF SELSTAND OPTION.
!     SID2  = THE STAND IDENT. IN THE FIRST FIELD OF SELSTAND OPTION.
!     LNSSPL= LENGTH OF STAND POLICY LABEL IN THE SELSPLAB OPTION.
!     SLSPLB= THE STAND POLICY LABEL IN THE SELSPLAB OPTION.
!     LNSHPL= LENGTH OF POLICY LABEL IN THE SELMSPLB OPTION.
!     SLHPLB= THE POLICY LABEL IN THE SELMSPLB OPTION.
!     CMPID = THE MANAGEMENT ID OF THE COMPOSITE YIELD TABLE.
!     CSISN = THE INTERNAL STAND NUMBER IN THE FIRST FIELD OF
!             THE SELISN OPTION.
!     CLISN = THE ISN IN THE SECOND FIELD OF THE SELISN OPTION.
!     IBRPH = THE PHASE WITHIN TREGRO WHEN THE BRANCH OCCURRED.
!     DCTOP = DEFINE CLEAR CUT: TOP HEIGHT
!     DCTPA = DEFINE CLEAR CUT: TREES PER ACRE
!     ISDWK1= A WORK ARRAY USED TO PROCESS LISTS OF STAND POINTERS.
!     NBYGRP= THE SWITCH SETTING FOR THE BYGRPS KEYWORDS:
!             0=NOBYGRPS, 1=BYSTAND, 2=BYMGMTID, 3=BYSPLAB, 4=BYHPLAB.
!     JLBWTS= DATA SET REF NUMBER FOR LABEL & WEIGHTS OUTPUT FILE.
!     LBWTS = TRUE IF LABWTS OPTION IS ON, FALSE OTHERWISE.
!     CLBWID= THE IDENT USED TO IN THE FIRST 4 COLS OF THE LABWTS FILE.
!     NDACUM= VECTOR LISTING THE CUM NUMBER OF RECORDS PER ALL-DATA
!             FILE. NDACUM(1)=0, (2)=NUMBER IN FILE 1, (3)=NUMBER IN
!             FILE 1 PLUS THOSE IN FILE 2... ETC.
!     NAURPS= THE NUMBER OF INITIAL REPS FOR EACH ADDSTAND, USUALLY 1,
!             BUT CAN BE UP TO 9*9*9=729
!     JEXOPT= DATA SET REFERENCE NUMBER USED TO READ THE "MORE ACTIVITES"
!             FILE.
!
!-----END SEGMENT
