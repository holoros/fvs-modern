INTEGER I_CMPU,IADDCMPU,ICMRPT,ICHRPT,ICANPR
INTEGER IOUTDBREF,IINDBREF,ICALIB,ISTATS1,ISTATS2
INTEGER IREG1,IREG2,IREG3,IREG4,IREG5,IPOTFIREC
INTEGER IVBCSUM,IVBCTRELST,IVBCCUTLST,IVBCATRLST

