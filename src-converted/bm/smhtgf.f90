SUBROUTINE SMHTGF (I,HHT,H,MODE,DTIME,ICYC,JOSTND,DEBUG)
IMPLICIT NONE
!----------
! BM $Id$
!----------
!  THIS ROUTINE CALCULATES THE HEIGHT GROWTH OF SMALL TREES (D<3.0 IN).
!  WHEN CALLED FROM ESSUBH (MODE=0), DTIME= TOTAL TREE AGE.
!  IF CALLED FROM REGENT (MODE=1), DTIME= FINT AND THE
!  CALLED FROM ***ESSUBH
!  CALLED FROM ***REGENT
!
!  INPUT VARIABLES
!  I      - SPECIES SEQUENCE NUMBER
!  H      - BEGINING HEIGHT
!  SI     - SITE INDEX FOR SPECIES
!  MODE   - = 0 IF CALL IS FROM ESSUBH, = 1 IF CALL IS FROM REGENT
!  DTIME  - DTIME=TOTAL SEEDLING AGE (MODE= 0), DTIME=FINT (MODE= 1)
!
!  RETURN VARIABLES
!  HHT    - HEIGHT GROWTH OVER TIME INCREMENT DTIME (REGENT)
!  HHT    - HEIGHT 5 YEARS INTO CYCLE, OR END OF CYCLE FINT<5 (ESSUBH)
!----------
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'PDEN.f90'
!
!
INCLUDE 'PLOT.f90'
!
!OMMONS
!
!----------
!  VARIABLE DECLARATIONS:
!----------
!
EXTERNAL RANN
!
LOGICAL  DEBUG
!
INTEGER  I,ICYC,JOSTND,MODE
!
REAL     AGEPDT,C1,C2,C3,C4,EFFAGE,DTIME,H,HHT,HHT1,HHT2
REAL     SHI,SI,SLO
!
!----------
!  SPECIES ORDER:
!   1=WP,  2=WL,  3=DF,  4=GF,  5=MH,  6=WJ,  7=LP,  8=ES,
!   9=AF, 10=PP, 11=WB, 12=LM, 13=PY, 14=YC, 15=AS, 16=CW,
!  17=OS, 18=OH
!
!  SPECIES EXPANSION:
!  WJ USES SO JU (ORIGINALLY FROM UT VARIANT; REALLY PP FROM CR VARIANT)
!  WB USES SO WB (ORIGINALLY FROM TT VARIANT)
!  LM USES UT LM
!  PY USES SO PY (ORIGINALLY FROM WC VARIANT)
!  YC USES WC YC
!  AS USES SO AS (ORIGINALLY FROM UT VARIANT)
!  CW USES SO CW (ORIGINALLY FROM WC VARIANT)
!  OS USES BM PP BARK COEFFICIENT
!  OH USES SO OH (ORIGINALLY FROM WC VARIANT)
!----------
!  IF NO GROWTH IS TO BE CALCULATED ON ESTAB TREES, I.E. PLANTING
!  IS MORE THAN 5 YEARS INTO THE SIMULATION, THEN RETURN AND
!  LEAVE THE SMALL TREE GROWTH, DURING THE PLANTING CYCLE, TO
!  THE REGENT ROUTINE.
!----------
HHT = 0.
IF(DTIME .LE. 0.0) GO TO 120
!
SI= SITEAR(I)
!
SELECT CASE (I)
!----------
!  HEIGHT OF TALLEST SUBSEQUENT SPECIES 1 (WHITE PINE)
!
!  FOR NON-LINEAR HEIGHT GROWTH MODEL SOLVE FIRST FOR EFFECTIVE
!  AGE BASED ON INPUT HEIGHT.
!----------
CASE(1)
  C1= 0.375045
  C2= 0.92503
  C3= -0.020796
  C4= 2.48811
!
  IF(MODE .EQ. 0) THEN
    EFFAGE= 0.
    AGEPDT= EFFAGE + DTIME
  ELSEIF(MODE .EQ. 1) THEN
    EFFAGE= ALOG( (1.0 - (C1/SI * H)**(1/C4))/C2 ) / C3
    AGEPDT= EFFAGE + DTIME
  ENDIF
!
  HHT1 = (SI/C1)*(1.0-C2*EXP(C3*EFFAGE))**C4
  HHT2 = (SI/C1)*(1.0-C2*EXP(C3*AGEPDT))**C4
  HHT= HHT2 - HHT1
!----------
!  HEIGHT OF TALLEST SUBSEQUENT SPECIES 2 (WESTERN LARCH)
!----------
CASE(2)
  HHT = ((-3.9725 + 0.50995*SI)/(28.1168 - 0.05661*SI))*DTIME
!----------
!  HT OF TALLEST SUBS. SPECIES 3 (DOUGLAS-FIR)
!----------
CASE(3)
  HHT = ((2.0 + 0.420*SI)/(28.5 - 0.05*SI))*DTIME
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 4 (GRAND FIR)
!----------
CASE(4)
  HHT = ((4.2435 + 0.1510*SI)/(19.0184 - 0.0570*SI))*DTIME
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 5 (MOUNTAIN HEMLOCK)
!  IN METERS CONVERTED TO FEET
!----------
CASE(5)
  HHT = ((0.965758 + 0.082969*SI)/(55.249612 - 1.288852*SI))*DTIME &
           *3.280833
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 6 (WESTERN JUNIPER)
!----------
CASE(6)
  SHI=75.
  SLO=5.
  SI=SITEAR(I)
  IF(SI .GT. SHI) SI=SHI
  IF(SI .LE. SLO) SI=SLO + 0.5
  HHT = (SI/5.0)*(SI*1.5-H)/(SI*1.5)
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 7 (LODGEPOLE PINE)
!----------
CASE(7)
  HHT = (0.02008805*SI)*DTIME
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 8 (ENGELMANN SPRUCE)
!----------
CASE(8)
  HHT = ((0.09211 + 0.208517*SI)/(43.358 - 0.168166*SI))*DTIME
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 9 (SUBALPINE FIR)
!----------
CASE(9)
  HHT = ((6.0 + 0.14*SI)/(33.882 - 0.06588*SI))*DTIME
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 10 (PONDEROSA PINE) AND
!                           SPECIES 17 (OTHER SOFTWOODS)
!----------
CASE(10,17)
  HHT = ((-1.0 + 0.32857*SI)/(28.0 - 0.042857*SI))*DTIME
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 11 (WHITEBARK PINE)
!----------
CASE(11)
  HHT = ((0.02008805*SI)*DTIME)*1.6
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 12 (LIMBER PINE)
!----------
CASE(12)
  HHT = 0.5
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 13 (PACIFIC YEW)
!----------
CASE(13)
  HHT = ((1.47043 + 0.23317*SI)/(31.56252 - 0.05586*SI))*DTIME
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 14 (ALASKA CEDAR)
!----------
CASE(14)
  HHT = ((1.47043 + 0.23317*SI)/(31.56252 - 0.05586*SI))*DTIME
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 15 (QUAKING ASPEN)
!----------
CASE(15)
  HHT = 5.0
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 16 (BLACK COTTONWOOD)
!----------
CASE(16)
  HHT = ((1.47043 + 0.23317*SI)/(31.56252 - 0.05586*SI))*DTIME
!----------
!  HT OF TALLEST SUBSEQUENT SPECIES 18 (OTHER HARDWOODS)
!----------
CASE(18)
  HHT = ((1.47043 + 0.23317*SI)/(31.56252 - 0.05586*SI))*DTIME
!
END SELECT
!
120 CONTINUE
IF (DEBUG) &
    WRITE(JOSTND,*)' LEAVING SMHTGF, ICYC,I,HHT,DTIME,SI= ', &
   ICYC,I,HHT,DTIME,SI
!
RETURN
END
