SUBROUTINE CCFCAL(ISPC,D,H,JCR,P,LTHIN,CCFT,CRWDTH,MODE)
IMPLICIT NONE
!----------
! UT $Id$
!----------
!  THIS ROUTINE COMPUTES CROWN WIDTH AND CCF FOR INDIVIDUAL TREES.
!  CALLED FROM DENSE, PRTRLS, SSTAGE, AND CVCW.
!
!  ARGUMENT DEFINITIONS:
!    ISPC = NUMERIC SPECIES CODE
!       D = DIAMETER AT BREAST HEIGHT
!       H = TOTAL TREE HEIGHT
!     JCR = CROWN RATIO IN PERCENT (0-100)
!       P = TREES PER ACRE
!   LTHIN = .TRUE. IF THINNING HAS JUST OCCURRED
!         = .FALSE. OTHERWISE
!    CCFT = CCF REPRESENTED BY THIS TREE
!  CRWDTH = CROWN WIDTH OF THIS TREE
!    MODE = 1 IF ONLY NEED CCF RETURNED
!           2 IF ONLY NEED CRWDTH RETURNED
!----------
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
!OMMONS
!----------
!  DIMENSION AND DATA STATEMENTS FOR INTERNAL VARIABLES.
!
!     CCF COEFFICIENTS FOR TREES THAT ARE GREATER THAN OR
!     EQUAL TO 1.0 IN. DBH:
!      RD1 -- CONSTANT TERM IN CROWN COMPETITION FACTOR EQUATION,
!             SUBSCRIPTED BY SPECIES
!      RD2 -- COEFFICIENT FOR SUM OF DIAMETERS TERM IN CROWN
!             COMPETITION FACTOR EQUATION,SUBSCRIPTED BY SPECIES
!      RD3 -- COEFFICIENT FOR SUM OF DIAMETER SQUARED TERM IN
!             CROWN COMPETITION EQUATION, SUBSCRIPTED BY SPECIES
!
!     CCF COEFFICIENTS FOR TREES THAT ARE LESS THAN 1.0 IN. DBH AND
!     GREATER THAN 0.10 IN. DBH:
!      RDA -- MULTIPLIER.
!      RDB -- EXPONENT.  CCF(I) = RDA*DBH**RDB
!
!
!  SPECIES ORDER:
!  1=WB,  2=LM,  3=DF,  4=WF,  5=BS,  6=AS,  7=LP,  8=ES,  9=AF, 10=PP,
! 11=PI, 12=WJ, 13=GO, 14=PM, 15=RM, 16=UJ, 17=GB, 18=NC, 19=FC, 20=MC,
! 21=BI, 22=BE, 23=OS, 24=OH
!
! VARIANT EXPANSION:
! GO AND OH USE OA (OAK SP.) EQUATIONS FROM UT
! PM USES PI (COMMON PINYON) EQUATIONS FROM UT
! RM AND UJ USE WJ (WESTERN JUNIPER) EQUATIONS FROM UT
! GB USES BC (BRISTLECONE PINE) EQUATIONS FROM CR
! NC, FC, AND BE USE NC (NARROWLEAF COTTONWOOD) EQUATIONS FROM CR
! MC USES MC (CURL-LEAF MTN-MAHOGANY) EQUATIONS FROM SO
! BI USES BM (BIGLEAF MAPLE) EQUATIONS FROM SO
! OS USES OT (OTHER SP.) EQUATIONS FROM UT
!
!
!  SOURCES OF CCF COEFFICIENTS:
!     1 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!     2 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!     3 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: DOUGLAS-FIR
!     4 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: GRAND FIR
!     5 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: ENGELMANN SPRUCE
!     6 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN RED CEDAR
!     7 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!     8 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: ENGELMANN SPRUCE
!     9 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: SUBALPINE FIR
!    10 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: PONDEROSA PINE
!    11 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    12 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    13 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN HEMLOCK
!    14 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    15 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    16 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    17 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    18 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN HEMLOCK
!    19 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN HEMLOCK
!    20 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    21 = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    22 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN HEMLOCK
!    23 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    24 = NORTHERN IDAHO VARIANT INT-133 TABLE 8: WESTERN HEMLOCK
!
!      WYKOFF, CROOKSTON, STAGE, 1982. USER'S GUIDE TO THE STAND
!        PROGNOSIS MODEL. GEN TECH REP INT-133. OGDEN, UT:
!        INTERMOUNTAIN FOREST AND RANGE EXP STN. 112P.
!
!----------
LOGICAL LTHIN
REAL RD1(MAXSP),RD2(MAXSP),RD3(MAXSP),RDA(MAXSP),RDB(MAXSP)
REAL CRWDTH,CCFT,P,H,D,BREAK
INTEGER MODE,JCR,ISPC
REAL RDANUW
LOGICAL LDANUW
INTEGER IDANUW
!
DATA RD1 / .01925, .01925,    .11,    .04,    .03, &
                 .03, .01925,    .03,    .03,    .03, &
              .01925, .01925,    .03, .01925, .01925, &
              .01925, .01925,    .03,    .03,  .0204, &
               .0204,    .03, .01925,    .03/
!
DATA RD2 / .01676, .01676,  .0333,  .0270,  .0173, &
               .0238, .01676,  .0173,  .0216,  .0180, &
              .01676, .01676,  .0215, .01676, .01676, &
              .01676, .01676,  .0215,  .0215,  .0246, &
               .0246,  .0215, .01676,  .0215/
!
DATA RD3 / .00365, .00365, .00259, .00405, .00259, &
              .00490, .00365, .00259, .00405, .00281, &
              .00365, .00365, .00363, .00365, .00365, &
              .00365, .00365, .00363, .00363,  .0074, &
               .0074, .00363, .00365, .00363/
DATA RDA/ &
    0.009187, 0.009187, 0.017299, 0.015248, 0.007875, &
    0.008915, 0.009187, 0.007875, 0.011402, 0.007813, &
    0.009187, 0.009187, 0.011109, 0.009187, 0.009187, &
    0.009187, 0.009187, 0.011109, 0.011109,       0., &
          0., 0.011109, 0.009187, 0.011109/
!
DATA RDB/ &
      1.7600,   1.7600,   1.5571,   1.7333,   1.7360, &
      1.7800,   1.7600,   1.7360,   1.7560,   1.7680, &
      1.7600,   1.7600,   1.7250,   1.7600,   1.7600, &
      1.7600,   1.7600,   1.7250,   1.7250,       0., &
          0.,   1.7250,   1.7600,   1.7250/
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
RDANUW = H
IDANUW = JCR
IDANUW = MODE
LDANUW = LTHIN
!----------
!  INITIALIZE VARIABLES.
!----------
CCFT = 0.
CRWDTH = 0.
SELECT CASE (ISPC)
CASE(17:19,22)
  BREAK=10.
CASE DEFAULT
  BREAK=1.
END SELECT
!----------
!  COMPUTE CCF
!----------
IF (D .GE. BREAK) THEN
  CCFT = RD1(ISPC) + D*RD2(ISPC) + D*D*RD3(ISPC)
ELSEIF (D .GT. 0.1) THEN
  IF(ISPC.EQ.20 .OR. ISPC.EQ.21)THEN
    CCFT = D * (RD1(ISPC)+RD2(ISPC)+RD3(ISPC))
  ELSE
    CCFT = RDA(ISPC) * (D**RDB(ISPC))
  ENDIF
ELSE
  IF(ISPC.EQ.20 .OR. ISPC.EQ.21)THEN
    CCFT = D * (RD1(ISPC)+RD2(ISPC)+RD3(ISPC))
  ELSE
    CCFT = 0.001
  ENDIF
ENDIF
!----------
!  COMPUTE CROWN WIDTH
!----------
CRWDTH = SQRT(CCFT/0.001803)
IF(CRWDTH .GT. 99.9) CRWDTH=99.9
!
CCFT = CCFT * P
!
RETURN
END
