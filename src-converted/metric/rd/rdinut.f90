IF (LMTRIC) THEN
   IF(LKECHO)WRITE(JOSTND,3811) KEYWRD, (DEDAGE(J),J=1,5)
ELSE
   IF(LKECHO)WRITE(JOSTND,3801) KEYWRD, (DEDAGE(J),J=1,5)
