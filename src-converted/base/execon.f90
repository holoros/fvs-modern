RETURN
!----------
! ENTRY ECIN CALLED FROM INITRE
!----------
ENTRY ECIN(IRECNT,I1,I2,CARRAY2,I3,LKECHO,IARRAY)
  CALL ERRGRO (.TRUE.,11)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
