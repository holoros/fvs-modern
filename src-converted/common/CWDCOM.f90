!ODE SEGMENT CWDCOM
!----------
! COMMON $Id$
!----------
!
LOGICAL LSPCWE(MAXSP)
!
REAL    CWDL0(MAXSP),CWDL1(MAXSP),CWDL2(MAXSP),CWDL3(MAXSP), &
           CWDS0(MAXSP),CWDS1(MAXSP),CWDS2(MAXSP),CWDS3(MAXSP), &
           CWTDBH(MAXSP)
!
COMMON /CWDCOM/ CWDL0,CWDL1,CWDL2,CWDL3, &
                   CWDS0,CWDS1,CWDS2,CWDS3,CWTDBH,LSPCWE
!
!----------
!  DEFINITIONS OF VARIABLES IN 'CWDCOM' COMMON BLOCK:
!----------
!
!   CWDL0 -- USER-DEFINED COEFFICIENTS IN THE CROWN WIDTH EQUATION.
!   CWDL1      CW = CWD0 + CWD1 + CWD2*DBH**CWD3
!   CWDL2    FOR TREES WITH DBH GREATER THAN OR EQUAL TO
!   CWDL3    THE TRANSITION SIZE.
!
!   CWDS0 -- USER-DEFINED COEFFICIENTS IN THE CROWN WIDTH EQUATION.
!   CWDS1      CW = CWD0 + CWD1 + CWD2*DBH**CWD3
!   CWDS2    FOR TREES WITH DBH LESS THAN THE TRANSITION SIZE.
!   CWDS3
!
!  CWTDBH -- CROWN WIDTH TRANSITION DBH FOR SWITCHING BETWEEN THE
!            SMALL TREE EQUATIONS AND LARGE TREE EQUATIONS
!  LSPCWE -- LOGICAL VARIABLE.
!             .TRUE. INDICATES THE USER HAS INPUT THEIR OWN EQUATIONS
!                    FOR THIS SPECIES.
!            .FALSE. INDICATES THE DEFAULT EQUATIONS SHOULD BE USED.
!
!-----END SEGMENT
