!
!   21 = JUOC     WESTERN JUNIPER           JUNIPERUS OCCIDENTALIS
!   22 = PIBR     BREWER SPRUCE             PICEA BREWERIANA
!   23 = SEGI2    GIANT SEQUOIA             SEQUOIADENDRON GIGANTIUM
!   24 = TABR2    PACIFIC YEW               TAXUS BREVIFOLIA
!
!   25 =          OTHER SOFTWOODS
!
!
!
!   26 = QUAG     COAST LIVE OAK            QUERCUS AGRIFOLIA
!   27 = QUCH2    CANYON LIVE OAK           QUERCUS CHRYSOLEPSIS
!   28 = QUDO     BLUE OAK                  QUERCUS DOUGLASII
!   29 = QUEN     ENGELMANN OAK             QUERCUS ENGELMANNI
!   30 = QUGA4    OREGON WHITE OAK          QUERCUS GARRYANA
!   31 = QUKE     CALIFORNIA BLACK OAK      QUERCUS KELLOGGII
!   32 = QULO     VALLEY WHITE OAK          QUERCUS LOBATA
!   33 = QUWI2    INTERIOR LIVE OAK         QUERCUS WISLIZENII
!
!   34 = ACMA3    BIGLEAF MAPLE             ACER MACROPHYLLUM
!   35 = AECA     CALIFORNIA BUCKEYE        AESCULUS CALIFORNICA
!   36 = ALRU2    RED ALDER                 ALNUS RUBRA
!   37 = ARME     PACIFIC MADRONE           ARBUTUS MENZIESII
!   38 = CHCHC4   GIANT CHINQUAPIN          CHRYSOLEPIS CHRYSOPHYLLA
