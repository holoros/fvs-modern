CHARACTER*26 CNBID1(MXNBS),CNBID2(MXNBS)
COMMON / PPNBCC / CNBID1,CNBID2
INTEGER NBORD(MXNBS),NBID2(MXNBS),KEYLST(MXSTND,2),NNBS,NIDS
REAL    BORDER(MXNBS),EDGE(MXSTND)
COMMON / PPSPNB / BORDER,EDGE,NBORD,NBID2,KEYLST,NNBS,NIDS
!
!     CNBID1= ALPHANUMERICLY SMALLEST ID.
!     CNBID2= ALPHANUMERICLY LARGEST ID.
!     BORDER= THE LENGTH OF COMMON BORDER BETWEEN CNBID1(I) AND
!             CNBID2(I)
!     EDGE  = THE AMOUNT OF EDGE OF STAND I.
!     NBORD = POINTS TO CNBID1 IN ALPHANUMERIC ASCENDING ORDER.
!     NBID2 = SUBSCRIPT EQUIVALENTS TO CNBID2
!     KEYLST= KEYLST(I,1) POINTS TO THE FIRST POSITION IN NBORD
!             FOR THE I TH ACCESS ID...MXNBS OTHERWISE.
!             KEYLST(I,2) POINTS TO THE LAST POSITION IN NBORD.
!             WHEN KEYLST(I,2) IS NEGETIVE, ITS ABSOLUTE VALUE
!             POINTS TO A ROW IN KEYLST FORWHICH THE RULES APPLY.
!             THIS ALLOWS SPNBEX TO EXTRACT POINTERS FOR A LIST
!             OF STANDS THAT CONTAINS DUPLICATES.
!     NNBS  = THE NUMBER OF BORDER SEGMENTS IN THE LISTS.
!     NIDS  = THE NUMBER OF IDS IN THE LIST.
!
!-----END SEGMENT
