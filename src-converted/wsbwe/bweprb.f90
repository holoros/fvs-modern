SUBROUTINE BWEPRB
IMPLICIT NONE
!----------
! WSBWE $Id$
!----------
!
!     FINAL ADJUSTMENT TO PRBIO ARRAY FOR THE END OF THE CYCLE.
!
!     PART OF THE WESTERN SPRUCE BUDWORM MODEL/PROGNOSIS LINKAGE CODE.
!     N.L. CROOKSTON--FORESTRY SCIENCES LAB, MOSCOW, ID--JANUARY 1984
!
!     minor changes by K.Sheehan 7/96 to remove LBWDEB
!
!     CALLED FROM :
!
!       BWECUP - SINGLE STAND BUDWORM MODEL LINK TO PROGNOSIS.
!
!     SUBROUTINES CALLED :
!
!       BWECRC - DETERMINE CROWN CLASS OF TREE.
!
!  Revision History:
!    14-JUL-2010 Lance R. David (FMSC)
!       Previouse noted revision 06/19/84
!       Added IMPLICIT NONE and declared variables as needed.
!----------
!
!OMMONS
!
INCLUDE 'PRGPRM.f90'
INCLUDE 'ARRAYS.f90'
INCLUDE 'CONTRL.f90'
INCLUDE 'BWESTD.f90'
INCLUDE 'BWECOM.f90'
!
!OMMONS
!
LOGICAL LTOLG,LTOMD
INTEGER I, IC1NX, IC2NX, ICRC1, ICRC2, ICROWN, IFAGE, &
           IHOST, ISPI, ISZI, ISZNX, ITCLS
REAL BASWT, BIO, DIV, HTI, TRFBMS(MAXTRE), TRNSFL(6,9,4,3)


EQUIVALENCE (WK6,TRNSFL),(WK4,TRFBMS(1))
!
!     SET DATE OF APPLICABILITY FOR THE RETAINED
!     PROPORTION OF BIOMASS ARRAY.
!
IPRBYR=IBWYR2+1
!
!     RECOMPUTE THE RETAINED PROPORTION ARRAY AND COMPUTE THE PROPORTION
!     OF POTENTIAL FOLIAGE CURRENTLY HELD AS ADJUSTED POTENTIAL.
!
DO 110 IHOST=1,6
IF (IFHOST(IHOST).EQ.0) GOTO 110
DO 105 ICROWN=1,9
!
!     RECOMPUTE THE RETAINED PROPORTION ARRAY
!
DIV=FOLADJ(IHOST,ICROWN,1)
PRBIO(IHOST,ICROWN,1)=.0
IF (DIV.GT..00001) PRBIO(IHOST,ICROWN,1)=FNEW(ICROWN,IHOST)/DIV
DIV=FOLADJ(IHOST,ICROWN,2)
PRBIO(IHOST,ICROWN,2)=.0
IF (DIV.GT..00001) PRBIO(IHOST,ICROWN,2)=FOLD1(ICROWN,IHOST)/DIV
DIV=FOLADJ(IHOST,ICROWN,3)
PRBIO(IHOST,ICROWN,3)=.0
IF (DIV.GT..00001) PRBIO(IHOST,ICROWN,3)=FOLD2(ICROWN,IHOST)/DIV
DIV=FOLADJ(IHOST,ICROWN,4)
PRBIO(IHOST,ICROWN,4)=.0
IF (DIV.GT..00001) PRBIO(IHOST,ICROWN,4)=FREM (ICROWN,IHOST)/DIV
!
!     COMPUTE PROPORTION OF POTENTIAL.
!
DO 100 IFAGE=1,4
POFPOT(IHOST,ICROWN,IFAGE)=1.0
DIV=FOLPOT(IHOST,ICROWN,IFAGE)
IF (DIV.GT..00001) POFPOT(IHOST,ICROWN,IFAGE)= &
                      FOLADJ(IHOST,ICROWN,IFAGE)/DIV
100 CONTINUE
105 CONTINUE
110 CONTINUE
!
!     ****** FINAL ADJUSTMENT TO PRBIO ******
!
!     ADJUST PRBIO SUCH THAT THE ESTIMATES OF DEFOLIATION WILL
!     APPLY DURING THE NEXT CYCLE EVEN THOUGH THE TREES WHICH MAKE UP
!     THE CURRENT CLASSES MAY MOVE INTO THE NEXT CLASS.  THIS ADJUSTMENT
!     IS MADE BY FIRST COMPUTING A FOLIAGE TRANSITION ARRAY WHICH
!     CONTAINS THE AMOUNT OF FOLIAGE EXPECTED TO MOVE INTO ANOTHER
!     TREE CLASS.  THEN, THE CURRENT VALUES OF PRBIO (PROPORTION OF
!     RETAINED BIOMASS) ARE RECOMPUTED FOR THE CROWNS CLASS MAKING UP
!     THE TWO LARGEST TREE CLASSES.  THE SMALL-TREE CROWN CLASSES ARE
!     LEFT AS IS.  THE COMPUTATION IS AN AVERAGE PRBIO FOR EACH LARGER
!     TREE CROWN CLASS.  THE AVERAGE IS A WEIGHTED AVERAGE WHERE THE
!     WEIGHTS ARE THE CURRENT BIOMASS IN THE CLASS AND THE BIOMASS
!     EXPECTED TO MOVE INTO THE CLASS.   THE PRBIO ESTIMATES FOR THE
!     BIOMASS WHICH IS LIKELY TO MOVE ARE THUS AVERAGED WITH THOSE
!     FOR THE BIOMASS WHICH WILL NOT MOVE.
!
!     INITIALIZE LOGICAL FLAGS, AND INITIALIZE THE TRANSITION ARRAY
!     WHICH IS EQUIVALENT TO WK6 IN MEMORY.
!
LTOLG=.FALSE.
LTOMD=.FALSE.
DO 120 I=1,648
WK6(I)=0.
120 CONTINUE
!
!     DO FOR ALL TREES.
!
IF (ITRN.LE.0) GO TO 160
DO 150 I=1,ITRN
!
!     LOAD THE SPECIES CODE.
!
ISPI=ISP(I)
IHOST=IBWSPM(ISPI)
!
!     IF TREE IS NON-HOST, BYPASS THE CALCULATIONS.
!
IF (IHOST.GE.7) GOTO 150
!
!     FIND THE TREE CLASS FOR THE TREE THIS DURING THIS CYCLE AND
!     THE ESTIMATE FOR THE TREE NEXT CYCLE.
!
HTI=HT(I)
CALL BWECRC (HTI,ISZI,ICRC1,ICRC2)
CALL BWECRC (HTI+HTG(I),ISZNX,IC1NX,IC2NX)
!
!     IF ISZI EQUALS ISZNX, THE TREE WILL NOT MOVE CLASSES, THEN:
!     SKIP THE REMAINING CALCULATIONS FOR THE TREE.
!
IF (ISZI.EQ.ISZNX) GOTO 150
!
!     SET THE TRANSITION CLASS INDEX, ISZI.
!     ITCLS=1 IF THE TREE MOVED FROM THE SMALL CLASS TO THE MID.
!           2 IF THE TREE MOVED FROM THE MID CLASS TO THE LARGE.
!           3 IF THE TREE MOVED FROM THE SMALL CLASS TO THE LARGE.
!
ITCLS=1
IF (ISZI.EQ.1 .AND. ISZNX.EQ.2) GOTO 130
ITCLS=2
IF (ISZI.EQ.2 .AND. ISZNX.EQ.3) GOTO 130
ITCLS=3
130 CONTINUE
!
!     SET LOGICAL FLAG INDECATING THAT AT LEAST SOME FOLIAGE MOVED.
!
LTOLG=LTOLG .OR. ITCLS.GE.2
LTOMD=LTOMD .OR. ITCLS.EQ.1
!
!     USE THE TOTAL TREE BIOMASS ESTIMATE FOR THE TREE AS THE BEGINNING
!     OF THE CYCLE IN COMPUTING THE WEIGHT.  NOTE THAT 1120.85921=
!     453.6*2.47103, WHICH IS G/HA = G/POUND  * ACRE/HA. THUS BIO IS IN
!     UNITS OF GRAMS PER HA.
!
BIO=TRFBMS(I)*PROB(I)*1120.85921
!
!     IF THE TREE MOVED FROM THE MID TREE CLASS, REDUCE THE NUMBER OF
!     TREES PER HA IN THIS CLASS.  THIS STEP IS NOT NECESSARY FOR THE
!     LARGE TREES BECAUSE FOLIAGE DOES NOT MOVE FROM THE LARGE CLASS,
!     AND IT IS NOT NECESSARY FOR THE SMALL CLASS BECAUSE FOLIAGE DOES
!     NOT ENTER THE CLASS FROM ANOTHER CLASS.
!
IF (ITCLS.EQ.2) BWTPHA(IHOST,ISZI)=BWTPHA(IHOST,ISZI) &
                                     - (PROB(I)* 2.47103)
!
!     ACCUMULATE THE FOLIAGE TRANSFERING FROM CLASS TO CLASS.
!
DO 140 IFAGE=1,4
DO 140 ICROWN=1,9
TRNSFL(IHOST,ICROWN,IFAGE,ITCLS)=TRNSFL(IHOST,ICROWN,IFAGE,ITCLS) &
         + (THEOFL(IFAGE,ICROWN)*PRCRN3(ICROWN)*BIO)
140 CONTINUE
150 CONTINUE
160 CONTINUE
!
!     IF THERE WERE NO TRANSITIONS, SKIP ALL THE REST OF THIS SECTION.
!
IF (.NOT.(LTOLG .OR. LTOMD)) RETURN
!
!     WRITE THE FOLIAGE TRANSITION ARRAYS (DEBUG).
!
!     COMPUTE THE WEIGHTED AVERAGE DEFOLIATION IN EACH CROWN CLASS.
!
DO 320 IHOST=1,6
IF (IFHOST(IHOST).EQ.0) GOTO 320
DO 310 IFAGE=1,4
!
!     FIRST DO CROWN CLASSES 7 TO 9 (LARGEST TREES).
!     IF THERE WERE NO TREES MOVING TO THE LARGE TREE CLASS, THEN:
!     SKIP DOWN TO THE MID CLASS.
!
IF (.NOT.LTOLG) GOTO 260
DO 230 ICROWN=7,9
BASWT=FOLPOT(IHOST,ICROWN,IFAGE)*BWTPHA(IHOST,3)
PRBIO(IHOST,ICROWN,IFAGE)=(PRBIO(IHOST,ICROWN,IFAGE)*BASWT) &
       + (TRNSFL(IHOST,ICROWN,IFAGE,2) &
       *  PRBIO (IHOST,ICROWN-3,IFAGE)) &
       + (TRNSFL(IHOST,ICROWN,IFAGE,3) &
       *  PRBIO (IHOST,ICROWN-6,IFAGE))
POFPOT(IHOST,ICROWN,IFAGE)=(POFPOT(IHOST,ICROWN,IFAGE)*BASWT) &
       + (TRNSFL(IHOST,ICROWN,IFAGE,2) &
       *  POFPOT (IHOST,ICROWN-3,IFAGE)) &
       + (TRNSFL(IHOST,ICROWN,IFAGE,3) &
       *  POFPOT (IHOST,ICROWN-6,IFAGE))
!
!     LET TRNSFL HOLD THE SUM OF THE WEIGHTS.
!
TRNSFL(IHOST,ICROWN,IFAGE,2)=TRNSFL(IHOST,ICROWN,IFAGE,2)+BASWT+ &
                                TRNSFL(IHOST,ICROWN,IFAGE,3)
230 CONTINUE
!
!     NOW DIVIDE EACH CLASS BY THE SUM OF THE WEIGHTS.  IF THE SUM
!     IS NEAR ZERO, SET THE PORPORTION RETAINED BIOMASS EQUAL 1.0
!
DO 250 ICROWN=7,9
DIV=TRNSFL(IHOST,ICROWN,IFAGE,2)
IF (DIV.LE. 0.00001) GOTO 240
PRBIO(IHOST,ICROWN,IFAGE)=PRBIO(IHOST,ICROWN,IFAGE)/DIV
POFPOT(IHOST,ICROWN,IFAGE)=POFPOT(IHOST,ICROWN,IFAGE)/DIV
GOTO 250
240 CONTINUE
PRBIO(IHOST,ICROWN,IFAGE)=1.0
POFPOT(IHOST,ICROWN,IFAGE)=1.0
250 CONTINUE
260 CONTINUE
!
!     IF THERE WAS NO TRANSITION TO MID-CLASS TREES, SKIP THESE
!     CALCULATIONS.
!
IF (.NOT.LTOMD) GOTO 300
!
!     ELSE: DO THE MID-SIZED CLASS.
!
DO 270 ICROWN=4,6
BASWT=FOLPOT(IHOST,ICROWN,IFAGE)*BWTPHA(IHOST,2)
PRBIO(IHOST,ICROWN,IFAGE)=(PRBIO(IHOST,ICROWN,IFAGE)*BASWT) &
       + (TRNSFL(IHOST,ICROWN,IFAGE,1) &
       *  PRBIO (IHOST,ICROWN-3,IFAGE))
POFPOT(IHOST,ICROWN,IFAGE)=(POFPOT(IHOST,ICROWN,IFAGE)*BASWT) &
       + (TRNSFL(IHOST,ICROWN,IFAGE,1) &
       *  POFPOT (IHOST,ICROWN-3,IFAGE))
TRNSFL(IHOST,ICROWN,IFAGE,1)=TRNSFL(IHOST,ICROWN,IFAGE,1)+BASWT
270 CONTINUE
!
!     DIVIDE EACH CLASS BY THE SUM OF WEIGHTS.
!
DO 290 ICROWN=4,6
DIV=TRNSFL(IHOST,ICROWN,IFAGE,1)
IF (DIV.LE. 0.00001) GOTO 280
PRBIO(IHOST,ICROWN,IFAGE)=PRBIO(IHOST,ICROWN,IFAGE)/DIV
POFPOT(IHOST,ICROWN,IFAGE)=POFPOT(IHOST,ICROWN,IFAGE)/DIV
GOTO 290
280 CONTINUE
PRBIO(IHOST,ICROWN,IFAGE)=1.0
POFPOT(IHOST,ICROWN,IFAGE)=1.0
290 CONTINUE
300 CONTINUE
310 CONTINUE
320 CONTINUE
!
RETURN
END
