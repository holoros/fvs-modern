SUBROUTINE TREDEL (IVACT,INDEX)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     DELETES TREE RECORDS BY MOVING TREES FROM THE BOTTOM OF THE LIST
!     TO FILL IN EMPTY TREE RECORD LOCATIONS AT THE TOP OF THE LIST.
!     VACANT RECORDS ARE LABELED BY THE CALLING ROUTINE BY SETTING
!     THE SIGN OF CORRESPONDING MEMBERS OF INDEX NEGATIVE.  THE VALUE
!     OF IVACT IS PASSED IN AS THE NUMBER OF NEGATIVE MEMBERS OF INDEX.
!
!     **** MODIFICATION:  IF ITRN-IVAC IS EQUAL TO 1 WHEN THIS ROUTINE
!     IS CALLED, THE TREE RECORD IS DUPLICATED AND THE PROB IS SPLIT
!     BETWEEN EACH DUP.  NL CROOKSTON, APRIL 1985.
!
!     **** MODIFICATION: RECORDS BEING DELETED ARE MOVED TO THE BOTTOM
!     OF THE ARRAYS RATHER THAN BEING DISCARDED. THIS MAKES THE VALUES
!     AVAILABLE FOR FURTHER CALCULATIONS (SUCH AS SPMCDBH ON CUT TREES).
!     GARY DIXON, MAY 1998.
!
!     NL CROOKSTON-FORESTRY SCIENCES LAB, MOSCOW, ID.-JUNE 1982.
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
INCLUDE 'ESTREE.f90'
!
!
!OMMONS
!
!
INTEGER INDEX(MAXTRE),IVACT,IV,IR,IVAC,IREC
!
!     IF THERE ARE NO TREE RECORDS, BRANCH TO SET IREC1 AND RETURN.
!
IF (ITRN.LE.0) GOTO 50
!
!     (A) SORT THE INDEX LIST SUCH THE VACANCY POINTERS ARE AT THE
!     TOP OF THE LIST.  THE ABSOLUTE VALUES OF THESE POINTERS POINT
!     TO THE VACANCIES IN DESCENDING ORDER.
!
CALL IQRSRT(INDEX,ITRN)
!
!     (B) UPDATE THE VISULIZATION DATA STRUTURE FOR THE DELETED
!         TREES AND THEN INITIALIZE THE DATA STRUCTURE TO
!         RE-REFERENCE DATA
!
CALL SVTDEL(INDEX,IVACT)
CALL SVCMP1
!
!     (C) INITILIZE THE POINTERS TO THE INDICES OF
!     VACANCIES AND TREES. IVACT POINTS TO THE END OF THE
!     VACANCY POINTERS.
!
IV = IVACT+1
IR = ITRN+1
10 CONTINUE
!
!     (D) DECREMENT THE INDEX TO THE VACANCY POINTERS.
!     IF THERE ARE NO MORE VACANCIES: EXIT
!
IV = IV-1
IF (IV .LT. 1) GO TO 20
!
!     (E) DECREMENT THE INDEX TO THE TREE POINTERS.
!     IF THERE ARE NO MORE TREES: EXIT
!
IR = IR-1
IF (IR .LE. IVACT) GO TO 20
!
!     (F) LOAD THE POINTERS TO THE VACANCY AND THE TREE RECORD.
!     IF THE VACANCY POINTER IS GREATER THAN THE TREE POINTER:  EXIT
!
IVAC = -INDEX(IV)
IREC = INDEX(IR)
IF (IVAC .GT. IREC) GO TO 20
!
!     (F) MOVE THE TREE FROM POSITION IREC TO POSITION IVAC.
!
CALL TREMOV (IVAC,IREC)
!
!     CALL WESTERN ROOT DISEASE MODEL VER. 3.0 SUBROUTINE TO DELETE
!     AND PACK WESTERN ROOT DISEASE TREELIST
!
CALL RDTDEL (IVAC,IREC)
!
!     CALL BLISTER RUST ROUTINE TO REMOVE BLISTER RUST RECORDS.
!
CALL BRTDEL (IVAC,IREC)
!
!     CALL THE FIRE MODEL TO MOVE ITS TREES
!
CALL FMTDEL (IVAC,IREC)
!
!     REGISTER THE MOVE WITH THE VISULIZATION DATA
!
CALL SVCMP2 (IVAC,IREC)
!
!     (G) GO ON TO THE NEXT TREE/VACANCY.
!
GO TO 10
20 CONTINUE
!
!     REBUILD THE VISULIZATION DATA POINTERS
!
CALL SVCMP3
!
!     RESET ITRN.
!
ITRN=ITRN-IVACT
50 CONTINUE
IREC1=ITRN
RETURN
END
