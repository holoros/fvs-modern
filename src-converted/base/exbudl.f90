INTEGER     I1,I2,I3,KEY
REAL        R1
CHARACTER*8 KEYWRD,NOKEY
REAL RDANUW
INTEGER IDANUW
LOGICAL LDANUW
!
DATA NOKEY/'*NO WSBE'/
!
!----------
!  ENTRY BWECUP
!----------
ENTRY BWECUP
RETURN
!
!----------
!  ENTRY BWEGO
!----------
ENTRY BWEGO (L)
