XRDGRO=XRDMLT(ISPC)
XMX=XMAX(ISPC)
XMN=XMIN(ISPC)
DGMX=DGMAX(ISPC)
IF(TTVAR)DGMX=FINT*DGMAX(ISPC)
IF(DEBUG)WRITE(16,*)'ENTERING 25 LOOP, CIVAR,TTVAR,UTVAR,', &
   'XMX,XMN,DGMX,LSKIPH,SCALE,SCALE2= ', &
   CIVAR,TTVAR,UTVAR,XMX,XMN,DGMX,LSKIPH,SCALE,SCALE2
!
DO 25 I3=I1,I2
