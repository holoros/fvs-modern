SUBROUTINE SETUP
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     SET UP THE INDEX POINTERS FOR IND1( SPECIES INDICES ).
!     THE DEFINITION OF MOST VARIABLES OCCURES IN **INTREE**
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'CONTRL.f90'
!
INTEGER IX,LAST,I,IR,KNT,ISTART,NEXT,J
!
!OMMONS
!
!
IF (IREC1.GT.0) GOTO 10
ITRN=0
RETURN
10 CONTINUE
IX= 1
LAST= 0
!
DO  300  I= 1, MAXSP
!
IR= IREF(I)
IF( IR .EQ. 0 ) GO TO 300
KNT= KOUNT(IR)
ISCT(I,1)= LAST + 1
LAST= LAST + KNT
ISCT(I,2)= LAST
ISTART= IBEGIN(I)
IND1(IX)= ISTART
IX= IX + 1
IF( KNT .EQ. 1 ) GO TO  300
NEXT= IND2( ISTART )
!
DO  299  J= 2, KNT
!
IND1(IX)= NEXT
IX= IX + 1
IF(J .EQ. KNT) GOTO 299
NEXT= IND2( NEXT )
299 CONTINUE
300 CONTINUE
!
ITRN = LAST
!
!
RETURN
END
