SUBROUTINE OPCYCL (NCYC,IY)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     OPTION PROCESSING ROUTINE - NL CROOKSTON - JAN 1981 - MOSCOW
!
!     OPCYCL ESTABLISHES THE VALUES IN ARRAY IMGPTS SUCH THAT
!     IMGPTS(ICYC,1) POINTS TO THE POSITION IN IOPSRT WHICH
!     INTURN POINTS TO THE FIRST ACTIVITY TO BE
!     ACCOMPLISHED DURING CYCLE ICYC (IF NO ACTIVITIES, THE
!     VALUE WILL BE ZERO), AND IMGPTS(ICYC,2) POINTS TO THE LAST
!     VALUE IN IOPSRT WHICH POINTS TO ACTIVITY APPLICABLE TO
!     CYCLE ICYC.
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'OPCOM.f90'
!
!
!OMMONS
!
INTEGER IY(MAXCY1),NCYC,MXACT,I,J,ILDT,IY2
!
!     SET MXACT EQUAL TO THE BOTTOM OF IACT.
!
MXACT=IMGL-1
!
!     IF THERE ARE NO ACTIVITIES;
!     THEN: SIMPLY INITIALIZE IMGPTS TO ZEROS AND RETURN.
!
IF (MXACT .GT. 0) GOTO 5
DO 1 I=1,NCYC
IMGPTS(I,1)=0
1 CONTINUE
RETURN
5 CONTINUE
!
!     ELSE:
!     LOAD THE SMALLEST DATE INTO ILDT AND INITIALIZE A COUNTER,
!     J, WHICH WILL BE USED TO STEP THRU IOPSRT.
!
J=1
ILDT=IOPSRT(1)
ILDT=IDATE(ILDT)
!
!     DO FOR ALL CYCLES
!
DO 40 I=1,NCYC
!
!     LET IY2 BE THE VALUE OF THE FIRST YEAR OF THE NEXT CYCLE.
!
IY2=IY(I+1)
!
!     IF THE SMALLEST DATE IS GE TO THE FIRST YEAR OF THE NEXT
!     CYCLE, OR IF THE ACTIVITIES LIST IS COMPLETELY ASSIGNED,
!     THEN: THE CURRENT CYCLE CONTAINS NO MANAGEMENT.
!
IF (ILDT.GE.IY2 .OR. J.GT.MXACT) GOTO 30
!
!     ELSE: SAVE THE VALUE OF J AS THE POINTER TO THE FIRST
!     POISTION IN IOPSRT WHICH POINTS TO THE FIRST ACTIVITY OF THE
!     CYCLE AND START LOOKING FOR THE LAST ACTIVITY OF THE CYCLE.
!
IMGPTS(I,1)=J
10 CONTINUE
J=J+1
!
!     IF J IS GT THE NUMBER OF ACTIVITIES;
!     THEN: THE LAST ACTIVITY IS ALSO THE LAST ONE FOR THIS CYCLE.
!
IF (J .GT. MXACT) GOTO 20
!
!     ELSE: CONTINUE SERCHING FOR THE LAST ACTIVITY FOR THIS CYCLE.
!
ILDT=IOPSRT(J)
ILDT=IDATE(ILDT)
!
!     IF THE CURRENT VALUE OF ILDT IS LESS THAN THE LAST YEAR OF THE
!     CURRENT CYCLE; THEN: WE HAVE NOT FOUND THE LAST ACTIVITY
!     APPLICABLE TO THE CYCLE.
!
IF(ILDT .LT. IY2) GOTO 10
!
!     ELSE: THE LAST ACTIVITY HAS BEEN FOUND. SAVE THE POINTER AND
!     GO ON TO THE NEXT CYCLE.
!
20 CONTINUE
IMGPTS(I,2)=J-1
GOTO 40
30 CONTINUE
IMGPTS(I,1)=0
IMGPTS(I,2)=0
40 CONTINUE
RETURN
END
