WK3(I)=H2
IF(DEBUG)WRITE(JOSTND,*)'I,H1,HTGRL,H2,KPER,REGYR,', &
   'XRHGRO,WK4,CON= ', &
   I,H1,HTGRL,H2,KPER(J),REGYR,XRHGRO,CON,WK4(I)
!----------
!  UPDATE DENSITY FOR NEXT SUBCYCLE.  IF ONLY ONE SUBCYCLE OR
