SUBROUTINE FINDAG(I,ISPC,D,D2,H,SITAGE,SITHT,AGMAX1,HTMAX,HTMAX2, &
                     DEBUG)
IMPLICIT NONE
!----------
! WC $Id$
!----------
!  THIS ROUTINE FINDS EFFECTIVE TREE AGE BASED ON INPUT VARIABLE(S)
!  CALLED FROM **COMCUP
!  CALLED FROM **CRATET
!  CALLED FROM **HTGF
!----------
!  COMMONS
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'PLOT.f90'
!
!OMMONS
!----------
!  LOCAL VARIABLE DEFINITIONS:
!----------
!  INCRNG = HAS A VALUE OF 1 WHEN THE SITE CURVE HAS BEEN MONOTONICALLY
!           INCREASING IN PREVIOUS ITERATIONS.
!   OLDHG = HEIGHT GUESS FROM PREVIOUS ITERATION.
!----------
!  DECLARATIONS
!----------
LOGICAL DEBUG
INTEGER I,ISPC,MAPPHD,MAPHD(MAXSP),INCRNG
REAL AGMAX(MAXSP),AG,DIFF,H,HGUESS,SINDX,TOLER
REAL HDRAT1(8),HDRAT2(8)
REAL SITAGE,SITHT,AGMAX1,HTMAX,HTMAX2,D,D2,OLDHG
!----------
!  DATA STATEMENTS
!----------
DATA AGMAX  / MAXSP*200. /
DATA MAPHD  /3*1,2*2,6,2,2*3,2,4,4*5,2*6,3,2*7,8*8,5*3,4*8,2*6/
DATA HDRAT1 /4.3396271,4.3149844,3.2412923,2.3475244, &
                5.5324838,6.3657425,4.0156013,3.9033821/
DATA HDRAT2 /43.9957174,39.6317079,62.7139427,65.7622908, &
                18.6043842,16.2223589,51.9732476,59.3370816/
!----------
!  INITIALIZATIONS
!----------
TOLER=2.0
SINDX = SITEAR(ISPC)
AGMAX1 = AGMAX(ISPC)
AG = 2.0
MAPPHD=MAPHD(ISPC)
HTMAX=HDRAT1(MAPPHD)*D+HDRAT2(MAPPHD)
HTMAX2=HDRAT1(MAPPHD)*D2+HDRAT2(MAPPHD)
!----------
!  CRATET CALLS FINDAG AT THE BEGINING OF THE SIMULATION TO
!  CALCULATE THE AGE OF INCOMMING TREES.  AT THIS POINT ABIRTH(I)=0.
!  THE AGE OF INCOMMING TREES HAVING H>=HMAX IS CALCULATED BY
!  ASSUMEING A GROWTH RATE OF 0.10FT/YEAR FOR THE INTERVAL H-HMAX.
!  TREES REACHING HMAX DURING THE SIMULATION ARE IDENTIFIED IN HTGF.
!----------
IF(H .GE. HTMAX) THEN
  SITAGE = AGMAX1 + (H - HTMAX)/0.10
  SITHT = H
  IF(DEBUG)WRITE(JOSTND,*)' H,HTMAX,AGMAX1,SITAGE,SITHT= ', &
     H,HTMAX,AGMAX1,SITAGE,SITHT
  GO TO 30
ENDIF
!
HGUESS = 0.
75 CONTINUE
OLDHG = HGUESS
!----------
!  CALL HTCALC TO CALCULATE POTENTIAL HT GROWTH
!----------
INCRNG = 0
HGUESS = 0.0
CALL HTCALC(SINDX,ISPC,AG,HGUESS,JOSTND,DEBUG)
!
IF(DEBUG)WRITE(JOSTND,91200)AG,HGUESS,H
91200 FORMAT(' IN GUESS AN AGE--AGE,HGUESS,H ',3F10.2)
!----------
!  AVOID NEGATIVE PREDICTED HEIGHTS AT SMALL AGES FROM SOME SI CURVES
!  GED 4/20/18
!----------
IF(HGUESS .LT. 1.)GO TO 175
!
DIFF=ABS(HGUESS-H)
IF(DIFF .LE. TOLER .OR. H .LT. HGUESS)THEN
  SITAGE = AG
  SITHT = HGUESS
  IF(DEBUG)THEN
    WRITE(JOSTND, *)' DIFF,TOLER,H,HGUESS,AG,SITAGE,SITHT= ', &
       DIFF,TOLER,H,HGUESS,AG,SITAGE,SITHT
  ENDIF
  GO TO 30
END IF
!----------
!  SOME SITE CURVES DECREASE AT THE START BEFORE INCREASING. IF DECREASING,
!  KEEP GOING; IF SITE CURVE WAS INCREASING, BUT NOW HAS FLATTENED OFF, STOP
!  THE ITERATION GED 04/19/18
!----------
DIFF = (HGUESS-OLDHG)
IF(OLDHG.NE.0.0 .AND. DIFF.GE.0.05) INCRNG=1
IF(DEBUG)WRITE(JOSTND,*)' IN FINDAG OLDHG,DIFF,INCRNG= ', &
   OLDHG,DIFF,INCRNG
IF(INCRNG.EQ.1 .AND. DIFF .LT. 0.05)THEN
  SITAGE = AG
  SITHT = HGUESS
  IF(DEBUG)THEN
    WRITE(JOSTND, *)' SITE CURVE FLAT OLDHG,AG,HGUESS,SITAGE,', &
       'SITHT= ',OLDHG,AG,HGUESS,SITAGE,SITHT
  ENDIF
  GO TO 30
END IF
!
175 CONTINUE
AG = AG + 2.
!
IF(AG .GT. AGMAX1) THEN
!----------
!  H IS TOO GREAT AND MAX AGE IS EXCEEDED
!----------
  SITAGE = AGMAX1
  SITHT = H
  GO TO 30
ELSE
  GO TO 75
ENDIF
!
30 CONTINUE
IF(DEBUG)WRITE(JOSTND,50)I,SITAGE,SITHT
50 FORMAT(' LEAVING SUBROUTINE FINDAG  I,SITAGE,SITHT =', &
   I5,2F10.3)
!
RETURN
END
!**END OF CODE SEGMENT
