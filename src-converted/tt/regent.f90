9000 FORMAT('IN REGENT LOOP 2, I=',I4,', K=',I4,', ISPC=',I3, &
          ', CUR HT=',F7.2,', HT INC=',F7.4/T20,'NEW HT=',F7.2, &
          ', CUR DBH=',F7.2,', 10-YR DBH INC=',F7.4)
