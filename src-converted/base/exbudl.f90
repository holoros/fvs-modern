! BASE $Id$
!----------
!     THE PURPOSE OF THIS SUBROUTINE IS TO SATISFY EXTRA EXTERNAL
!     REFERENCES TO THE GENDEFOL/BUDWORM ("BUDLITE") MODEL
!
