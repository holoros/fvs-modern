       6.2936,    5.4044,    6.9356,    6.9059,    4.2281, &
       4.7066,   12.1300,    5.5983,    5.5983,    7.6793, &
       6.2428,    6.0404,    7.4995,    4.7726,    9.9284, &
       4.1543,   17.1081,    5.8103,    4.8977,    9.9284, &
