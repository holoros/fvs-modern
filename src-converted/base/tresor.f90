SUBROUTINE TRESOR
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!   THIS SUBROUTINE IS CALLED TO SORT AND MATCH TREE IDS (IDTREE)
!   WITH THE INTERNAL TREE INDEX NUMBERS GENERATED BY PROGNOSIS.
!   THIS IS NECESSARY TO INSURE THAT DATA  NEEDED BY EXTENSIONS
!   TO THE PROGNOSIS MODEL IS MAPPED TO THE RIGHT TREE RECORDS.
!   THIS ROUTINE IS CALLED FROM INITRE.
!----------
!  CALL SUBROUTINES TO SORT AND MATCH RECORDS FOR THE VARIOUS
!  EXTENSIONS.
!----------
!
!*    CALL MISSOR
!*    CALL RRSOR
!*    CALL BWSOR
!*    CALL TMSOR
!*    CALL MPBSOR
CALL BRSOR
!
RETURN
END
