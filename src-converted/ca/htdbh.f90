      94.5048,  117.7410, 1176.9704,  403.3221,   97.7769, &
     105.0771,  679.1972,   55.0   ,   47.3648,  179.0706, &
     149.5861,   55.0   ,  114.1627,   40.3812,  595.1068 /
!
DATA (CURARN(I,2),I=1,50)/ &
