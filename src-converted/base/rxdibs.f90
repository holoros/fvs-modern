SUBROUTINE RXDIBS(DBH,FC,HT,TOPD,SMDIA,GLOGLN,NUMLOG,JOSTND)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!  SAME AS OLD  R6DIBS --- NAME CHANGED TO ALLOW USE OF NEW R6DIBS
!  FROM VOLUME LIBRARY
!----------
!  THIS SUBROUTINE COMPUTES THE NUMBER OF 16FT LOGS
!  AND THE INSIDE BARK DIAMETERS FOR EACH.
!----------------------------------------------------------------------
!               LIST OF VARIABLES FOUND IN SUBROUTINE DIBS
! NAME   SOURCE TYPE                    DESCRIPTION
!
! D16    LOCAL  REAL  DIAMETER INSIDE BARK @ 16 FT.
! DBH    ARGMT  REAL  DIAMETER BREAST HEIGHT OUTSIDE BARK.
! SMDIA  ARGMT  R(19) DIAMETER INSIDE BARK FOR EACH LOG.
! FC     ARGMT  REAL  TREE FORM CLASS.
! HT    ARGMT  INTGR TOTAL TREE HEIGHT.
! GLOGLN ARGMT  R(19) LOG LENGTHS FOR EACH LOG IN TREE.
! NUMLOG ARGMT  INTGR NUMBER OF LOGS IN THE TREE.
! STMFND LOCAL  REAL  LENGTH OF STEM ALREADY CALCULATED.
! STMCAL LOCAL  REAL  LENGTH OF STEM NEEDING DIA'S AND LENGTHS.
! STMRAT LOCAL  REAL  RATIO OF STEM REMAINING TO UPPER STEM HEIGHT.
! TOPD   ARGMT  REAL  MERCHANTIBLE TOP DIAMETER FOR TREE.
! USHT   LOCAL  REAL  UPPER STEM HEIGHT.
!---------------------------------------------------------------------
!
INTEGER JOSTND,NUMLOG,I
REAL TOPD,HT,DBH,D16,USHT,STMFND,STMCAL,STMRAT,XD,XM,FC
REAL SMDIA(20),GLOGLN(20)
!----------
!  INITIALIZE DIAMETER & LOG LENGTH ARRAYS...
!----------
DO 1 NUMLOG = 1,20
   SMDIA(NUMLOG)=0.0
   GLOGLN(NUMLOG) = 0.0
1 CONTINUE
!----------
!  COMPUTE DIB AT 16.3' (FORM CLASS HEIGHT)...
!----------
D16=DBH*(FC/100.0)
!----------
!  CHECK FOR A TREE HAVING ONLY 1 LOG...
!----------
IF (D16.LE.TOPD) THEN
!----------
!        ONLY ONE LOG IN TREE.
!----------
   NUMLOG = 1
!----------
!  SET THE SMALL END DIAMETER OF THE 1ST LOG TO MERCH TOP...
!----------
   SMDIA(1) = TOPD
!----------
!  COMPUTE THE LENGTH OF THE LOG AS A RATIO BETWEEN DBH TO MERCH TOP
!  AND DBH TO DIB @ 16.3'; MINIMUM LOG LENGTH = 0.1 FT.
!----------
   GLOGLN(1) = (((DBH**2-TOPD**2)/(DBH**2-D16**2))*12.3)+4.0
   IF( GLOGLN(1) .LT. 0.1 ) GLOGLN(1) = 0.1
ELSE
!----------
!  TREE HAS MORE THAN ONE LOG; SET SMALL END DIAMETER OF BUTT LOG TO
!  D16 AND LOG LENGTH TO 16.3 FT.
!----------
   SMDIA(1) = D16
   GLOGLN(1) = 16.3
!----------
!  COMPUTE TOTAL STEM LENGTH ABOVE THE BUTT LOG; LOOP TO COMPUTE
!  REMAINING LOG LENGTHS AND SMALL END DIAMETERS.
!----------
   USHT = HT - 16.3
   DO 150 NUMLOG = 2,20
   STMFND = 16.3*(NUMLOG-1)
   STMCAL = USHT - STMFND
!----------
!  CHECK FOR LAST LOG LESS THAN 16.3 FT.
!----------
   IF (STMCAL.LT.0.0) GO TO 167
!----------
!  CALCULATE DIAMETER INSIDE BARK FOR SMALL END OF CURRENT LOG
!  AND CHECK TO SEE IF GREATER THAN MERCHANTABILITY LIMIT.
!----------
   STMRAT = STMCAL/USHT
   SMDIA(NUMLOG) = (STMRAT/(0.49*STMRAT+0.51))*D16
   IF(SMDIA(NUMLOG).LE.TOPD) GO TO 167
!----------
!  FULL LOG: ASSIGN LENGTH AND CONTINUE LOOP.
!----------
   GLOGLN(NUMLOG) = 16.3
150    CONTINUE
!----------
!  MORE THAN 20 LOGS IN CURRENT TREE.  PRINT ERROR MESSAGE AND RETURN.
!----------
   WRITE(JOSTND,151)
151    FORMAT(/' ********  ERROR: IN **R6DIBS**  MORE THAN 20 LOGS', &
       ' WERE FOUND IN A TREE; VOLUME TRUNCATED AT 20 LOGS')
   WRITE(JOSTND,152)  DBH,FC,HT,NUMLOG,TOPD,(SMDIA(I),I=1,NUMLOG)
152    FORMAT(' IN DIBS: DBH FC HT NUMLOG TOPD SMDIA=', &
               3F8.1,I5,F8.1,/,19F6.1)
   RETURN
167    CONTINUE
!----------
!  COMPUTE LENGTH OF LAST LOG IN MULTI-LOG TREE. SET SMALL END DIAMETER
!  TO TOPD.
!----------
   SMDIA(NUMLOG) = TOPD
   USHT = HT - 16.3
!----------
!  COMPUTE A TAPER RATE AS THE RATIO BETWEEN SMALL ENDS OF THE
!  FIRST AND LAST LOGS...
!----------
   XD = TOPD / SMDIA(1)
!----------
!  COMPUTE THE LENGTH OF THE TIP OF THE STEM ABOVE MERCH TOP.
!----------
   XM = (XD*0.51*USHT) / (1.0-(0.49*XD))
!----------
!  SUBTRACT THE TIP LENGTH AND THE LENGTH OF THE LOGS BETWEEN THE TOP
!  OF THE FIRST AND LAST LOGS (DIB @ 16.3' AND TOPD) TO GIVE THE LENGTH
!  OF THE LAST PIECE BELOW MERCH TOP.  BOUND TO MINIMUM OF 0.1 FT.
!----------
   GLOGLN(NUMLOG) = (USHT - XM) - ((NUMLOG-2)*16.3)
   IF( GLOGLN(NUMLOG) .LE. 0.0 ) GLOGLN(NUMLOG) = 0.1
END IF
!
RETURN
END
