SUBROUTINE EVIF (KEYWRD,ARRAY,LNOTBK,IRECNT,IREAD,RECORD, &
                    KARD,JOSTND,LDEBUG,LKECHO)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     COMPILES AND STORES THE IF EXPRESSION.
!
!     EVENT MONITOR ROUTINE - NL CROOKSTON - AUG 1982 - MOSCOW, ID.
!
!     ARGUMENTS:
!     KEYWRD= THE 8-CHAR KEYWORD.
!     ARRAY = THE ARRAY OF VALUES ON THE KEYWORD RECORD.
!     LNOTBK= TRUE IF CORRESPONDING VALUE ON THE KEYWORD RECORD IS
!             ENTERED, FALSE IF LEFT BLANK.
!     IRECNT= THE KEYWORD FILE RECORD COUNT.
!     RECORD= CHAR RECORD STRING.
!     KARD  = C*10 BY 7 KEYWORD FIELDS.
!     IREAD = THE KEYWORD FILE DATA SET REFERENCE NUMBER.
!     JOSTND= PRINT DATA SET REFERENCE NUMBER.
!     LDEBUG= TRUE IF DEBUGGING.
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'OPCOM.f90'
!
!
!OMMONS
!
INTEGER JOSTND,IREAD,IRECNT,IWAIT,IRC,IRTNCD
REAL ARRAY(12)
LOGICAL LNOTBK(12),LDEBUG,LKECHO
CHARACTER*8 KEYWRD
CHARACTER RECORD*(*),KARD(12)*10
!
!     IF THIS IF FOLLOWS ANOTHER IF WITHOUT AN INTERMEDIATE ENDIF,
!     THEN: CALL EVEND TO CLOSE THE PREVIOUS EVENT.
!
IF (LOPEVN) CALL EVEND (LDEBUG,JOSTND,IRECNT,KEYWRD,ARRAY, &
                           LNOTBK,KARD,-1,LKECHO)
!
!     ENTRY EVEND IN SUBROUTINE EVTACT INCREMENTS IEVT.  CHECK ON
!     THE MAGNITUDE OF IEVT TO INSURE IT IS NOT OVER MAXEVT.  IF
!     IT IS, ISSUE AN ERROR AND ABEND THE RUN.
!
IF (IEVT.GT.MAXEVT) CALL ERRGRO(.FALSE.,10)
CALL fvsGetRtnCode(IRTNCD)
IF (IRTNCD.NE.0) RETURN
!
!     WRITE THE KEYWORD AND FIRST PARM.
!
IWAIT=1
IF (LNOTBK(1)) IWAIT=IFIX(ARRAY(1))
IF(LKECHO)WRITE(JOSTND,5) KEYWRD,IWAIT
5 FORMAT (/A8,'   MINIMUM DELAY TIME BETWEEN RESPONSES', &
           ' TO THE EVENT = ',I5)
!
!     SET UP THE POINTERS IN THE EVENT ARRAY.
!
IEVNTS(IEVT,1)=ICOD
IEVNTS(IEVT,2)=-1
IEVNTS(IEVT,3)=IWAIT
LEVUSE=.TRUE.
!
!     READ AND COMPILE THE IF-EXPRESSION.
!
CALL EVCOMP (IRC,IREAD,JOSTND,RECORD,LDEBUG,IRECNT,LKECHO)
CALL fvsGetRtnCode(IRTNCD)
IF (IRTNCD.NE.0) RETURN
!
!     SET LOPEVN=TRUE TO INDECATE THAT THE EVENT HAS BEEN ENTERED.
!
LOPEVN=.TRUE.
RETURN
END
