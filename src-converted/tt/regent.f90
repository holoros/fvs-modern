CIVAR = .FALSE.
TTVAR = .FALSE.
UTVAR = .FALSE.
SELECT CASE (ISPC)
CASE(1:3,5:9,17)
  TTVAR = .TRUE.
  SCALE=YR/FINT
  IF(.NOT.LSKIPH)SCALE2 = YR/FLOAT(NTYR)
CASE(10)
  CIVAR = .TRUE.
  SCALE = FLOAT(NTYR)/REGYR
  IF(.NOT.LSKIPH)SCALE2 = YR/FLOAT(NTYR)
CASE(4,11:16,18)
  UTVAR = .TRUE.
  SCALE=FLOAT(NTYR)/YR
  IF(.NOT.LSKIPH)SCALE2 = YR/FLOAT(NTYR)
