    IF(HTGR .LT. 0.1)THEN
      HTGR = 0.1
      IF(DEBUG)WRITE(JOSTND,*)'CR/UT RESETTING HTGR ', &
         'TO 0.1 FOOT'
    ENDIF
!----------
  ENDIF
ENDIF
