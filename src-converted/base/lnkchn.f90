SUBROUTINE LNKCHN (ITREE)
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
INCLUDE 'PLOT.f90'
!
!
!OMMONS
!
INTEGER ITREE,II,KPTRII,IR
!
II = ISP(ITREE)
IF (IBEGIN(II) .NE. 0) GO TO 30
IBEGIN(II) = ITREE
NUMSP=NUMSP + 1
IUSED(NUMSP)=NSP(II,1)
IREF(II)= NUMSP
GO TO 40
30 CONTINUE
KPTRII=KPTR(II)
IND2(KPTRII) = ITREE
40 CONTINUE
KPTR(II) = ITREE
IR = IREF(II)
!
!     COUNT NUMBER OF TREE RECORDS PER SPECIES
!
KOUNT(IR)=KOUNT(IR) + 1
!
RETURN
END
