RETURN

!     CALLED FROM TREDEL
!
ENTRY BRTDEL (IVAC,IREC)
RETURN

!     CALLED FROM TRESOR
!
ENTRY BRSOR
RETURN

!     CALLED FROM TRIPLE
!
ENTRY BRTRIP (ITFN,I,WEIGHT)
RETURN

END
