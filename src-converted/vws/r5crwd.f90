SUBROUTINE R5CRWD(ISPC,D,H,CRWDTH)
IMPLICIT NONE
!----------
! VWS $Id$
!----------
!  THIS ROUTINE COMPUTES CROWN WIDTH FOR INDIVIDUAL TREES.
!  CALLED FROM CCFCAL IN ALL REGION 5 VARIANTS.
!
!  CROWN WIDTH EQUATIONS FOR REGION 5:
!  EQUATIONS FOR TREES LARGER THAN THE THRESHOLD DBH ARE FROM
!  RALPH WARBINGTON AND JACK LEVITAN AS PRESENTED IN THE R5 FIA
!  USERS GUIDE.
!  EQUATIONS FOR TREES 4.5 FEET TALL UP TO THE THRESHOLD DBH WERE
!  DEVELOPED BY GARY DIXON.  THOSE FOR DOUGLAS-FIR, PONDEROSA PINE,
!  WHITE FIR, GIANT CHINQUAPIN, MADRONE, AND TANOAK WERE FIT FROM DATA
!  IN THE SIX RIVERS PLANTATION INVENTORY.  SLOPE VALUES WERE ADJUSTED
!  SLIGHTLY FROM THE REGRESSION VALUES TO INTERCECT THE WARBINGTON &
!  LEVITAN EQUATIONS AT THE THRESHOLD DBH.  FOR OTHER SPECIES, THE
!  GENERIC INTERCEPT OF 3.5 FOR CONIFERS, AND 2.5 FOR HARDWOODS, WAS
!  USED, AND SLOPE VALUES COMPUTED TO INTERSECT THE W/L EQUATIONS AT
!  THE THRESHOLD DBH.
!  EQUATIONS FOR TREES LESS THAN 4.5 FEET TALL ARE JUST A STRAIGHT LINE
!  FROM (0,0) UP TO THE GENERIC INTERCEPT VALUES.
!  EQUATIONS 29-35 ARE FROM ANN WITHERS 4/29/1996 INTERNAL MEMO TO
!  GARY DIXON.
!
!----------
!  DEFINITION OF VARIABLES:
!
!     CROWN WIDTH COEFFICIENTS FOR WARBINGTON/LEVITAN EQUATIONS
!       CRWDTH = WB1() + WB2()*D                   EQN TYPE 1
!       CRWDTH = WB1() * D**WB2()                  EQN TYPE 2
!       CRWDTH = WB1() + WB2()*D + WB3()*D*D       EQN TYPE 3
!       WB1() = CONSTANT OR INTERCEPT, SUBSCRIPTED BY SPECIES
!       WB2() = CONSTANT OR EXPONENT, SUBSCRIPTED BY SPECIES
!       WB3() = CONSTANT, SUBSCRIPTED BY SPECIES
!
!     CROWN WIDTH COEFFICIENTS FOR DIXON EQUATIONS
!       CRWDTH = DX1() + DX2()*D
!       DX1() = INTERCEPT, SUBSCRIPTED BY SPECIES
!       DX2() = DBH CONSTANT, SUBSCRIPTED BY SPECIES
!
!     CRWDTH = CROWN WIDTH FOR A TREE (FEET)
!     D      = DIAMETER AT BREAST HEIGHT (INCHES)
!     H      = TOTAL TREE HEIGHT (FEET)
!     ISPC   = NUMERIC FVS SPECIES CODE
!
!  TO CONVERT CROWN WIDTH TO CCF:
!  THE CONSTANT 0.001803 IS USED TO CONVERT CROWN WIDTH IN FEET
!  TO THE PERCENT OF AN ACRE COVERED BY THE CROWN
!     CCF = (PIE * (CRWDTH/2)**2 / 43560) * 100
!     CCF = 0.001803 * CRWDTH**2
!
!  ORDER OF COEFFICIENTS IN THIS SUBROUTINE:
!
!   1 = DOUGLAS FIR
!   2 = SUGAR PINE
!   3 = WESTERN WHITE PINE
!   4 = WHITE FIR
!   5 = RED FIR
!   6 = MOUNTAIN HEMLOCK
!   7 = INCENSE CEDAR
!   8 = BLACK OAK
!   9 = CANYON LIVE OAK
!  10 = TANOAK
!  11 = MADRONE
!  12 = GRAND FIR
!  13 = SPRUCE
!  14 = WESTERN HEMLOCK
!  15 = YEW
!  16 = WESTERN RED CEDAR
!  17 = RED ALDER
!  18 = ASH/COTTONWOOD/ASPEN
!  19 = OREGON WHITE OAK
!
!  20 = CHINQUAPIN
!
!  21 = PONDEROSA PINE
!  22 = JEFFREY PINE
!  23 = LODGEPOLE PINE
!  24 = WHITEBARK PINE
!  25 = WESTERN JUNIPER
!  26 = BLUE OAK
!
!  27 = OTHER SOFTWOODS
!  28 = OTHER HARDWOODS
!
!  29 = BIGCONE DOUGLAS-FIR
!  30 = CA BAY LAUREL
!  31 = REDWOOD
!  32 = COULTER PINE
!  33 = DIGGER (GRAY) PINE
!  34 = COAST LIVE OAK
!  35 = CA WHITE (VALLEY) OAK
!----------
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!OMMONS
!----------
REAL WB1(35),WB2(35),WB3(35),DX1(35),DX2(35),SM(35)
INTEGER IEQN(35)
REAL SPLINE(35)
INTEGER MAPSO(33),MAPNC(12),MAPWS(43),MAPCA(50)
REAL CRWDTH,H,D,SPDBH
INTEGER ISPC,INDX,ITYPE
!
DATA WB1/ &
     6.81 , -1.476, -0.997,  5.82 ,  6.71 ,  4.72 ,  7.11 , 10.0  , &
     5.0  , 10.0  ,  1.0  ,  6.19 ,  6.50 ,  4.57 ,  4.2  ,  4.00 , &
     8.00 ,  0.50 ,  3.08 ,  2.98 ,  2.24 ,  1.52 ,  1.91 ,  2.37 , &
     4.31 ,  4.49 ,  6.0  ,  2.0  , 27.030, 12.733, 9.0684, 3.9347, &
    3.8273, 5.3732, 4.5628/
DATA WB2/ &
    0.732 , 1.01  , 0.92  , 0.591 , 0.421 , 0.608 , 0.470 , 1.20  , &
    1.69  , 1.05  , 1.43  , 1.01  , 1.80  , 1.41  , 1.42  , 1.60  , &
    1.53  , 1.62  , 1.92  , 1.55  , 0.763 , 0.891 , 0.784 , 0.736 , &
    0.628 , 0.688 , 0.6   , 1.5   , 0.8612, 2.249 , 0.4702, 0.7086, &
    0.7624, 0.7707, 0.6925/
DATA WB3/ 19*0., -0.014, 8*0., 7*0. /
!
DATA DX1/ &
     3.62 ,  3.5  ,  3.5  ,  3.26 ,  3.5  ,  3.5  ,  3.5  ,  2.5  , &
     2.5  ,  2.23 ,  3.11 ,  3.5  ,  3.5  ,  3.5  ,  3.5  ,  3.5  , &
     2.5  ,  2.5  ,  2.5  ,  2.15 ,  3.77 ,  3.5  ,  3.5  ,  3.5  , &
     3.5  ,  2.5  ,  3.5  ,  2.5  ,  3.5  ,  2.5  ,  3.5  ,  3.5  , &
     3.5  ,  2.5  ,  2.5  /
DATA DX2/ &
     1.370,  0.338,  0.329,  1.103,  1.063,  0.852,  1.192,  2.700, &
     2.190,  1.630,  1.008,  1.548,  2.400,  1.624,  1.560,  1.700, &
     2.630,  1.220,  2.036,  1.646, 0.7756, 0.5754, 0.6492, 0.8496, &
    1.6684, 2.2175,  1.1  ,  1.4  , 5.5672, 4.2956, 3.1654, 1.7618, &
    1.9108, 3.2150, 2.2816/
!
DATA IEQN/ 19*1, 3, 6*2, 2*1, 2*1, 5*2 /
!
DATA SPLINE/ &
     5.0  ,  7.4  ,  7.6  ,  5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  , &
     5.0  , 13.4  ,  5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  , &
     5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  , &
     5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  ,  5.0  , &
     5.0  ,  5.0  ,  5.0  /
!
DATA SM/ &
    0.7778, 0.7778, 0.7778, 0.7778, 0.7778, 0.7778, 0.7778, 0.5556, &
    0.5556, 0.5556, 0.5556, 0.7778, 0.7778, 0.7778, 0.7778, 0.7778, &
    0.5556, 0.5556, 0.5556, 0.5556, 0.7778, 0.7778, 0.7778, 0.7778, &
    0.7778, 0.5556, 0.7778, 0.5556, 0.7778, 0.5556, 0.7778, 0.7778, &
    0.7778, 0.5556, 0.5556/
!----------
! MAPPING FUNCTION FOR SPECIES IN THE --SO-- VARIANT
!  1=WP,  2=SP,  3=DF,  4=WF,  5=MH,  6=IC,  7=LP,  8=ES,  9=SH, 10=PP,
! 11=WJ, 12=GF, 13=AF, 14=SF, 15=NF, 16=WB, 17=WL, 18=RC, 19=WH, 20=PY,
! 21=WA, 22=RA, 23=BM, 24=AS, 25=CW, 26=CH, 27=WO, 28=WI, 29=GC, 30=MC,
! 31=MB, 32=OS, 33=OH
!----------
DATA MAPSO/ &
     3,  2,  1,  4,  6,  7, 23, 13,  5, 21, &
    27, 12,  5,  4,  4, 24,  6, 16, 14, 15, &
    17, 17, 28, 18, 18, 28, 19, 28, 20, 28, &
    28, 27, 28/
!----------
! MAPPING FUNCTION FOR SPECIES IN THE --NC-- VARIANT
! 1=OC, 2=SP, 3=DF, 4=WF, 5=MA, 6=IC, 7=BO, 8=TO, 9=RF, 10=PP, 11=OH, 12=RW
!----------
DATA MAPNC/ &
    27,  2,  1,  4, 11,  7,  8, 10,  5, 21, 28, 1/
!----------
! MAPPING FUNCTION FOR SPECIES IN THE --WS-- VARIANT
! NOTE: 21=GB DOESN'T USE THIS ROUTINE; EQN 1 SET TO PREVENT INDEX OF 0
!             AS A PRECAUTION.
!
!     SPECIES LIST FOR WESTERN SIERRAS VARIANT.
!
!     1 = SUGAR PINE (SP)                   PINUS LAMBERTIANA
!     2 = DOUGLAS-FIR (DF)                  PSEUDOTSUGA MENZIESII
!     3 = WHITE FIR (WF)                    ABIES CONCOLOR
!     4 = GIANT SEQUOIA (GS)                SEQUOIADENDRON GIGANTEAUM
!     5 = INCENSE CEDAR (IC)                LIBOCEDRUS DECURRENS
!     6 = JEFFREY PINE (JP)                 PINUS JEFFREYI
!     7 = CALIFORNIA RED FIR (RF)           ABIES MAGNIFICA
!     8 = PONDEROSA PINE (PP)               PINUS PONDEROSA
!     9 = LODGEPOLE PINE (LP)               PINUS CONTORTA
!    10 = WHITEBARK PINE (WB)               PINUS ALBICAULIS
!    11 = WESTERN WHITE PINE (WP)           PINUS MONTICOLA
!    12 = SINGLELEAF PINYON (PM)            PINUS MONOPHYLLA
!    13 = PACIFIC SILVER FIR (SF)           ABIES AMABILIS
!    14 = KNOBCONE PINE (KP)                PINUS ATTENUATA
!    15 = FOXTAIL PINE (FP)                 PINUS BALFOURIANA
!    16 = COULTER PINE (CP)                 PINUS COULTERI
!    17 = LIMBER PINE (LM)                  PINUS FLEXILIS
!    18 = MONTEREY PINE (MP)                PINUS RADIATA
!    19 = GRAY PINE (GP)                    PINUS SABINIANA
!         (OR CALIFORNIA FOOTHILL PINE)
!    20 = WASHOE PINE (WE)                  PINUS WASHOENSIS
!    21 = GREAT BASIN BRISTLECONE PINE (GB) PINUS LONGAEVA
!    22 = BIGCONE DOUGLAS-FIR (BD)          PSEUDOTSUGA MACROCARPA
!    23 = REDWOOD (RW)                      SEQUOIA SEMPERVIRENS
!    24 = MOUNTAIN HEMLOCK (MH)             TSUGA MERTENSIANA
!    25 = WESTERN JUNIPER (WJ)              JUNIPERUS OCIDENTALIS
!    26 = UTAH JUNIPER (UJ)                 JUNIPERUS OSTEOSPERMA
!    27 = CALIFORNIA JUNIPER (CJ)           JUNIPERUS CALIFORNICA
!    28 = CALIFORNIA LIVE OAK (LO)          QUERCUS AGRIFOLIA
!    29 = CANYON LIVE OAK (CY)              QUERCUS CHRYSOLEPSIS
!    30 = BLUE OAK (BL)                     QUERCUS DOUGLASII
!    31 = CALIFORNIA BLACK OAK (BO)         QUERQUS KELLOGGII
!    32 = VALLEY OAK (VO)                   QUERCUS LOBATA
!         (OR CALIFORNIA WHITE OAK)
!    33 = INTERIOR LIVE OAK (IO)            QUERCUS WISLIZENI
!    34 = TANOAK (TO)                       LITHOCARPUS DENSIFLORUS
!    35 = GIANT CHINQUAPIN (GC)             CHRYSOLEPIS CHRYSOPHYLLA
!    36 = QUAKING ASPEN (AS)                POPULUS TREMULOIDES
!    37 = CALIFORNIA-LAUREL (CL)            UMBELLULARIA CALIFORNICA
!    38 = PACIFIC MADRONE (MA)              ARBUTUS MENZIESII
!    39 = PACIFIC DOGWOOD (DG)              CORNUS NUTTALLII
!    40 = BIGLEAF MAPLE (BM)                ACER MACROPHYLLUM
!    41 = CURLLEAF MOUNTAIN-MAHOGANY (MC)   CERCOCARPUS LEDIFOLIUS
!    42 = OTHER SOFTWOODS (OS)
!    43 = OTHER HARDWOODS (OH)
!
!  SURROGATE EQUATION ASSIGNMENT:
!
!    FROM EXISTING WS EQUATIONS --
!      USE 1(SP) FOR 11(WP) AND 24(MH)
!      USE 3(WF) FOR 13(SF)
!      USE 8(PP) FOR 18(MP)
!      USE 34(TO) FOR 35(GC), 36(AS), 38(MA), AND 39(DG)
!      USE 31(BO) FOR 29(CY), 30(BL), 33(IO), 40(BM), AND
!                     43(OH)
!
!    FROM CA VARIANT --
!      USE CA11(KP) FOR 12(PM), 14(KP), 15(FP), 17(LM), 20(WE), 25(WJ),
!                     26(WJ), AND 27(CJ)
!      USE CA12(LP) FOR 9(LP) AND 10(WB)
!
!    FROM SO VARIANT --
!      USE SO30(MC) FOR 41(MC)
!
!    FROM UT VARIANT --
!      USE UT17(GB) FOR 21(GB)
!----------
DATA MAPWS/ &
     2,  1,  4, 27,  7, 22,  5, 21, 23, 24, &
     3, 27,  4, 27, 27, 32, 27, 27, 33, 27, &
    27, 29, 27,  6, 25, 25, 25, 34,  9, 26, &
     8, 35, 28, 10, 20, 18, 30, 11, 28, 28, &
    28, 27, 28/
!----------
! MAPPING FUNCTION FOR SPECIES IN THE --CA-- VARIANT
!  1=PC  2=IC  3=RC  4=WF  5=RF  6=SF  7=DF  8=WH  9=MH 10=WB
! 11=KP 12=LP 13=CP 14=LM 15=JP 16=SP 17=WP 18=PP 19=MP 20=GP
! 21=JU 22=BS 23=GS 24=PY 25=OS 26=LO 27=CY 28=DO 29=EO 30=WO
! 31=BO 32=VO 33=IO 34=BM 35=BU 36=RA 37=MA 38=GC 39=DG 40=OA
! 41=WA 42=TO 43=SY 44=AS 45=CO 46=WI 47=CN 48=BL 49=OH,50=RW
!----------
DATA MAPCA/ &
    27,  7, 16,  4,  5,  5,  1, 14,  6, 24, &
    27, 23, 32, 27, 22,  2,  3, 21, 27, 33, &
    25, 13, 27, 15, 27, 34,  9, 26, 28, 19, &
     8, 35, 28, 28, 28, 17, 11, 20, 28, 18, &
    28, 10, 28, 18, 18, 28, 28, 28, 28, 1/
!----------
! ASSIGN THE APPROPRIATE EQUATION NUMBER BASED ON THE VARIANT.
!----------
SELECT CASE (VARACD)
!
  CASE ('CA','OC')
    INDX=MAPCA(ISPC)
!
  CASE ('NC')
    INDX=MAPNC(ISPC)
!
  CASE ('SO')
    INDX=MAPSO(ISPC)
!
  CASE ('WS')
    INDX=MAPWS(ISPC)
!
  CASE DEFAULT
    INDX=99
!
END SELECT
!----------
!  SET EQUATION TYPE AND SPLINE DBH
!----------
ITYPE = IEQN(INDX)
SPDBH = SPLINE(INDX)
!----------
! COMPUTE CROWN WIDTH
!----------
IF(INDX .EQ. 99) THEN
  CRWDTH=0.
  GO TO 100
ENDIF
!
IF(D .GE. SPDBH) THEN
  IF(ITYPE .EQ. 1) THEN
    CRWDTH = WB1(INDX) + WB2(INDX)*D
  ELSE IF(ITYPE .EQ. 2) THEN
    CRWDTH = WB1(INDX) * D**WB2(INDX)
  ELSE IF(ITYPE .EQ. 3) THEN
    CRWDTH = WB1(INDX) + WB2(INDX)*D + WB3(INDX)*D*D
  ELSE
    CRWDTH = 0.
  ENDIF
ELSE IF(H .GE. 4.5) THEN
  CRWDTH = DX1(INDX) + DX2(INDX)*D
ELSE
  CRWDTH = SM(INDX)*H
ENDIF
!
100 CONTINUE
RETURN
END
