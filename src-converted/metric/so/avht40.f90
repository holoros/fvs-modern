SUBROUTINE AVHT40
IMPLICIT NONE
!----------
! METRIC-SO $Id$
!----------
!  THIS SUBROUTINE IS USED TO CALCULATE THE AVERAGE HEIGHT
!  OF THE 40 TPA OF LARGEST DIAMETER. (METRIC EQUIV)
!
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'CONTRL.f90'
!
!
INCLUDE 'ARRAYS.f90'
!
!
INCLUDE 'PLOT.f90'
!
!
INCLUDE 'METRIC.f90'
!
!
!OMMONS
!
INTEGER I,II
REAL    SSUMN,TARG,P

AVH=0.
IF (ITRN.LE.0) GOTO 70
SSUMN=0.
TARG = 100.0/HAtoACR ! METRIC VERSION = 40.47/AC
DO 60 I=1,ITRN
II=IND(I)
P=PROB(II)
IF(SSUMN+P.GT.TARG) P=TARG-SSUMN
SSUMN=SSUMN+P
AVH=AVH+HT(II)*P
IF(SSUMN.GE.TARG) GO TO 65
60 CONTINUE
65 CONTINUE
IF (SSUMN .GT. 0.) AVH = AVH/SSUMN
70 CONTINUE
RETURN
END
