CHARACTER*40 HVPLAB(MXHRVP)
CHARACTER*250 WKHPLB
COMMON /PPHVCC/ HVPLAB,WKHPLB
LOGICAL LHVDEB,LPRTCT,LHVOUT(MXHRVP),LHVMXC,LHVUNT,LHIER
INTEGER LNHPLB(MXHRVP,2),IHVTAB(MXHRVP,3),IHVSTA(MXSTND,MXHRVP), &
           IUNLAB(MXHRVP),IXHRVP,JOHVDS(MXHRVP),IHVEXT
REAL    HVYLDS(MXSTND,MXHRVP),HVTHIN(MXSTND,MXHRVP), &
           HVPART(MXHRVP),HVPRI(MXSTND,MXHRVP),TRGETS(MXHRVP), &
           TRGSTS(9,MXHRVP),HVMXCC
COMMON /PPHVCM/ LHVDEB,LPRTCT,LNHPLB,IHVTAB,HVYLDS,HVTHIN,IHVSTA, &
                   HVPART,HVPRI,TRGETS,TRGSTS,IUNLAB,IXHRVP,LHVOUT, &
                   LHVMXC,HVMXCC,LHVUNT,LHIER,JOHVDS,IHVEXT
!
!     NOTE: HARVEST SIMULATION, AND HARVEST SELECTION IS NOW CALLED
!     MULTISTAND TREATMENT SCHEDULING.  EVERY VARIABLE, SUBROUTINE
!     NAME, AND COMMON AREA THAT CONTAINS 'HV' REFERS TO MULTISTAND
!     TREATMENT SCHEDULING (ALSO CALLED MULTISTAND POLICY SIMULATION).
!
!     HVPLAB= MULTISTAND TREATMENT POLICY LABELS.
!     WKHPLB= A WORK STRING USED IN PROCESSING LABELS
!     LHVDEB= DEBUG THE MULTISTAND POLICY LOGIC
!     LPRTCT= TRUE IF STANDS WILL BE PARTIALLY CUT IF NECESSARY TO
!             MAKE EXACTLY MEET THE TARGET, AND FALSE IF THE
!             STANDS CAN NOT BE PARTIALLY CUT.
!     LNHPLB= LENGTH OF EACH POLICY LABEL
!             (.,1)=TOTAL LENGTH
!             (.,2)=LENGTH OF THE FIRST PART (UP TO THE PERIOD), OR
!                   EQUAL TO TOTAL LENGTH WHEN THERE IS NO PERIOD.
!     IXHRVP= THE LARGEST POLICY NUMBER THAT WAS DEFINED
!     IHVEXT= 0 IF PPE HV PROCESSING IS USED TO SELECT HARVEST UNITS
!             1 IF EXTERNAL METHOD #1 IS BEING USED (FIELD 7 OF MSPOLOCY)
!     IHVTAB= POINTERS TO THE OPCODE ARRAY FOR THE
!             (.,1)= TARGET KEYWORD
!             (.,2)= PRIORITY KEYWORD
!             (.,3)= CUTUNITS KEYWORD
!     HVYLDS= THE CONTRIBUTION THAT A STAND CAN MAKE TOWARD REACHING
!             THE TARGET OF A GIVEN MULTISTAND POLICY IF IT IS
!             SELECTED UNDER THE POLICY
!     HVTHIN= THE CONTRIBUTION THAT A STAND CAN MAKE TOWARD REACHING
!             THE TARGET OF A GIVEN POLICY IF IT IS NOT SELECTED
!             UNDER THE POLICY.
!     HVPRI = THE PRIORITY AS COMPUTED BY HVALOC.
!     HVPART= THE PROPORTION OF A STAND THAT IS NEEDED TO MEET A TARGET
!             WHEN THE STATUS CODE IS 4...ONLY ONE SUCH STAND CAN EXIST
!             PER POLICY.
!     IHVSTA= STATUS CODE FOR EACH STAND UNDER EACH POLICY. CODES ARE:
!             0=THE STAND CAN NOT CONTRIBUTE TOWARD THE TARGET
!               CALCULATIONS AND CAN NOT BE CUT UNDER A THE MULTISTAND
!               POLICY.
!          +/-1=THE STAND CAN CONTRIBUTE TOWARD THE TARGET BUT IS
!               NOT CURRENTLY SELECTED.  THIS IS THE
!               INITIAL SETTING FOR ALL STANDS AND POLICIES,
!               +1= STAND WILL NOT BE IN "CLEAR CUT" CONDITION IF
!               SELECTED, -1= STAND WOULD BE IN "CLEAR CUT" CONDITION.
!               THE SIGN BIT IS ONLY USED BETWEEN CALL HVHRV1 AND
!               DURING THE EXECUTION OF HVSEL. (SEE 3, BELOW).
!             2=THE STAND IS NOT NEEDED TO REACH THE TARGET...THINNING
!               YIELD, IF ANY, DOES CONTRIBUTE.
!          +/-3=THE STAND IS NEEDED TO REACH THE TARGET, SIGN
!               INDICATES THE "CLEAR CUT" CONDITION.  NOTE THAT ALL
!               SIGNS ARE POSITIVE AFTER THE CALL TO HVSEL.
!             4=THE STAND IS PARTIALLY NEEDED...REPLICATE THE STAND
!               AND SPLIT ITS SAMPLING WEIGHT.  THE PERCENTAGE OF THE
!               STAND THAT IS NEEDED IS STORED IN THE CORRESPONDING
!               WORD IN HVPRI.
!     TRGSTS= THE STATISTICS USED TO COMPUTE POLICIES.
!             STORED BY STATISTIC TYPE AND POLICY NUMBER.
!             (1,.)= AVBTPA    AVERAGE BEFORE THIN TREES PER ACRE
!             (2,.)= AVBTCUFT  AVERAGE BEFORE THIN CUBIC VOLUME
!             (3,.)= AVBMCUFT  AVERAGE BEFORE THIN CUBIC VOLUME
!             (4,.)= AVBBDFT   AVERAGE BEFORE THIN BOARD FOOT VOLUME
!             (5,.)= AVBBA     AVERAGE BEFORE THIN BASAL AREA
!             (6,.)= AVBACC    LAST CYCLE ACCRETION (CUFT/ACRE/YR)
!             (7,.)= AVBMORT   LAST CYCLE MORTALITY (CUFT/ACRE/YR)
!             (8,.)= TOTALWT   TOTAL WEIGHT UNDER THE POLICY
!             (9,.)= OLDTARG   LAST MASTER CYCLE TARGET.
!     TRGETS= TARGET BY POLICY
!     IUNLAB= MEMBERS OF THIS ARRAY INDEXED BY POLICY.
!             IF THE VALUE OF IUNLAB IS:
!             0=POLICY IS NOT DEFINED.
!             1=THE LABEL FOR THIS POLICY IS THE SAME, UP TO THE PERIOD,
!               AS THE LABEL FOR POLICY 1.
!             2=THE LABEL FOR THIS POLICY IS THE SAME, UP TO THE PERIOD,
!               AS THE LABEL FOR POLICY 2.
!             3,...,MXHRVP AND SO FORTH.
!     LHVOUT= TRUE WHEN REPORTS ARE BEING GENERATED FOR THE MULTISTAND
!             POLICY, FALSE IF NOT (USED IN HVSEL).
!     LHVMXC= TRUE IF MAX CONTIGUOUS CLEARCUT ACRE CONSTRAINT IS BEING
!             USED BY THE MULTISTAND POLICY ROUTINES.
!     HVMXCC= THE MAX CONTIGUOUS ACRES OF CLEAR CUT.
!     LHVUNT= TRUE IF COORDINATED MANAGEMENT UNIT LOGIC IS BEING USED,
!             FALSE IF EACH STAND IS A SEPARATE UNIT.
!     LHIER = TRUE IF FIRST POLICY ACTS AS THE PRIMARY LEVEL IN A
!             POLICY HIERARCHY.  THE NEXT IS THE SECOND LEVEL.
!     JOHVDS= DATA SET REFERENCES NUMBERS FOR HARVEST SIMULATION
!             MACHINE READABLE OUTPUT (DEFAULTS ARE ZERO).
!
!-----END SEGMENT
