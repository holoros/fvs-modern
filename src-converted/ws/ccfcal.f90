SUBROUTINE CCFCAL(ISPC,D,H,JCR,P,LTHIN,CCFT,CRWDTH,MODE)
IMPLICIT NONE
!----------
! WS $Id$
!----------
!  THIS ROUTINE COMPUTES CROWN WIDTH AND CCF FOR INDIVIDUAL TREES.
!  CALLED FROM DENSE, PRTRLS, SSTAGE, AND CVCW.
!
!  ARGUMENT DEFINITIONS:
!    ISPC = NUMERIC SPECIES CODE
!       D = DIAMETER AT BREAST HEIGHT
!       H = TOTAL TREE HEIGHT
!     JCR = CROWN RATIO IN PERCENT (0-100)
!       P = TREES PER ACRE
!   LTHIN = .TRUE. IF THINNING HAS JUST OCCURRED
!         = .FALSE. OTHERWISE
!    CCFT = CCF REPRESENTED BY THIS TREE
!  CRWDTH = CROWN WIDTH OF THIS TREE
!    MODE = 1 IF ONLY NEED CCF RETURNED
!           2 IF ONLY NEED CRWDTH RETURNED
!
!  CROWN WIDTH EQUATIONS FOR REGION 5:
!  FROM WARBINGTON/LEVITAN & DIXON --- SEE DOCUMENTATION IN
!                                      SUBROUTINE **R5CRWD**
!----------
!OMMONS
!
!
INCLUDE  'PRGPRM.f90'
!
!
!OMMONS
!----------
!     CONVERTING CROWN WIDTH TO CCF:
!     THE CONSTANT 0.001803 CAN BE USED TO CONVERT CROWN WIDTH
!     IN FEET TO THE PERCENT OF AN ACRE COVERED BY THE CROWN
!     CCF = (PIE * (CRWDTH/2)**2 / 43560) * 100
!     CCF = 0.001803 * CRWDTH**2
!
!
!  CCF EQUATIONS:
!     CCF COEFFICIENTS FOR TREES THAT ARE GREATER THAN 10.0 IN. DBH:
!      RD1 -- CONSTANT TERM IN CROWN COMPETITION FACTOR EQUATION,
!             SUBSCRIPTED BY SPECIES
!      RD2 -- COEFFICIENT FOR SUM OF DIAMETERS TERM IN CROWN
!             COMPETITION FACTOR EQUATION,SUBSCRIPTED BY SPECIES
!
!     CCF COEFFICIENTS FOR TREES THAT ARE LESS THAN 10.0 IN. DBH:
!      RDA -- MULTIPLIER.
!      RDB -- EXPONENT.  CCF(I) = RDA*DBH**RDB
!
!     SPECIES LIST FOR WESTERN SIERRAS VARIANT.
!
!     1 = SUGAR PINE (SP)                   PINUS LAMBERTIANA
!     2 = DOUGLAS-FIR (DF)                  PSEUDOTSUGA MENZIESII
!     3 = WHITE FIR (WF)                    ABIES CONCOLOR
!     4 = GIANT SEQUOIA (GS)                SEQUOIADENDRON GIGANTEAUM
!     5 = INCENSE CEDAR (IC)                LIBOCEDRUS DECURRENS
!     6 = JEFFREY PINE (JP)                 PINUS JEFFREYI
!     7 = CALIFORNIA RED FIR (RF)           ABIES MAGNIFICA
!     8 = PONDEROSA PINE (PP)               PINUS PONDEROSA
!     9 = LODGEPOLE PINE (LP)               PINUS CONTORTA
!    10 = WHITEBARK PINE (WB)               PINUS ALBICAULIS
!    11 = WESTERN WHITE PINE (WP)           PINUS MONTICOLA
!    12 = SINGLELEAF PINYON (PM)            PINUS MONOPHYLLA
!    13 = PACIFIC SILVER FIR (SF)           ABIES AMABILIS
!    14 = KNOBCONE PINE (KP)                PINUS ATTENUATA
!    15 = FOXTAIL PINE (FP)                 PINUS BALFOURIANA
!    16 = COULTER PINE (CP)                 PINUS COULTERI
!    17 = LIMBER PINE (LM)                  PINUS FLEXILIS
!    18 = MONTEREY PINE (MP)                PINUS RADIATA
!    19 = GRAY PINE (GP)                    PINUS SABINIANA
!         (OR CALIFORNIA FOOTHILL PINE)
!    20 = WASHOE PINE (WE)                  PINUS WASHOENSIS
!    21 = GREAT BASIN BRISTLECONE PINE (GB) PINUS LONGAEVA
!    22 = BIGCONE DOUGLAS-FIR (BD)          PSEUDOTSUGA MACROCARPA
!    23 = REDWOOD (RW)                      SEQUOIA SEMPERVIRENS
!    24 = MOUNTAIN HEMLOCK (MH)             TSUGA MERTENSIANA
!    25 = WESTERN JUNIPER (WJ)              JUNIPERUS OCIDENTALIS
!    26 = UTAH JUNIPER (UJ)                 JUNIPERUS OSTEOSPERMA
!    27 = CALIFORNIA JUNIPER (CJ)           JUNIPERUS CALIFORNICA
!    28 = CALIFORNIA LIVE OAK (LO)          QUERCUS AGRIFOLIA
!    29 = CANYON LIVE OAK (CY)              QUERCUS CHRYSOLEPSIS
!    30 = BLUE OAK (BL)                     QUERCUS DOUGLASII
!    31 = CALIFORNIA BLACK OAK (BO)         QUERQUS KELLOGGII
!    32 = VALLEY OAK (VO)                   QUERCUS LOBATA
!         (OR CALIFORNIA WHITE OAK)
!    33 = INTERIOR LIVE OAK (IO)            QUERCUS WISLIZENI
!    34 = TANOAK (TO)                       LITHOCARPUS DENSIFLORUS
!    35 = GIANT CHINQUAPIN (GC)             CHRYSOLEPIS CHRYSOPHYLLA
!    36 = QUAKING ASPEN (AS)                POPULUS TREMULOIDES
!    37 = CALIFORNIA-LAUREL (CL)            UMBELLULARIA CALIFORNICA
!    38 = PACIFIC MADRONE (MA)              ARBUTUS MENZIESII
!    39 = PACIFIC DOGWOOD (DG)              CORNUS NUTTALLII
!    40 = BIGLEAF MAPLE (BM)                ACER MACROPHYLLUM
!    41 = CURLLEAF MOUNTAIN-MAHOGANY (MC)   CERCOCARPUS LEDIFOLIUS
!    42 = OTHER SOFTWOODS (OS)
!    43 = OTHER HARDWOODS (OH)
!
!  SURROGATE EQUATION ASSIGNMENT:
!
!    FROM EXISTING WS EQUATIONS --
!      USE 1(SP) FOR 11(WP) AND 24(MH)
!      USE 2(DF) FOR 22(BD)
!      USE 3(WF) FOR 13(SF)
!      USE 4(GS) FOR 23(RW)
!      USE 8(PP) FOR 18(MP)
!      USE 34(TO) FOR 35(GC), 36(AS), 37(CL), 38(MA), AND 39(DG)
!      USE 31(BO) FOR 28(LO), 29(CY), 30(BL), 32(VO), 33(IO), 40(BM), AND
!                     43(OH)
!
!    FROM CA VARIANT --
!      USE CA11(KP) FOR 12(PM), 14(KP), 15(FP), 16(CP), 17(LM), 19(GP), 20(WE),
!                       25(WJ), 26(WJ), AND 27(CJ)
!      USE CA12(LP) FOR 9(LP) AND 10(WB)
!
!    FROM SO VARIANT --
!      USE SO30(MC) FOR 41(MC)
!
!    FROM UT VARIANT --
!      USE UT17(GB) FOR 21(GB)
!
!  SOURCES OF CCF EQUATION COEFFICIENTS:
!     ALL COEFFICIENTS ARE FROM THE R5 FIAS SYSTEM AT THE TIME THE
!     VARIANT WAS FIT. THESE WERE GENERALLY BASED ON MAPPING PROJECTS
!     FROM THE TAHOE, STANISLAUS, AND PLUMAS NATIONAL FORESTS.
!     SOME COEFFICIENTS WERE UPDATED IN 1995 WHEN THE MAPPING PROJECTS
!     ON THE MODOC AND LASSEN WERE COMPLETED. THIS IS WHY SOME OF THESE
!     COEFFICIENTS ARE DIFFERENT FROM THE CROWN WIDTH COEFFICIENTS IN
!     SUBROUTINE R5CRWD.
!
!     1 SP = OLDER VERSION OF THE R5 FIAS USER'S GUIDE EQUATION
!     2 DF = R5 FIAS USER'S GUIDE (SEE R5CRWD): DOUGLAS-FIR
!     3 WF = R5 FIAS USER'S GUIDE (SEE R5CRWD): WHITE FIR
!     4 GS = R5 FIAS USER'S GUIDE (SEE R5CRWD): WHITE FIR
!     5 IC = R5 FIAS USER'S GUIDE (SEE R5CRWD): INCENSE CEDAR
!     6 JP = OLDER VERSION OF THE R5 FIAS USER'S GUIDE EQUATION
!     7 RF = R5 FIAS USER'S GUIDE (SEE R5CRWD): RED FIR
!     8 PP = OLDER VERSION OF THE R5 FIAS USER'S GUIDE EQUATION
!    21 GB = NORTHERN IDAHO VARIANT INT-133 TABLE 8: LODGEPOLE PINE
!    31 BO = R5 FIAS USER'S GUIDE (SEE R5CRWD): BLACK OAK
!    34 TO = R5 FIAS USER'S GUIDE (SEE R5CRWD): TANOAK
!    41 MC = PAINE AND HANN TABLE 2: CALIFORNIA BLACK OAK
!    42 OS = OLDER VERSION OF THE R5 FIAS USER'S GUIDE EQUATION
!----------
LOGICAL LTHIN
REAL RD1(MAXSP),RD2(MAXSP),RDA(MAXSP),RDB(MAXSP)
REAL CRWDTH,CCFT,P,H,D
INTEGER MODE,JCR,ISPC
INTEGER IDANUW
LOGICAL LDANUW
!
DATA RD1/ &
    6.74, 6.81, 5.82, 5.82, 7.11, &
    3.10, 6.71, 5.13, 0.00, 0.00, &
    6.74, 0.00, 5.82, 0.00, 0.00, &
    0.00, 0.00, 5.13, 0.00, 0.00, &
    0.00, 6.81, 5.82, 6.74, 0.00, &
    0.00, 0.00, 10.0, 10.0, 10.0, &
    10.0, 10.0, 10.0, 10.0, 10.0, &
    10.0, 10.0, 10.0, 10.0, 10.0, &
    0.00, 7.07, 10.0/
!
DATA RD2/ &
    0.623, 0.732, 0.591, 0.591, 0.470, &
    0.839, 0.421, 0.658, 0.000, 0.000, &
    0.623, 0.000, 0.591, 0.000, 0.000, &
    0.000, 0.000, 0.658, 0.000, 0.000, &
    0.000, 0.732, 0.591, 0.623, 0.000, &
    0.000, 0.000, 1.200, 1.200, 1.200, &
    1.200, 1.200, 1.200, 1.050, 1.050, &
    1.050, 1.050, 1.050, 1.050, 1.200, &
    0.000, 0.551, 1.200/
!
DATA RDA/ &
    0.007244, 0.017299, 0.015248, 0.011109, 0.008915, &
    0.007875, 0.011402, 0.007813, 0.000000, 0.000000, &
    0.007244, 0.000000, 0.015248, 0.000000, 0.000000, &
    0.000000, 0.000000, 0.007813, 0.000000, 0.000000, &
    0.000000, 0.017299, 0.011109, 0.007244, 0.000000, &
    0.000000, 0.000000, 0.009187, 0.009187, 0.009187, &
    0.009187, 0.009187, 0.009187, 0.011109, 0.011109, &
    0.011109, 0.011109, 0.011109, 0.011109, 0.009187, &
         0.0, 0.009884, 0.009187/
!
DATA RDB/ &
    1.8182, 1.5571, 1.7333, 1.7250, 1.7800, &
    1.7360, 1.7560, 1.7780, 0.0000, 0.0000, &
    1.8182, 0.0000, 1.7333, 0.0000, 0.0000, &
    0.0000, 0.0000, 1.7780, 0.0000, 0.0000, &
    0.0000, 1.5571, 1.7250, 1.8182, 0.0000, &
    0.0000, 0.0000, 1.7600, 1.7600, 1.7600, &
    1.7600, 1.7600, 1.7600, 1.7250, 1.7250, &
    1.7250, 1.7250, 1.7250, 1.7250, 1.7600, &
       0.0, 1.6667, 1.7600/
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
IDANUW = JCR
LDANUW = LTHIN
!
!----------
!  INITIALIZE RETURN VARIABLES.
!----------
CCFT = 0.
CRWDTH = 0.
!----------
!  COMPUTE CCF
!----------
IF(MODE.EQ.1) THEN
  SELECT CASE (ISPC)
!----------
!  SPECIES USING SURROGATE EQNS FROM THE CA VARIANT
!----------
    CASE(9,10,12,14:17,19,20,25:27)
      CALL R5CRWD (ISPC,D,H,CRWDTH)
      CCFT= CRWDTH  * CRWDTH  * 0.001803
!----------
!  SPECIES USING SURROGATE EQNS FROM THE SO VARIANT
!----------
  CASE(41)
    IF (D .LT. 1.0) THEN
      CCFT = D * (0.0204 + 0.0246 + 0.0074)
    ELSE
      CCFT = 0.0204 + 0.0246*D + 0.0074*D**2.0
    ENDIF
!----------
!  SPECIES USING SURROGATE EQNS FROM THE UT VARIANT
!----------
  CASE(21)
    IF (D .GE. 10.0) THEN
      CCFT = 0.01925 + 0.01676*D + 0.00365*D*D
    ELSEIF (D .GT. 0.1) THEN
      CCFT = 0.009187 * (D**1.7600)
    ELSE
      CCFT = 0.001
    ENDIF
!----------
!  SPECIES USING ORIGINAL WS EQNS
!----------
    CASE DEFAULT
      IF (D .GE. 1.0) THEN
        CCFT = ((RD1(ISPC)+D*RD2(ISPC))**2.0)*0.001803
      ELSE IF(D.GT.0.1) THEN
        CCFT = RDA(ISPC) * (D**RDB(ISPC))
      ELSE
        CCFT=0.001
      ENDIF
!
  END SELECT
!
  IF(P .GT. 0.)CCFT = CCFT * P
ENDIF
!----------
! COMPUTE CROWN WIDTH
!----------
IF(MODE.EQ.2) THEN
!
  SELECT CASE (ISPC)
!
    CASE (21)
      IF (D .GE. 10.0) THEN
        CCFT = 0.01925 + 0.01676*D + 0.00365*D*D
      ELSEIF (D .GT. 0.1) THEN
        CCFT = 0.009187 * (D**1.7600)
      ELSE
        CCFT = 0.001
      ENDIF
      CRWDTH = SQRT(CCFT/0.001803)
!
    CASE DEFAULT
      CALL R5CRWD (ISPC,D,H,CRWDTH)
!
  END SELECT
!----------
!  LIMIT CROWN WIDTH FOR PRINTING ON TREELIST.
!----------
  IF(CRWDTH .GT. 99.9) CRWDTH=99.9
ENDIF
!
RETURN
END
