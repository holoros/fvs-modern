SDNORM = 369.0
SDISLP = -0.0033

IF (LMTRIC) THEN
   IF (ARRAY(1) .GT. 0.0) SDNORM = ARRAY(1) / HATOAC
   IF (LNOTBK(2)) SDISLP = ARRAY(2) * HATOAC
ELSE
   IF (ARRAY(1) .GT. 0.0) SDNORM = ARRAY(1)
   IF (LNOTBK(2)) SDISLP = ARRAY(2)
