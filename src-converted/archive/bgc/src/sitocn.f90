!  SITOC CALCULATES THE PROPORTION OF TOTAL SITE LEAF AREA CONTRIBUTED
!  BY AN ENTITY. THIS PROPORTION IS USED AS AN INDICATOR OF RELATIVE
!  SITE OCCUPANCY BY AN ENTITY. IT IS USED TO DEFINE THE GROUND SURFACE AREA
!  NEEDED FOR CALCULATION OF AVAILABLE SOIL WATER.
