SUBROUTINE EXBGC
IMPLICIT NONE
!----------
! BASE $Id$
!----------
!
!     EXTERNAL REFERENCES FOR THE BGC MODEL (KELSEY MILNER)
!
!----------
INTEGER I1,I2
REAL ARRAY(12)
LOGICAL LNOTBK(12),L,LKECHO
CHARACTER*8 KEYWRD
INTEGER IDANUW
REAL RDANUW
CHARACTER*8 CDANUW
LOGICAL LDANUW
!----------
! BGCINT CALLED FROM INITRE
!----------
ENTRY BGCINT
RETURN
!----------
! BGCIN CALLED FROM INITRE
!----------
ENTRY BGCIN(KEYWRD,ARRAY,LNOTBK,LKECHO)
!
  CALL ERRGRO (.TRUE.,11)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  RDANUW = ARRAY(1)
  CDANUW(1:8) = KEYWRD(1:8)
  LDANUW = LKECHO
  LDANUW = LNOTBK(1)
!
RETURN
!----------
! BGCGO CALLED FROM GRINCR
!----------
ENTRY BGCGO(L)
  L=.FALSE.
RETURN
!----------
! BGCGRO CALLED FROM GRINCR
!----------
ENTRY BGCGRO(I1,I2)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  IDANUW = I1
  IDANUW = I2
!
RETURN
!----------
! BGCFVS CALLED FROM GRADD
!----------
ENTRY BGCFVS(I1)
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
  IF(.TRUE.)RETURN
  IDANUW = I1
!
RETURN
!
END
