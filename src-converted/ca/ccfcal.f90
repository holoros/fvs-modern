SUBROUTINE CCFCAL(ISPC,D,H,JCR,P,LTHIN,CCFT,CRWDTH,MODE)
IMPLICIT NONE
!----------
! CA $Id$
!----------
!  THIS ROUTINE COMPUTES CROWN WIDTH AND CCF FOR INDIVIDUAL TREES.
!  CALLED FROM DENSE, PRTRLS, SSTAGE, AND CVCW.
!
!  ARGUMENT DEFINITIONS:
!    ISPC = NUMERIC SPECIES CODE
!       D = DIAMETER AT BREAST HEIGHT
!       H = TOTAL TREE HEIGHT
!     JCR = CROWN RATIO IN PERCENT (0-100)
!       P = TREES PER ACRE
!   LTHIN = .TRUE. IF THINNING HAS JUST OCCURRED
!         = .FALSE. OTHERWISE
!    CCFT = CCF REPRESENTED BY THIS TREE
!  CRWDTH = CROWN WIDTH OF THIS TREE
!    MODE = 1 IF ONLY NEED CCF RETURNED
!           2 IF ONLY NEED CRWDTH RETURNED
!----------
!OMMONS
!
!
INCLUDE 'PRGPRM.f90'
!
!
INCLUDE 'PLOT.f90'
!
!
!OMMONS
!----------
!  DIMENSION AND DATA STATEMENTS FOR INTERNAL VARIABLES.
!
!  CROWN WIDTH EQUATIONS FOR REGION 5:
!  FROM WARBINGTON/LEVITAN & DIXON --- SEE DOCUMENTATION IN
!                                      SUBROUTINE **R5CRWD**
!
!  CROWN WIDTH EQUATIONS FOR REGION 6:
!  FROM DONNELLY --- SEE DOCUMENTATION IN SUBROUTINE **R6CRWD**
!
!  CCF DERIVED FROM CROWN WIDTHS CALCULATED WITH R5 EQUATIONS
!
! SPECIES ORDER IN CA VARIANT:
!  1=PC  2=IC  3=RC  4=WF  5=RF  6=SH  7=DF  8=WH  9=MH 10=WB
! 11=KP 12=LP 13=CP 14=LM 15=JP 16=SP 17=WP 18=PP 19=MP 20=GP
! 21=JU 22=BR 23=GS 24=PY 25=OS 26=LO 27=CY 28=BL 29=EO 30=WO
! 31=BO 32=VO 33=IO 34=BM 35=BU 36=RA 37=MA 38=GC 39=DG 40=FL
! 41=WN 42=TO 43=SY 44=AS 45=CW 46=WI 47=CN 48=CL 49=OH 50=RW
!----------
INTEGER JCR,ISPC,MODE
REAL CRWDTH,CCFT,P,H,D,CRWD5
LOGICAL LTHIN
INTEGER IDANUW
LOGICAL LDANUW
!----------
!  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
!----------
IDANUW = JCR
LDANUW = LTHIN
!----------
!  INITIALIZE RETURN VARIABLES.
!----------
CCFT = 0.
CRWDTH = 0.
!----------
!  CALL R5CRWD TO GET CROWN WIDTH FOR CCF CALCULATIONS.
!----------
CALL R5CRWD (ISPC,D,H,CRWD5)
!----------
!  COMPUTE CROWN WIDTH
!----------
IF(IFOR .GT. 5) GO TO 100
CRWDTH = CRWD5
GO TO 200
!----------
!  REGION 6 CROWN WIDTH
!----------
100 CONTINUE
CALL R6CRWD (ISPC,D,H,CRWDTH)
200 CONTINUE
!----------
!  LIMIT CROWN WIDTH FOR PRINTING ON TREELIST.
!----------
IF(CRWDTH .GT. 99.9) CRWDTH=99.9
!----------
!  COMPUTE CCF
!----------
IF(MODE.EQ.1) THEN
  CCFT= CRWD5  * CRWD5  * 0.001803
  CCFT = CCFT * P
ENDIF
!
RETURN
END
