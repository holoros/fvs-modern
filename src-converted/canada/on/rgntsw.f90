      IF(H1.GT.4.5) THEN
      CALL ONHTDBH(ISPC,D1,BAJ,H1,RMSQD,NEWDBH)
      D1 = NEWDBH
      ENDIF
    CALL ONHTDBH(ISPC,D2,BAJ,H2,RMSQD,NEWDBH)
    D2 = NEWDBH
      DGJ=(D2-D1)*XRDGRO
      D2=D+DGJ

!     COMPUTE ADJUSTMENT TO STAND BASAL AREA AND CCF FOR NEXT SUBCYCLE.
!     ASSUME A 1.5 PERCENT ANNUAL MORTALITY RATE.

