SUBROUTINE GEMCR (IMODTY,IS,CR,BAU,BAT,HF,DF,H,RELDEN,PCTI)
IMPLICIT NONE
!----------
! CR $Id$
!----------
!    THIS SUBROUTINE RETURNS CROWN RATIO
!----------
!  SPECIES ORDER:
!   1=AF,  2=CB,  3=DF,  4=GF,  5=WF,  6=MH,  7=RC,  8=WL,  9=BC, 10=LM,
!  11=LP, 12=PI, 13=PP, 14=WB, 15=SW, 16=UJ, 17=BS, 18=ES, 19=WS, 20=AS,
!  21=NC, 22=PW, 23=GO, 24=AW, 25=EM, 26=BK, 27=SO, 28=PB, 29=AJ, 30=RM,
!  31=OJ, 32=ER, 33=PM, 34=PD, 35=AZ, 36=CI, 37=OS, 38=OH
!
!  SPECIES EXPANSION:
!  UJ,AJ,RM,OJ,ER USE CR JU
!  NC,PW USE CR CO
!  GO,AW,EM,BK,SO USE CR OA
!  PB USES CR AS
!  PM,PD,AZ USE CR PI
!  CI USES CR PP
!----------
INTEGER IS,IMODTY,ICRFLG
REAL PCTI,RELDEN,H,DF,HF,BAT,BAU,CR,CL
ICRFLG=0
!----------
!  BRANCH TO EQUATIONS FOR APPROPRIATE SPECIES.
!----------
SELECT CASE(IS)
!----------
!  SUB-ALPINE FIR
!  CORKBARK FIR
!
!  SWMC,SWPP USE CORKBARK EQN FOR GENGYM SWMC
!  OTHER MODEL TYPES USE AF/CB EQN FROM GENGYM S-F & LP
!----------
CASE(1,2)
  IF(IMODTY .LE. 2) THEN
    CL = 0.50706 + 0.73070 * HF
  ELSE
    CL = 0.36135 + 0.57085 * HF
  ENDIF
!----------
! DOUGLAS-FIR --- SWMC TYPE
!----------
CASE(3)
  CL =  6.47479 + 0.54482 * DF + 0.50703 * HF - 0.03326 * BAT
!----------
!  GRAND FIR --- USE WHITE FIR EQUATION
!  WHITE FIR
!----------
CASE(4,5)
  CL = 6.22959 + 0.67587 * HF - 0.03098 * BAT
!----------
!  MOUNTAIN HEMLOCK
!  MH EQUATION FROM NORTH IDAHO VARIANT, HABITAT TYPE 710 (TSME/XETE),
!  BITTERROOT NATIONAL FOREST
!----------
CASE(6)
  CR = 0.3450 - 0.00264*BAT + 0.00000512*RELDEN*RELDEN &
          - 0.25138*ALOG(H) + 0.05140*ALOG(PCTI)
  ICRFLG=1
!----------
!  WESTERN RED CEDAR
!  EQN FROM NI VARIANT, HABITAT TYPE 550 (THPL/OPHO)
!----------
CASE(7)
  CR = -1.6053 + 0.17479*ALOG(BAT) -0.00183*RELDEN &
          - 0.00560*DF + 0.11050*ALOG(PCTI)
  ICRFLG=1
!----------
!  WESTERN LARCH
!  EQN FROM NI VARIANT, 260 HABITAT TYPE, BITTERROOT NF
!----------
CASE(8)
  CR = 0.03441 - 0.00204*BAT + 0.30066*ALOG(DF) &
          - 0.59302*ALOG(H)
  ICRFLG=1
!----------
!  LODGEPOLE PINE
!----------
CASE(11)
  CL = 5.00215 + 0.06334 * HF + 0.88236 * DF - 0.03821 * BAU
!----------
!  BRISTLECONE PINE
!  LIMBER PINE
!  PINYON PINE, SINGLELEAF PINYON, BORDER PINYON, ARIZONA PINYON PINE
!  WHITEBARK PINE
!  UTAH JUNIPER, ALLIGATOR JUNIPER, ROCKY MTN JUNIPER, ONESEED JUNIPER
!  EASTERN REDCEDAR
!  GAMBEL OAK, ARIZONA WHITE OAK, EMORY OAK, BUR OAK, SILVERLEAF OAK
!  OTHER SOFTWOODS
!----------
CASE(9,10,12,14,16,23:27,29:35,37)
  CL = -0.59373 + 0.67703 * HF
!----------
!  PONDEROSA PINE, CHIHUAHUA PINE
!  SWMC AND SWPP HAVE THEIR OWN EQUATIONS.
!  OTHER MODEL TYPES USE THE BLACK HILLS EQUATION
!----------
CASE(13,36)
!
!  PONDEROSA PINE --- SWMC TYPE
!
  IF(IMODTY .EQ. 1)THEN
    CL = 5.63367 + 0.56252 * HF - 0.06411 * BAT
!
!  PONDEROSA PINE --- SWPP TYPE
!
  ELSEIF (IMODTY .EQ. 2) THEN
    CL = 4.35671 + 0.84714 * DF + 0.32549 * HF - 0.03802 * BAT
!
!  PONDEROSA PINE  --- BLACK HILLS PP TYPE
!
  ELSE
    CL =  3.49178 + 0.80767 * DF + 0.17421 * HF - 0.03272 * BAT
  ENDIF
!----------
!  WESTERN WHITE PINE
!----------
CASE(15)
  CL = 3.03832 + 0.65587 * HF - 0.01792 * BAT
!----------
!  BLUE SPRUCE
!----------
CASE(17)
  CL = 3.61635 + 0.93639 * DF + 0.61547 * HF - 0.02360 * BAT
!----------
!  ENGELMANN SPRUCE
!  SWMC & SWPP USE SWMC EQN
!  ALL OTHER MODEL TYPES USE S-F EQN
!----------
CASE(18)
!
!  ENGELMANN SPRUCE --- SWMC TYPE
!
  IF(IMODTY .LE. 2) THEN
    CL = 1.05857 + 0.68442 * HF
!
!  ENGELMANN SPRUCE --- SPRUCE/FIR MODEL TYPE
!
  ELSE
    CL = 3.22244 + 0.44315 * HF + 0.44755 * DF
  ENDIF
!----------
!  WHITE SPRUCE
!----------
CASE(19)
  CL = 0.15768 + 0.74697 * HF
!----------
!  ASPEN, PAPER BIRCH
!  NARROWLEAF COTTONWOOD, PLAINS COTTONWOOD
!  OTHER HARDWOODS
!----------
CASE(20,21,22,28,38)
  CL = 5.17281 + 0.32552 * HF - 0.01675 * BAT
!----------
!  PLACE FOR OTHER SPECIES.
!----------
CASE DEFAULT
  CL = 1.
!
END SELECT
!
!----------
!  IF CROWN LENGTH WAS PREDICTED, CONVERT TO CROWN RATIO
!----------
IF(ICRFLG .EQ. 0)THEN
  IF(CL .LT. 1.0) CL = 1.0
  IF(CL .GT. HF) CL = HF
  CR = CL / HF
ENDIF
!
RETURN
END
