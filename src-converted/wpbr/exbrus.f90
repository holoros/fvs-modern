RETURN

!     CALLED FROM MAIN
!
ENTRY BRSETP
RETURN

ENTRY BRPR
RETURN

ENTRY BRROUT
RETURN

!     CALLED FROM OPLIST
!
ENTRY BRKEY (KEY,KEYWRD)
