LOGICAL LKECHO,LPRTND,LTMP
CHARACTER*4 CARRAY(*),CH1_4,CARRAY2(*)
CHARACTER*8 KEYWRD
CHARACTER NPLT*26,ITITLE*72
REAL RDANUW
