  IF(I1.EQ.0) GOTO 30
  I2=ISCT(ISPC,2)

  XRDGRO=XRDMLT(ISPC)
  XMX=XMAX(ISPC)
  XMN=XMIN(ISPC)

  DO I3=I1,I2
    I=IND1(I3)
    D=DBH(I)
    IF(D.GE.XMX) GOTO 25
    IF(LESTB .AND. I.LT.ITRNIN) GOTO 25
